            <h6 class="question">Question 1</h6>
            Do you find that all living beings need the same kind of food?

            <h6 class="answer">Answer 1</h6>
            No, all living beings do not need the same kind of food because all organism eat different kind of food
            as per their body requirement<br>
            1)Some organism eat plants as food, and they are called herbivores for ex:cow ,goat elc.<br>
            2) Some organism feed on other animals, and they are called as Carnivores for ex: lion ,tiger etc.<br>
            3) Some others eat food from plant sources as well as animals, and such living beings are called
            omnivores for ex:crow ,cat .<br><br>

