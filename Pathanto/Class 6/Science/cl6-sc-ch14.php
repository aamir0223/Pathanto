      <h6 class="question">Question 7</h6>
      How are clouds formed?<br>

      <h6 class="answer">Answer 7</h6>
      s

      Clouds are formed by the process of evaporation and condensation.The process of condensation plays an important
      role in bringing water back to the surface of the earth. At a certain height from the surface of the earth, it
      gets cooler. When the air moves up, it gets cooler and air becomes so cool that the water vapor present in it
      condenses to form tiny drops of water called droplets. It is these tiny droplets that remain floating in the air
      and appear to us like clouds.<br><br>


