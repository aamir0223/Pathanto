      <h6 class="question">Question 8:</h6>
      You are given an iron strip. How will you make it into a magnet?<br>

      <h6 class="answer">Answer 8:</h6>

      Place one pole of a bar magnet near one end of the iron strip. Move the bar magnet along the length of the iron
      strip starting from one end to the other end

      Without lifting the bar magnet Move the magnet again along the iron bar in the same direction as you did before.
      Repeat this process for about 30-40 times.<br><br>
      <h6 class="question">Question 9:</h6>
      How is a compass used to find directions?

      <h6 class="answer">Answer 9:</h6>

      A we know compass is a small case of glass. A magnetised needle is pivoted inside the box. The needle can rotate
      freely. it also has a dial with directions marked on it. The compass is kept at the place where we need to find
      the directions. When the needle comes to rest it indicates north-south direction. The compass is then rotated
      until the north and south marked on the dial are at the two ends of the needle.<br><br>





