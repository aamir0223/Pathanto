               <h6 class="question">Question 9</h6>.
               Why should an electrician use rubber gloves while repairing an electric switch at your home? Explain.

               <h6 class="answer">Answer 9:</h6>

               the rubber gloves will save the electrician from any electric shock while repairing an electric switch or
               appliance because rubber is a bad conductor of electricity so it does not allow the electric current to
               pass through it.<br><br>

               <h6 class="question">Question 10</h6>.
               The handles of tools like screwdrivers and pliers used by electricians for repair work usually have
               plastic or rubber covers on them. Can you explain why?
               <h6 class="answer">Answer 9:</h6>

               Plastic or rubber is an insulator which does not allow electric current to pass through it. The handles
               of tools like screwdrivers and pliers used by electricians for repair work usually have plastic or rubber
               cover on them so that they do not allow the current to pass through them and save the electrician from
               any electric shock.<br><br>


