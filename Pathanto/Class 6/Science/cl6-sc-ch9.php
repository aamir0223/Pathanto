               <h6 class="answer">Answer 1</h6>
               Cactus are adapted to survive in a desert as they have<br>
               (i) The leaves present in cactus in shape of spiny that help to prevent water loss through
               transpiration.<br>
               (ii) Stem is modified in such a way that it performs photosynthesis and conserves water.<br>
               (iii) It has long roots that go deep inside the soil for absorbing water<br>

