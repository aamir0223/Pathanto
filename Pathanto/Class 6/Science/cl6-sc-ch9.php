               <h6 class="question">Question 1</h6>
               . What is a habitat?

               <h6 class="nswer">Answer 1</h6>
               A habitat is the type of natural environment in which a particular species of organism lives. The
               organisms depend on their habitat for their food, water, air, shelter , protection and other needs.
               Habitat means a dwelling place.it is characterized by both physical and biological features<br><br>

