      <h6 class="question">Question 5:</h6>
      How will you separate sand and water from their mixture?

      <h6 class="answer">Answer 5:</h6>

      In a mixture of sand and water,The mixture is allowed to stand without any disturbances. The heavier sand
      particles settle down at the bottom by the process of sedimentation and the water can be separated by decantation.
      Filtration can be used to separate components of a mixture of an insoluble solid and a liquid.<br><br>

