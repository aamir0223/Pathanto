      <h6 class="question">Question 1:</h6>
      Why do we need to separate different components of a mixture? Give two examples.

      <h6 class="answer">Answer 1</h6>

      When two or more substances are mixed together they form a mixture. Among different components of mixture there
      are many substances which are harmful or not useful for us. To remove these harmful we need to separate them. For
      example:<br>
      (a) Tea leaves are separated from the liquid with a strainer while preparing tea.<br>
      (b)Evaporation removes a liquid from a solution to leave a solid material.<br>
      (c)Filtration separates solids of different sizes<br><br>
      <h6 class="question">Qquestion 2:</h6>
      What is winnowing? Where is it used?

      <h6 class="answer">Answer 2:</h6>


      The process of separating heavier and lighter components of a mixture by wind is called winnowing. This method is
      used by farmers to separate lighter husk particles from heavier seeds of grain. In this method, heavier and
      lighter components of a mixture are separated by wind.<br><br>

