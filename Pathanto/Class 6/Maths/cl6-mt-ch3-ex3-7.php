               <h6 class="question">Question 11</h6> Find the LCM of the following numbers in which one number is the
               factor of the other.

               (a) 5, 20 (b) 6, 18 (c) 12, 48 (d) 9, 45<br>

               What do you observe in the results obtained?

               <h6 class="answer">Answer 11</h6>

               (a) 5, 20<br>
               LCM(5,20) = 2 × 2 × 5 = 20<br>

               (b) 6, 18<br>

               LCM(6,18) = 2 × 3 × 3 = 18<br>

               (c) 12, 48<br>

               LCM(12,48) = 2 × 2 × 2 × 2 × 3 = 48<br>

               (d) 9, 45<br>
               LCM(9,45) = 3 × 3 × 5 = 45<br>
               When a number is a factor of other number, then their LCM will be the larger number.

