               <h6 class="question">Question 1</h6>
               Renu purchases two bags of fertiliser of weights 75 kg and 69 kg. Find the maximum value of weight which
               can measure the weight of the fertiliser exact number of times.

               <h6 class="answer">Answer 1</h6>

               Weight of two bags of fertiliser = 75 kg and 69 kg<br>

               For maximum weight <br> HCF of 75 and 69<br>

               Factor of 75 = 3 × 5 × 5<br>

               Factor of 69 = 3 × 23<br>

               HCF = 3<br>

               3 kg of weight can measure the weight of the fertiliser exact number of times.<br><br>

               <h6 class="question">Question2</h6>
               Three boys step off together from the same spot. Their steps measure 63 cm, 70 cm and 77 cm respectively.
               What is the minimum distance each should cover so that all can cover the distance in complete steps?

               <h6 class="answer">Answer 2</h6>
               For minimum distance we need to find out LCM of 63cm,70cm and 77cm<br>
               First steps measure = 63 cm<br>

               Second steps measure = 70 cm<br>

               Third steps measure = 77 cm<br>


               LCM (70 , 77) = 2 × 3 × 3 × 5 × 7 × 11 = 6930<br>

               So, the minimum distance is 6930cm<br><br>

               <h6 class="question">Question 3</h6>
               The length, breadth and height of a room are 825 cm, 675 cm and 450 cm respectively. Find the longest
               tape which can measure the three dimensions of the room exactly.

               <h6 class="answer">Answer 3</h6>

               Length of a room = 825 cm<br>

               Breadth of a room = 675 cm<br>

               Height of a room = 450 cm<br>

               Factor of 825 = 3 × 5 × 5 × 11<br>

               Factor of 675 = 3 × 3 × 3 × 5 × 5<br>

               Factor of 450 = 2 × 3 × 3 × 5 × 5<br>

               HCF = 3 × 5 × 5 = 75 cm<br>

               Hence longest tape is 75 cm.<br><br>

