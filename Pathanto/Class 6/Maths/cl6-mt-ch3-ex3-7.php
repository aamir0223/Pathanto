               <h6 class="question">Question 6</h6> The traffic lights at three different road crossings change after
               every 48 seconds, 72 seconds and 108 seconds, respectively. If they change simultaneously at 7 a.m., at
               what time will they change simultaneously again?

               <h6 class="answer">Answer 6</h6>

               LCM of 48, 72, 108 <br>


               LCM(48,72,108) = 2 × 2 × 2 × 2 × 3 × 3 × 3 = 432<br>

               Here lights will change together after every 432 seconds <br>

               The lights will change at 7 minutes 12 seconds.<br>

