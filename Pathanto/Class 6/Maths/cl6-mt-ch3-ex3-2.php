               <h6 class="question">Question 1:</h6>
               What is the sum of any two<br>
               (a) Odd numbers<br>
               (b) Even numbers<br>

               <h6 class="answer"> Answer 1:</h6>

               (a) The sum of any two odd numbers is even numbers.<br>

               Examples: 5 + 7 = 12 ,

               17 + 15 = 32<br>

               (b) The sum of any two even numbers is even numbers<br>

               Examples: 4 + 6 = 10 ,

               14 + 34 = 48<br>

               <h6 class="question">Question 2:</h6>
               State whether the following statements are True or False:<br>

               (a) The sum of three odd numbers is even.<br>

               (b) The sum of two odd numbers and one even number is even.<br>

               (c) The product of three odd numbers is odd.<br>

               (d) If an even number is divided by 2, the quotient is always odd.<br>

               (e) All prime numbers are odd.<br>

               (f) Prime numbers do not have any factors.<br>

               (g) Sum of two prime numbers is always even.<br>

               (h) 2 is the only even prime number.<br>

               (i) All even numbers are composite numbers.<br>

               (j) The product of two even numbers is always even.<br>

               <h6 class="answer"> Answer 2:</h6>


               (a) False. Ex 3 + 5 + 7 = 15i.e odd number<br>

               (b) True. EX: 7 + 9 + 8 = 24 i.e is even number.<br>

               (c) True. Ex: 3 × 5 × 7 = 105 i.e is odd number.<br>

               (d) False. Ex: 10 ÷ 2 = 5 is even. <br>

               (e) False, Ex: 2 is a prime number but it is also an even number.<br>

               (f) False. <br>

               (g) False.
               Ex: 2 + 7 = 9 i.e odd number.<br>


               (h) True. 2 is the only even prime number.<br>

               (i) False. 2 is a prime number.<br>

               (j) True. Ex: 6 × 4 = 24 i.e even number.<br>

