<p><h5 class="question">Question 2</h5> Describe the different methods of asexual reproduction. Give examples.</strong><br />
<h6 class="answer">Answer </h6> The methods of asexual reproduction as follow <br />
(a) Fragmentation: organism breaks up into  two or more fragments. These fragments or pieces grow into new individuals.<br />

(b) Spore Formation:Spores are unicellular reproductive bodies present in sporangia. When Sporangia burst single-celled thin or thick walled structures called spores are obtained. Under favorable conditions, they develop into a new plant.<br />
(c) Budding:In yeast, The small bulb-like projection coming out from the yeast cell is called a bud. The bud gradually grows and bud gets detached from the parent cell and  forms a new yeast cell.<br />

(d) Vegetative propagation: Producing a new plant  using  vegetative parts of a plant like stems , leaves and root  is called vegetative propagation.</p>

