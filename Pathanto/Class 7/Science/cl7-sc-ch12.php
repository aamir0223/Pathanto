<p><h5 class="question">Question 7</h5>How does the process of fertilization take place in flowers?</strong><br /></p>
 <h6 class="answer">Answer </h6>
 When the pollen lands on a suitable stigma, it has to reach the female germ-cells which are in the ovary. A tube grows
out of the pollen grain and travels through the style to reach the ovary, it releases the male gametes. A male gamete fuses with a female gamete in the ovule. This process is known as fertilisation<br>
<br>

