<p><h5 class="question">Question 2</h5>What is sewage? Explain why it is harmful to discharge untreated sewage into rivers or seas. </strong></p>
<h6 class="answer">Answer </h6>
<p>Wastewater released by houses is called sewage . Untreated sewage should not be discharged untreated into the sea because sewage consists   some harmful substances and microorganisms  which  cause disease for  people .</p>

<p><h5 class="question">Question 3</h5> Why should oils and fats be not released in the drain? Explain.</strong></p>
<h6 class="answer">Answer </h6>
<p>Oils and fats should not be released in the drain because They can harden the soil and block the pipes .</p>

<p><h5 class="question">Question 4</h5> Describe the steps involved in getting clarified water from wastewater.</strong></p>
<h6 class="answer">Answer </h6>
<ul><li>Step 1:impurities like stones, plastic bags, cans etc are removed by filtration and sedimentation processes.<br></li>
<li>Step 2:Then the aeration process apply which allowed  Bacteria  to grow  .<br></li>
<li>Step 3: Bacteria eat away human waste from the wastewater.<br></li>
<li>Step 4:Chlorination is used to kill the germs in clarified water.<br></li>
<li>Step 5:So now the water becomes fit  for human consumption.<br></li>
</ul>

<p><h5 class="question">Question 5</h5> What is sludge? Explain how it is treated.</strong></p>
<h6 class="answer">Answer </h6>
<p>Solids like faeces settle  at the bottom This is the sludge. Sludge is removed using a skimmer and then transferred to a tank where it is decomposed by anaerobic bacteria to produce biogas.</p>
