        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What is dispersion of light? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Dispersion of light is the process by which white light is separated into its component colors when passing through a medium such as a glass prism. This occurs because different colors (or wavelengths) of light are refracted, or bent, by different amounts as they pass through the medium</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question"> What is the role of the iris in the human eye?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The iris is the colored part of the eye. It controls the size of the pupil and thus the amount of light that enters the eye. In dim light, the iris widens the pupil to allow more light in. In bright light, it narrows the pupil to limit the amount of light entering the eye</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">What is the role of the lens in the human eye?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The lens of the eye is a clear, flexible structure that sits behind the iris. Its primary function is to focus light onto the retina. It does this by changing shape – it flattens to focus on distant objects and thickens to focus on near objects.</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">What are rods and cones in the human eye?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Rods and cones are types of photoreceptor cells in the retina of the eye. Cones are responsible for color vision and function best in relatively bright light conditions. Rods, on the other hand, do not distinguish colors, but are responsible for vision in low light conditions.

</p>
      
