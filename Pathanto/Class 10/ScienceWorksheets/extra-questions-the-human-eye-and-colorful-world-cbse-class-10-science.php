      <h4 class="question">Question 7</h4>
      <h1 class="important-question">How do we see colors?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Color perception starts with light entering the eye and being absorbed by special cells in the retina called cones. Humans usually have three types of cones that are sensitive to short wavelengths (blue), middle wavelengths (green), and long wavelengths (red). The brain combines the information from these different types of cones to perceive all the possible colors.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">What causes a rainbow?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A rainbow is formed when white light, like sunlight, is broken up into its component colors by water droplets in the atmosphere. This process, known as dispersion, causes the light to spread out into a range of colors from red to violet. Each droplet of water acts as a tiny prism, refracting or bending the light, reflecting it inside on the back of the droplet, and refracting it again as it exits the droplet. This creates the spectrum of colors we see in a rainbow..</p>
      
      
