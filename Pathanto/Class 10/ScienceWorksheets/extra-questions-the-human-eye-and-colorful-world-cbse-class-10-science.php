      <h4 class="question">Question 1</h4>
      <h1 class="important-question">
  What is the structure of the human eye? 
      </h1>
       <h4 class="answer">Answer </h4>
      <p class="important-answer"> The human eye is a complex organ structured to receive light and transmit the interpreted information to the brain. It consists of various parts like the cornea, iris, pupil, lens, retina, and optic nerve. The cornea is the clear front surface that allows light to enter. The iris is the colored part that controls the light's amount that enters the eye by adjusting the pupil's size. The lens focuses the incoming light onto the retina, which is filled with light-sensitive cells that trigger nerve impulses. These impulses are then sent to the brain via the optic nerve, which interprets them as images.</p>
      
