      <h4 class="question">Question 2</h4>
      <h1 class="important-question">What is myopia and how can it be corrected?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Myopia, also known as nearsightedness, is a condition where the eye can see close objects clearly, but distant objects appear blurry. This occurs when the eye is longer than normal or the cornea is excessively curved, causing light to focus in front of the retina instead of directly on its surface. Myopia can be corrected with glasses, contact lenses, or refractive surgery. These corrective methods aim to reduce the eye's focusing power by moving the focus point back onto the retina.</p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question">What is hypermetropia and how can it be corrected?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Hypermetropia, or farsightedness, is a condition where distant objects can be seen clearly, but close ones appear blurry. This happens when the eye is shorter than normal, causing light to focus behind the retina. Hypermetropia can be corrected with glasses or contact lenses that have a 'converging' or convex lens. This increases the eye's focusing power and moves the focus point onto the retina. Refractive surgery is another option for correction.</p>

