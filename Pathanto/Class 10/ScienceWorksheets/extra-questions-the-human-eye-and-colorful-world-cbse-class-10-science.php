      <h4 class="question">Question 9</h4>
      <h1 class="important-question">What is the power of accommodation of the eye?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The power of accommodation refers to the eye's ability to adjust its focus from distant to near objects. The lens changes its shape to focus light on the retina. For distant objects, the lens becomes thinner, and for close objects, it becomes thicker. This adjustment in focus is known as accommodation.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">Why do stars twinkle?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Stars twinkle because of the way light is affected as it passes through the Earth's atmosphere. This phenomenon, known as astronomical scintillation, is caused by variations in air density in the atmosphere that refract or bend the path of light, causing it to shift direction rapidly. To observers on the ground, this shifting appears as a twinkling effect.</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">What is the blue moon phenomenon?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A "blue moon" doesn't refer to the moon's color. Instead, it refers to the occurrence of two full moons within one calendar month</p>
      
