      <h4 class="question">Question 4</h4>
      <h1 class="important-question">What is presbyopia? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Presbyopia is an age-related condition where the ability to focus up close becomes more difficult. As the eye ages, the lens can lose its ability to change shape enough to allow the eye to focus close objects clearly. Presbyopia is corrected with reading glasses or bifocal lenses.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">What is the blind spot in the eye?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">The blind spot is an area on the retina where the optic nerve leaves the eye. This area is called the blind spot because it lacks photoreceptors, so light focused on this spot cannot be seen.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">What is color blindness?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Color blindness is a genetic condition where a person can't distinguish between certain colors. This condition is caused by an absence or malfunction of certain color-sensitive cells in the retina. The most common form is red-green color blindness, where individuals have difficulty telling the difference between red and green.

</p>
      
