        <h4 class="question">Question 16</h4>
      <h1 class="important-question">What is the Tyndall effect?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The Tyndall effect, also known as Tyndall scattering, is the scattering of light by particles in a colloid or in a very fine suspension. This effect causes beams of light to become visible, like a beam of sunlight in a room filled with smoke. The color of the scattered light depends on the size of the particles and the wavelength of the light.
