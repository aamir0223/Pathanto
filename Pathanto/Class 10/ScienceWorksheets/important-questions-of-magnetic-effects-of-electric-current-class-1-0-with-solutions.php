      <h1 class="important-question">Explain the concept of magnetic field lines.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Magnetic field lines are imaginary lines that represent the direction of the magnetic field. They emerge from the north pole and enter the south pole, forming closed loops.</p>

