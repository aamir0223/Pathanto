      <h1 class="important-question"> How is the magnetic field produced around a current-carrying conductor?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">When current flows through a conductor, it produces a magnetic field around it. The magnetic field lines form concentric circles around the conductor.</p>

