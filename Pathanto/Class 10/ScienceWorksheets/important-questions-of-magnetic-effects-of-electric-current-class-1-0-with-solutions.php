      <h4 class="question">Question 4</h4>
      <h1 class="important-question"> Describe the Right-Hand Thumb Rule.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The Right-Hand Thumb Rule states that if you point your thumb in the direction of the current in a straight conductor, the curled fingers of your right hand indicate the direction of the magnetic field around the conductor.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">What is the magnetic field due to a current through a straight conductor?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">The magnetic field around a straight conductor is circular, and its strength increases with an increase in current.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">How does the magnetic field change when the current flows through a circular loop?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">The magnetic field is concentrated at the center of the loop, and its strength depends on the radius of the loop and the current flowing through it.

</p>
      
