        <h4 class="question">Question 16</h4>
      <h1 class="important-question">Explain Lenz's Law.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Lenz's Law states that the direction of the induced current in a coil is such that it opposes the change in magnetic flux that produced it.
.</p>

    <h4 class="question">Question 17</h4>
      <h1 class="important-question">What is the role of a commutator in an electric motor?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The commutator in an electric motor reverses the direction of the current in the coil, ensuring that the coil continues to rotate in the same direction.
.</p>

    <h4 class="question">Question 18</h4>
      <h1 class="important-question">Differentiate between direct current (DC) and alternating current (AC).</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> Direct current flows in one direction continuously, while alternating current periodically reverses its direction.
.</p>

    <h4 class="question">Question 19</h4>
      <h1 class="important-question">Explain the concept of eddy currents.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Eddy currents are circulating currents induced in a conductor when it is exposed to a changing magnetic field. They can cause energy loss in electrical devices
.</p>

    <h4 class="question">Question 20</h4>
      <h1 class="important-question">How does the Earth behave as a magnet?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The Earth behaves like a magnet with a magnetic north and south pole. The magnetic field lines emerge from the south pole and enter the north pole, similar to a bar magnet.
.</p>


