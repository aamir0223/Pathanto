      <h4 class="question">Question 9</h4>
      <h1 class="important-question">Define the term electromagnet.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">An electromagnet is a temporary magnet created by passing an electric current through a coil of wire, often wound around an iron core.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question"> How does the strength of an electromagnet change with an increase in current?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The strength of an electromagnet increases with an increase in the current flowing through the coil.</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">Explain the working of a simple electric motor.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A simple electric motor converts electrical energy into mechanical energy. It consists of a coil placed in a magnetic field, and when current flows through the coil, it experiences a force that causes it to rotate.</p>
      
