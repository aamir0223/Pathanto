  <h4 class="question">Question 23</h4>
      <h1 class="important-question">Explain the magnetic field due to a current in a solenoid.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Solenoids play a crucial role in generating strong and uniform magnetic fields. When an electric current flows through a solenoid—a coil of wire wound in a helical manner—it produces a magnetic field with parallel field lines along the axis of the solenoid. This property is extensively utilized in various devices, including electromagnets and transformers.
.</p>

 <h4 class="question">Question 24</h4>
