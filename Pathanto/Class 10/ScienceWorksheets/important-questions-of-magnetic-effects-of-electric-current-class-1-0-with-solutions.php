        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What is electromagnetic induction?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Electromagnetic induction is the process of generating an electromotive force (EMF) in a coil by changing the magnetic field around it, either by moving the coil or changing the current in a nearby coil.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">State Faraday's law of electromagnetic induction.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Faraday's law states that the magnitude of the induced EMF is directly proportional to the rate of change of magnetic flux through the coil.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">How does a transformer work?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A transformer works on the principle of electromagnetic induction. It consists of two coils (primary and secondary) placed close to each other. When the current in the primary coil changes, it induces an EMF in the secondary coil, leading to the transfer of electrical energy.</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">Define magnetic flux.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> Magnetic flux is the measure of the total magnetic field passing through a given area, and it is calculated as the product of the magnetic field strength, area, and the angle between them.
</p>
      
