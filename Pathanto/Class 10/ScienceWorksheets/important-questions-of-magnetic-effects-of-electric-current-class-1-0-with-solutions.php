      <h4 class="question">Question 7</h4>
      <h1 class="important-question"> Explain the magnetic field due to a current in a solenoid.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A solenoid is a coil of wire, and when current flows through it, it produces a strong and uniform magnetic field inside it. The magnetic field lines run parallel to the axis of the solenoid.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">What is the force experienced by a current-carrying conductor in a magnetic field?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The conductor experiences a force when placed in a magnetic field. The force is perpendicular to both the current direction and the magnetic field direction.</p>
      
      
