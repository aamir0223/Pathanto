      <h4 class="question">Question 7</h4>
      <h1 class="important-question">What is the ozone layer, and why is it important?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The ozone layer is like Earth's superhero shield, protecting us from harmful UV rays. It's our very own sunscreen. Without it, we'd be at risk of sunburns, just like Earth forgetting to put on sunscreen!</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">Explain how human activities contribute to ozone layer depletion.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Imagine humans as accidental villains, releasing harmful stuff like CFCs. These substances are like tiny wrecking balls, breaking down the ozone shield. We need to stop being unintentional troublemakers!</p>
