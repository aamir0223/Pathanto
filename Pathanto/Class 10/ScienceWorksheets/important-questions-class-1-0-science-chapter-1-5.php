      <h4 class="question">Question 2</h4>
      <h1 class="important-question">Explain the concept of food chains.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Think of a food chain like a menu for nature. It starts with plants, the chefs of the kitchen, who make their food through photosynthesis. Then, animals come in to eat the plants, and other animals follow, creating a chain of who eats whom.</p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question">How do food webs differ from food chains?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Picture a food web as a massive dinner party with lots of interconnected tables. Unlike a food chain, it shows the complicated relationships among different organisms. It's like saying, "Hey, in nature, everyone is connected in more ways than we realize!"</p>

