        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What are the dangers of plastic pollution in marine environments?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Picture the ocean as a giant bathtub, and plastic pollution as the dirty water. Marine animals mistake plastic for food, leading to ingestion and entanglement. It's like filling their homes with harmful trash, disrupting the delicate balance of marine life.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">How does overfishing impact marine ecosystems?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Overfishing is like taking too many snacks from the fridge without restocking. It depletes fish populations, disrupts the food chain, and can even lead to the collapse of entire ecosystems. We need to fish responsibly to maintain a healthy balance.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">Explain the role of the ozone layer in protecting life on Earth.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The ozone layer acts like Earth's sunglasses, blocking harmful UV rays. Without this protective shield, we'd be exposed to intense sunlight, leading to skin cancers, cataracts, and harm to both plants and animals. It's our natural sunscreen.</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">How does the use of chemical fertilizers and pesticides affect the environment?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Chemical fertilizers and pesticides are like strong medicine for plants. While they help crops grow, they can also harm the soil, water, and even non-target organisms. It's a balancing act between boosting agriculture and protecting the environment.</p>
      
