      <h4 class="question">Question 9</h4>
      <h1 class="important-question">What are the consequences of ozone layer depletion?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> Ozone depletion is like leaving the front door wide open for UV rays. It can harm our skin, mess with the ocean's balance, and even mess up our crops. It's like Earth losing its protective shield, and we need to fix that.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">How can we prevent further depletion of the ozone layer?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> It's time for us to be Earth's superhero sidekicks! We can stop using harmful substances like CFCs and support global agreements (like the Montreal Protocol) that aim to put an end to these bad guys.
</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">Explain the concept of acid rain and its impact on the environment.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Acid rain is like nature's sour surprise. When pollutants from burning fossil fuels mix with rain, it becomes acidic. This acidic rain harms soil, water, and plants, disrupting ecosystems and even damaging buildings and statues over time.</p>
      
