        <h4 class="question">Question 18</h4>
      <h1 class="important-question">Explain the concept of eutrophication and its impact on aquatic ecosystems.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Eutrophication is like overfeeding a pond. Excessive nutrients, often from agricultural runoff, lead to algae overgrowth. When these algae die, they consume oxygen during decomposition, harming aquatic life and creating "dead zones."
</p>
      
      
        <h4 class="question">Question 19</h4>
      <h1 class="important-question">How does the loss of biodiversity affect the stability of ecosystems?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> Biodiversity is like a safety net for ecosystems. The more diverse, the more resilient. Losing species is like removing threads from a safety net – it weakens the structure and makes the entire ecosystem more vulnerable to disturbances.</p>
      
