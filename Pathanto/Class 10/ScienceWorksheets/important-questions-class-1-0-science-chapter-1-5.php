      <h4 class="question">Question 4</h4>
      <h1 class="important-question">What are the consequences of disrupting a food chain in an ecosystem?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Messing with a food chain is like pulling a Jenga block – the whole tower could fall! If you remove one player, like a predator or prey, it creates a domino effect, messing up the balance of who eats whom and causing chaos in the ecosystem.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">How do human activities impact the environment?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">We humans are a big deal! Our actions, like chopping down trees and spewing out pollution, mess with the environment. It's like throwing a party in nature's house and leaving a huge mess behind. We need to be careful guests!</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">Explain the greenhouse effect and its role in climate change.</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Think of Earth as a cozy blanket. The greenhouse effect is like adding an extra layer – it keeps us warm. But when we humans release too many gases (like carbon dioxide), it's like wrapping ourselves in a super thick blanket, making the Earth too warm, and that's not good.</p>
      
