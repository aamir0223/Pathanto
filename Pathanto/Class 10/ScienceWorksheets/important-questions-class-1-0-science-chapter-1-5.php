        <h4 class="question">Question 20</h4>
      <h1 class="important-question">Explain the concept of renewable and non-renewable resources.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Imagine resources as snacks. Renewable resources, like solar and wind energy, are like snacks that keep getting restocked. Non-renewable resources, like fossil fuels, are like a limited bag of chips – once they're gone, they're gone. Balancing both ensures a sustainable snack shelf for the future.</p>
      
      

