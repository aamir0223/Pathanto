      <h4 class="question">Question 7</h4>
      <h1 class="important-question">What is a valence electron and why is it significant?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A valence electron is the outermost electron of an atom. These electrons are important because they are involved in chemical reactions and bond formation. The number of valence electrons determines the chemical composition of an element</p>

      <h4 class="question">Question 8</h4>
