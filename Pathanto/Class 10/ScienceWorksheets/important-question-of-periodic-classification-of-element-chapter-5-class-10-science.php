      <h4 class="answer">Answer </h4>
      <p class="important-answer">As you move from left to right at different periods in the periodic table the number of valence electrons increases from 1 to 8. This does not include transition metals with electron configurations</p>

      <h4 class="question">Question 9</h4>
      <h1 class="important-question">Why are noble gases inert?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Higher gases are ineffective because their outermost electron shells are full, containing about eight electrons. This stable electronic structure means that there is no need to gain, lose or share electrons, so they are generally not involved in chemical reactions.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">What are transition elements?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Transition factors are factors in groups 3-12 of the periodic table. They are characterized by a partially filled d orbital, multiple oxidation states, and generally stable ions. They include metals such as iron, copper and silver.</p>
      
