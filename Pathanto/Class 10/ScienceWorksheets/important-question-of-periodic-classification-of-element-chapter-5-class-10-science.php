      <h4 class="question">Question 4</h4>
      <h1 class="important-question">How does metallic character change as we move across a period and down a group?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">As we move through the period from left to right, the metallic color decreases. This is due to increased nuclear energy attracting electrons closer to each other, reducing the tendency to lose electrons. As we move down the group, the metallic collar increases. Due to the increase in the number of shells, the outermost electrons are further away from the nucleus, they can be lost more easily, which is characteristic of metals</p>

