        <h4 class="answer">Answer </h4>
      <p class="important-answer">As we move from left to right in the period, atomic mass decreases due to increased atomic energy attracting electrons closer to the nucleus but as we move down the cluster the atomic mass increases as shell mass increases and the outermost electrons are far from the nucleus</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question"> What are isotopes and isobars?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Isotopes are atoms of the same element with the same number of atoms but different masses. They have the same number of protons but different numbers of neutrons. For example, 1H, 2H (Deuterium), and 3H (Tritium) are isotopes of hydrogen. However, even strings are composed of atoms of the same size but with different atomic numbers. They have the same number of nuclei (protons + neutrons) but different numbers of protons. For example, 18Ar40 and 20Ca40 are isobaric.</p>
