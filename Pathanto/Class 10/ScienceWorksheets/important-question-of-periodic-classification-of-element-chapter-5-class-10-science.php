        <h4 class="question">Question 14</h4>
      <h1 class="important-question">What is the octet rule?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The octet rule states that atoms must gain, lose, or share electrons in order to have a complete outer shell of eight electrons. This gives them stability. This rule applies to many things but there are many exceptions, especially for heavier items.</p>
      
      
