        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What is fertilization?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Fertilization is the process of fusion of the male and female gametes resulting in the formation of a zygote. This zygote develops into a new individual.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">What is the role of the placenta in human reproduction?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The placenta is a temporary organ that connects the developing fetus via the umbilical cord to the uterine wall to allow nutrient uptake, waste elimination, and gas exchange via the mother's blood supply. It produces hormones that are necessary for pregnancy, such as progesterone and estrogen, which maintain the lining of the uterus and stimulate the development of mammary glands, respectively. The placenta also serves as a barrier to most harmful substances, protecting the fetus, although it's not entirely impermeable and some substances such as alcohol, drugs, and certain diseases can cross this barrier and harm the fetus.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">What are sexually transmitted diseases (STDs)?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">STDs are infectious diseases that spread from person to person through sexual contact. They are caused by bacteria, viruses, or parasites that can lead to severe health problems if left untreated. Examples include HIV/AIDS, syphilis, gonorrhea, and herpes. It's important to practice safe sex and get regular check-ups to prevent these diseases</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">What is the importance of reproduction in organisms?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Reproduction is vital for organisms as it ensures the continuation of a species or a generation. It results in offspring that are genetically similar to the parents. Reproduction also leads to variations in species which are necessary for evolution and adaptation to changing environments.

</p>
      
