        <h4 class="question">Question 16</h4>
      <h1 class="important-question">What are the different methods of contraception? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Contraception methods are used to prevent pregnancy. They include barrier methods (like condoms and diaphragms), hormonal methods (like birth control pills and patches), intrauterine devices (IUDs), emergency contraceptives, and permanent methods like sterilization. Each method works differently and the choice depends on an individual's health, frequency of sexual activity, number of sexual partners, and desire to have children in the future.
.</p>
      
      
        <h4 class="question">Question 17</h4>
      <h1 class="important-question">Explain the process of fertilization in humans.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">In humans, fertilization begins when a sperm penetrates an egg. This happens in the fallopian tubes, after ovulation. Once a sperm has penetrated the egg, the chromosomes from both parents combine, resulting in a zygote. This zygote then begins to divide, forming an embryo which moves down to the uterus and implants itself in the uterine wall, marking the beginning of pregnancy</p>
      
