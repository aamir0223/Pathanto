      <h4 class="question">Question 1</h4>
      <h1 class="important-question">
     What is reproduction? Explain the types of reproduction.
      </h1>
       <h4 class="answer">Answer </h4>
      <p class="important-answer">Reproduction is a biological process in which new individuals of the same species are produced by one parent (asexual reproduction) or both parents (sexual reproduction).</br>
      <b> Asexual reproduction: </b>This is where single parents produce offspring without the involvement of gametes (sex cells). Children born are like their parents. Examples include binary fission in bacteria and budding in yeast.</br>

<b>Sexual reproduction:</b> This applies to parents of the opposite sex. They produce gametes that combine to form a new person. Offspring are genetically different from their parents. Examples are humans, animals, and plants.
      
      
      
      
      </p>
      
