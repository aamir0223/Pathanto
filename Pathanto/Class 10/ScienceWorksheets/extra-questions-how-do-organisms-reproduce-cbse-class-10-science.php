      <h4 class="question">Question 4</h4>
      <h1 class="important-question">Explain budding. </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Budding is a type of asexual reproduction where a new organism develops as a small bulb-like projection called bud on the body of the parent organism. This bud grows and detaches from the parent to live independently. Yeast and Hydra reproduce by budding.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">What is a clone?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">A clone is an organism or cell, or group of organisms or cells, produced asexually from one ancestor to which they are genetically identical</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">What is vegetative propagation?Give examples.</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Vegetative propagation is a type of asexual reproduction in plants where new plants are produced from roots, stems, leaves, and buds. Examples include reproduction through stem in potato, through leaf in Bryophyllum, and through roots in sweet potato..</p>
      
