        <h4 class="question">Question 18</h4>
      <h1 class="important-question">Why is variation beneficial for species?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> Variation is beneficial for species as it contributes to the survival and evolution of a species. It allows organisms to adapt to changes in their environment and provides a basis for natural selection, where individuals with advantageous traits are more likely to survive and reproduce. This leads to the evolution of species over time.</p>
      
      
        <h4 class="question">Question 19</h4>
      <h1 class="important-question">What is the role of the ovary in the female reproductive system?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The ovary is an organ of the female reproductive system that produces eggs (ovum). Each month, during the menstrual cycle, an egg is released from the ovary in a process called ovulation. The ovaries are also responsible for producing the female sex hormones, estrogen, and progesterone, which regulate the menstrual cycle and pregnancy</p>
      
