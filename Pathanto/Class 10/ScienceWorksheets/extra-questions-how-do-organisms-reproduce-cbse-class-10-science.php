      <h4 class="question">Question 2</h4>
      <h1 class="important-question">Define the term 'Regeneration'.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Regeneration is an asexual reproduction in which an organism can reproduce or regrow its lost body parts. This method is common in starfish, hydra and other creatures</p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question">What is binary fission? Provide an example.</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Binary fission is a method of asexual reproduction in which a parent organism divides into two equal parts, each becoming a new organism. Amoeba and bacteria reproduce by this method.</p>

