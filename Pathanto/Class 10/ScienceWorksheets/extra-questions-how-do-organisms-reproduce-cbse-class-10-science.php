      <h4 class="question">Question 7</h4>
      <h1 class="important-question">What is the importance of DNA copying in reproduction?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">DNA copying is important in reproduction as it helps in the transmission of characters or traits from parents to offspring. It ensures the continuity of life by producing variations which are essential for the process of evolution.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">What is sexual reproduction? Explain the process in flowering plants.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Sexual reproduction is the process involving the fusion of male and female gametes. In flowering plants, the male gamete is found in the pollen grain (produced in anther), and the female gamete is found in the ovule (contained in the ovary). The transfer of pollen from anther to the stigma is called pollination. The fusion of male and female gametes is called fertilization, resulting in the formation of seeds.</p>
      
      
