      <h4 class="question">Question 9</h4>
      <h1 class="important-question">What is pollination? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Pollination is the transfer of pollen grains from the anther of one flower to the stigma of the same or another flower. It can be self-pollination (within the same flower) or cross-pollination (between two different flowers).</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">What is the role of the testes in the human male reproductive system?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The testes in the human male reproductive system are responsible for the production of male gametes or sperm cells. They also produce the hormone testosterone, which is responsible for the development of secondary sexual characteristics in males.</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">Explain the menstrual cycle.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The menstrual cycle is a monthly cycle in females which prepares the uterus for pregnancy. It involves the maturation of the egg, its release (ovulation), preparation of the uterus for implantation. If fertilization does not occur, the lining of the uterus sheds off, causing menstruation.</p>
      
