Cerebellum: The cerebellum, located at the base of the brainstem at the back of the brain, regulates voluntary motor activity. It controls our balance, coordination and posture, ensuring that all our movements are smooth and precise. The brain also plays a role in motor skills, such as learning to ride a bike or learning another dance.
