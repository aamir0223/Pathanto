      <h4 class="answer">Answer </h4>
      <p class="important-answer">A nerve cell, also known as a neuron, is the primary conductor of electricity in the nervous system.</br>

Structure: A neuron has three main parts:</br>

Cell body: This is the central portion of the tissue, which contains the nucleus and other cell organelles. It is responsible for normal muscle function and maintenance.</br>
Dendrites: These are small branches that extend from the cell body. Sensations are received and transmitted to the cell body.</br>
Axon: This is a long rod-shaped extension that carries impulses from the cell body to other tissues or motor components such as muscles or nerves. The axon is coated with a myelin sheath that acts as a protective layer and increases the velocity of impulse transmission.</br>
