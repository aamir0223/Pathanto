      <h4 class="question">Question 1</h4>
      <h1 class="important-question">
        Differentiate between metals and non-metals based on their physical properties:
      </h1>
       <h4 class="answer">Answer </h4>
      <p class="important-answer">Metals are generally solid at room temperature (except mercury), have luster, good
        conductors of heat and
        electricity, malleable and ductile.
        Non-metals can be solid, liquid, or gas at room temperature, lack luster, poor conductors of heat and
        electricity, not malleable or ductile.</p>

      <h4 class="question">Question 2</h4>
      <h1 class="important-question"> Explain the terms malleability and ductility with examples:</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Malleability: The property of metals to be beaten into thin sheets. Example: Gold can
        be beaten into extremely
        thin sheets called gold leaf.
        Ductility: The property of metals to be drawn into wires. Example: Copper can be drawn into thin wires.</p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question"> Why do metals conduct electricity? Give an example of a good conductor of
        electricity:</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Metals conduct electricity due to the presence of free electrons that can move freely
        within the metal lattice.
        Example: Copper is a good conductor of electricity.</p>

