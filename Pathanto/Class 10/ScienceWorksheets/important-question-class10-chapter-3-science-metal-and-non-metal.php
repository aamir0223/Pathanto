      <h4 class="question">Question 4</h4>
      <h1 class="important-question"> Define the term 'corrosion.' How can corrosion of metals be prevented?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Corrosion is the process of gradual deterioration of metals due to chemical reactions
        with the environment.
        Corrosion of metals can be prevented by methods such as painting, galvanization (applying a layer of zinc), and
        using sacrificial anodes.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question"> What is the reactivity series of metals? Arrange the following metals in order
        of increasing reactivity:
        copper, iron, magnesium, and zinc:</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Reactivity series: Potassium, sodium, calcium, magnesium, aluminum, zinc, iron, lead,
        hydrogen, copper, silver,
        gold.
        Arrangement: Copper, iron, zinc, magnesium.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question"> Explain the process of extraction of metals using carbon reduction, using the
        example of iron extraction
        from its ore:</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Iron is extracted from its ore (hematite) by heating it with carbon (coke) in a blast
        furnace. Carbon reduces
        the iron oxide to form iron and carbon dioxide.</p>

      <h4 class="question">Question 7</h4>
      <h1 class="important-question"> What is an alloy? Provide two examples of common alloys and their uses:</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">An alloy is a mixture of two or more metals, or a metal and a non-metal.
        Examples: Brass (copper and zinc) used for making utensils, bronze (copper and tin) used for making statues and
        coins.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question"> Discuss the importance of metals and non-metals in our daily life:</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Metals are used for construction, transportation, electrical wiring, etc.
        Non-metals like oxygen are essential for respiration, carbon is the basis of organic compounds, etc.</p>

      <h4 class="question">Question 9</h4>
      <h1 class="important-question"> Why are some metals considered noble or inert? Give an example of a noble metal:
      </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Noble or inert metals do not readily react with other substances. Example: Gold is a
        noble metal.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question"> Describe the properties and uses of hydrogen as a non-metal:</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Hydrogen is a colorless, odorless gas, is highly flammable and burns with a pale blue
        flame.
        It is used in the Haber process for ammonia synthesis, in the hydrogenation of fats and oils, and as rocket
        fuel.</p>










