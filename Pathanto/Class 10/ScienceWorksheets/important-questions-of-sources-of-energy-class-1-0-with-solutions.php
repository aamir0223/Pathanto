      <h1 class="important-question">What is a Thermal Power Plant, and how does it generate electricity?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer"> A Thermal Power Plant generates electricity by burning fossil fuels (coal, oil, or natural gas) to produce high-pressure steam. This steam then turns turbines connected to generators, converting thermal energy into electrical energy.</p>

