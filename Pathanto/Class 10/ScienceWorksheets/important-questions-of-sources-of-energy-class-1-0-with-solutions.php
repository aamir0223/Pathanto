      <h4 class="question">Question 7</h4>
      <h1 class="important-question">How long will an energy source last, and what factors influence its lifespan?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The lifespan of an energy source depends on its availability and rate of consumption. Fossil fuels are finite and deplete over time, influenced by extraction rates, technological advancements, and conservation efforts.</p>

