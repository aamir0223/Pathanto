        <h4 class="question">Question 16</h4>
      <h1 class="important-question">How does geothermal energy harness heat from the Earth's interior?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Geothermal energy taps into the Earth's internal heat by accessing hot water or steam from underground reservoirs. This heat is then used for direct heating or to generate electricity through steam turbines, making it a sustainable and continuous energy source.
.</p>

    <h4 class="question">Question 17</h4>
      <h1 class="important-question">How does solar energy contribute to sustainable power generation?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Solar energy is harnessed using photovoltaic cells or solar thermal systems. Photovoltaic cells convert sunlight directly into electricity, while solar thermal systems use mirrors or lenses to concentrate sunlight for heating purposes or to produce steam for electricity generation.
.</p>

    <h4 class="question">Question 18</h4>
      <h1 class="important-question">What are the challenges in harnessing tidal energy?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> Tidal energy faces challenges such as high initial installation costs, limited suitable locations, and potential environmental impacts. The construction of tidal power plants must consider the delicate marine ecosystems and the effects of altering tidal patterns.
.</p>

    <h4 class="question">Question 19</h4>
      <h1 class="important-question">What are the social and economic implications of transitioning to renewable energy?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The transition to renewable energy sources can create job opportunities in the renewable energy sector, stimulate economic growth, and reduce dependence on finite resources. However, it may also require workforce retraining and adaptation to new economic structures.
.</p>

    <h4 class="question">Question 20</h4>
      <h1 class="important-question">What role do alternative energy sources play in reducing environmental impact?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Alternative energy sources play a crucial role in reducing environmental impact. Unlike conventional sources, alternatives like solar, wind, and geothermal energy produce minimal air and water pollution, and they don't contribute significantly to climate change through greenhouse gas emissions.

Solar and wind energy, in particular, are abundant and renewable, harnessing the power of natural processes without depleting finite resources. Additionally, these sources have a lower ecological footprint, preserving biodiversity and minimizing habitat disruption compared to the extraction of fossil fuels.

Investing in alternative energy technologies promotes sustainability, helps combat climate change, and fosters a cleaner and healthier environment for both present and future generations.
.</p>


    
      
