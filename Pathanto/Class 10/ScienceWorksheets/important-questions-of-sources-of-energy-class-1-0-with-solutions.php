        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What policies and measures can governments implement to promote sustainable energy?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Governments can promote sustainable energy through policies such as subsidies for renewable energy, setting emission reduction targets, investing in research and development, and creating a regulatory framework that incentivizes the transition to clean energy sources.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">Explain the concept of fossil fuels.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Fossil fuels are organic materials, primarily the remains of plants and animals, that undergo geological processes to become coal, oil, or natural gas. These fuels store energy from the sun captured by plants through photosynthesis millions of years ago.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">What are the improvements in technology for using conventional sources of energy?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Technological advancements in conventional energy sources include more efficient combustion techniques, advanced turbine designs, and cleaner methods of utilizing fossil fuels. These improvements aim to increase energy extraction efficiency and reduce environmental impacts.</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question"> Explain the impact of fossil fuel consumption on climate change.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Fossil fuel consumption releases carbon dioxide and other greenhouse gases into the atmosphere. This enhances the greenhouse effect, contributing to global warming and climate change by trapping heat and altering Earth's climate patterns.
</p>
      
