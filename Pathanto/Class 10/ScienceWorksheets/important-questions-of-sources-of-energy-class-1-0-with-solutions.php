      <h4 class="question">Question 9</h4>
      <h1 class="important-question">Explain the working principle of a wind turbine in generating electricity.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Wind turbines convert kinetic energy from the wind into mechanical energy. The spinning blades turn a rotor connected to a generator, producing electricity. Wind power is a sustainable and clean energy source.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">How can energy conservation practices contribute to a sustainable future?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Energy conservation involves efficient use and reduced wastage. Practices such as using energy-efficient appliances, insulating homes, and adopting sustainable transportation contribute to a sustainable future by minimizing resource depletion and environmental impact.</p>
      
        <h4 class="question">Question 11</h4>
