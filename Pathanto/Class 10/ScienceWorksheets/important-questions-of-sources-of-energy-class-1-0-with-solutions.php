      <h4 class="question">Question 4</h4>
      <h1 class="important-question">How do Hydro Power Plants generate electricity?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Hydro Power Plants harness the kinetic energy of flowing water. Water is directed to turn turbines, which are connected to generators, converting the water's energy into electrical energy through electromagnetic induction.</p>

