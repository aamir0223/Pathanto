      <h4 class="question">Question 7</h4>
      <h1 class="important-question">What is the focal length of a lens? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The focal length of a lens is the distance between the center of the lens and the focal point. It is determined by the curvature of the lens surfaces and the material of the lens. A lens with a short focal length has greater optical power than one with a long focal length.

</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">What are the uses of concave and convex lenses? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Concave lenses are used in a variety of optical devices like telescopes, eyeglasses, cameras, and binoculars. They are used in eyeglasses to correct short-sightedness.</br>

Convex lenses are used in devices like magnifying glasses, microscopes, and cameras. They are used in eyeglasses to correct long-sightedness.</p>
      
      
