      <h1 class="important-question">What is the use of a convex lens?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A convex lens, also known as a converging lens, focuses the parallel rays of light that pass through it at a particular point. Hence, it is used in various optical devices such as a magnifying glass, camera, microscope, and spectacles for hypermetropic (far-sighted) individuals.r</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">What is a spherical mirror? </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> A spherical mirror is a mirror which has the shape of a piece cut out of a spherical surface. There are two types of spherical mirrors: concave and convex. A concave mirror has the reflecting surface that bulges inwards. A convex mirror has the reflecting surface that bulges outwards.

</p>
      
