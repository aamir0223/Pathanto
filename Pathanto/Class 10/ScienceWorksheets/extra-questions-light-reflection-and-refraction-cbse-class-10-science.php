      <h4 class="question">Question 2</h4>
      <h1 class="important-question">Define and differentiate between concave and convex mirrors</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">
          <b>Concave:</b>  mirrors are curved mirrors where the reflecting surface bulges inwards. They converge light rays to a single point known as the focus. They produce both virtual and enlarged as well as real and diminished images, based on the object's position.</br>

          <b>Convex</b> mirrors have a reflecting surface that bulges outwards. They diverge light rays, making them appear as if they are coming from a particular point on the mirror. They produce images that are virtual, diminished, and upright
      </p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question">Describe the refraction of light.</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Refraction is the bending of light as it passes from one medium to another. This happens due to the change in speed of light in different media. The laws of refraction state that the incident ray, the refracted ray, and the normal to the interface of two media at the point of incidence all lie in the same plane, and the ratio of the sine of the angle of incidence to the sine of the angle of refraction is a constant</p>

