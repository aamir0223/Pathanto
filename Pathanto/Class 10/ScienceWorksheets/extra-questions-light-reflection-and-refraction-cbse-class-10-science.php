        <h4 class="question">Question 20</h4>
      <h1 class="important-question">What is the difference between a concave and a convex lens?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A concave lens is thinner at the center than at the edges, and it spreads out light rays that are incident on it. It forms a virtual, erect, and diminished image. A convex lens, on the other hand, is thicker at the center than at the edges. It bends the light rays towards a point, forming a real, inverted image (or a virtual, erect image, depending on the position of the object).

</p>
      
      
