      <h4 class="question">Question 4</h4>
      <h1 class="important-question">How is the human eye similar to a camera?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The human eye functions much like a camera. The cornea in the eye is like a camera lens as it focuses light to create clear images. The iris and the pupil control the amount of light entering the eye, just like the aperture in a camera. The retina is akin to the film or image sensor in a camera, where the image is formed.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">What is the principle of reversibility of light?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">The principle of reversibility of light means that if the path of light is reversed, it will retrace its original path. This principle is used in ray diagrams to locate the image formed by lenses and mirrors.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">Explain how a rainbow is formed.</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">A rainbow is formed due to the dispersion, refraction, and reflection of sunlight by water droplets in the atmosphere. Sunlight is first refracted as it enters the water droplet, then it gets reflected inside the droplet and refracts again as it leaves the droplet. The white light from the sun gets dispersed into its spectrum of colors, forming a rainbow.

</p>
      
