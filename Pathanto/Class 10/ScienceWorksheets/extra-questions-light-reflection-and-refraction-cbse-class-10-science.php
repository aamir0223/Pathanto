      <h4 class="question">Question 9</h4>
      <h1 class="important-question">How do lenses form images?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Lenses form images by refracting light and focusing it at a point. Convex lenses converge light rays, forming a real image that can be projected on a screen. On the other hand, concave lenses diverge light rays, forming a virtual image that cannot be projected on a screen.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">What is a prism and how does it work?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">
      A prism is a transparent optical element with at least two polished, flat surfaces that are inclined to each other. The traditional geometrical shape is that of a triangular prism with a triangular base and rectangular sides. The most common type of prism is made of clear, transparent plastic or glass.</br>

A prism works by refracting, or bending, light. This happens because light travels at different speeds through different mediums. When light enters the prism, it is moving from air (one medium) into glass (another medium), which causes the light to slow down and change direction - this is refraction.</br>

The key to a prism's color-splitting ability lies in the different degrees of refraction for different colors of light. White light, like sunlight, is composed of multiple colors. When white light enters a prism, each color of light is refracted by a slightly different amount. Red light bends the least, while violet light bends the most. This causes the different colors to spread out and form a spectrum, also known as a rainbow.</br>

This property of prisms is used in various optical devices, such as binoculars and spectrometers, to manipulate light for various purposes.</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">How is the image formed by a plane mirror?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A plane mirror forms a virtual and erect image of the object. The image formed is of the same size as the object and is located at the same distance behind the mirror as the object is in front of the mirror.</p>
      
