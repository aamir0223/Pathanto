        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What is a real image?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A real image is formed when light rays converge at a point. It can be obtained on a screen, is inverted, and can be larger or smaller than the object depending upon the position of the object relative to the lens or mirror..</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">What do you understand by the power of a lens?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The power of a lens is the measure of the degree of convergence or divergence of light rays achieved by the lens. It is the reciprocal of the focal length (in meters) of the lens. The power of a lens is measured in diopters.</p>
      
