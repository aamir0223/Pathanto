        <h4 class="question">Question 18</h4>
      <h1 class="important-question">What is the focal length of a mirror?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> The focal length of a mirror is the distance between the pole (P) and the focus (F) of the mirror. For a concave mirror, the focal length is positive, and for a convex mirror, the focal length is negative.</p>
      
      
        <h4 class="question">Question 19</h4>
      <h1 class="important-question">How does a lens form an image?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A lens forms an image by bending light rays that pass through it. When light rays pass through a convex lens, they converge at a point on the other side of the lens. The point at which the rays meet is where the image is formed. For a concave lens, the light rays diverge, and the image is formed where the rays appear to originate from.</p>
      
