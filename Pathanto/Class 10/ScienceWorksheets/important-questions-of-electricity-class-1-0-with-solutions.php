      <h4 class="question">Question 7</h4>
      <h1 class="important-question">What does a circle in a circuit diagram represent?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A circle represents a light bulb or any other electrical load in a circuit diagram.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">State Ohm's Law.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Ohm's Law states that the current passing through a conductor between two points is directly proportional to the voltage across the two points, provided the temperature remains constant.</p>
      
      
