      <h4 class="question">Question 4</h4>
      <h1 class="important-question">What is electric potential?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Electric potential is the electric potential energy per unit charge at a point in an electric field. It's measured in volts.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">Explain the concept of potential difference.
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Potential difference, or voltage, is the difference in electric potential between two points in a circuit. It's the driving force that makes electric charges move.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">How is a switch represented in a circuit diagram?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">A switch is represented by a break in the circuit line with a gap, which can be closed or opened to control the flow of current.

</p>
      
