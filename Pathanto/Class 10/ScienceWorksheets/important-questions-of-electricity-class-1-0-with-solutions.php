      <h4 class="question">Question 9</h4>
      <h1 class="important-question">If the voltage across a resistor is 12 volts and the current is 3 amperes, what is the resistance?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Using Ohm's Law (R = V/I), the resistance is 4 ohms.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question"> List the factors on which the resistance of a conductor depends.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The resistance of a conductor depends on its length, cross-sectional area, temperature, and the material it's made of.</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question"> How does an increase in temperature affect the resistance of a conductor?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Generally, an increase in temperature increases the resistance of a conductor.</p>
      
