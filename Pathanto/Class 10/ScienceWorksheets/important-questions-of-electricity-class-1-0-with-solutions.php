        <h4 class="question">Question 12</h4>
      <h1 class="important-question">Explain how resistances add up in a series circuit.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">In a series circuit, resistances add up. The total resistance (R_total) is the sum of individual resistances.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">Describe how resistances combine in a parallel circuit.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">In a parallel circuit, the reciprocal of the total resistance is equal to the sum of the reciprocals of individual resistances.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">What is the heating effect of electric current?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">When electric current passes through a conductor, it produces heat due to the collisions between electrons and atoms in the conductor.</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question"> How is the heating effect of electric current useful in appliances?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The heating effect is used in appliances like electric heaters and stoves to generate heat for various purposes.

</p>
      
