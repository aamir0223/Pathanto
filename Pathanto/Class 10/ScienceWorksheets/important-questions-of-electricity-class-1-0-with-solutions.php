      <h1 class="important-question">Explain the difference between AC and DC currents.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">AC (Alternating Current) changes its direction periodically, while DC (Direct Current) flows in one direction consistently.</p>

