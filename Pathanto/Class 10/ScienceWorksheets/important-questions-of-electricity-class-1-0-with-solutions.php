        <h4 class="question">Question 16</h4>
      <h1 class="important-question">Define electric power.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Electric power is the rate at which electric energy is transferred or converted, measured in watts (W).
.</p>

    <h4 class="question">Question 17</h4>
      <h1 class="important-question">How is electric power related to voltage and current?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Electric power (P) is the product of voltage (V) and current (I), expressed by the formula P = VI.
.</p>

    <h4 class="question">Question 18</h4>
      <h1 class="important-question">Why is it important to use fuses in electrical circuits?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Fuses protect electrical circuits by melting and breaking the circuit when the current exceeds a safe limit, preventing damage to devices and fires.
.</p>

    <h4 class="question">Question 19</h4>
      <h1 class="important-question">Explain the concept of electrical grounding.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Electrical grounding provides a path for electric current to flow safely into the ground, preventing electric shocks and ensuring safety.
.</p>

    <h4 class="question">Question 20</h4>
      <h1 class="important-question">How does the size of a wire affect its resistance?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Generally, a thinner wire has higher resistance than a thicker wire. This is because a thicker wire allows for more electrons to flow, reducing resistance.
.</p>
      
    
      
