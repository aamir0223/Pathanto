      <h4 class="question">Question 4</h4>
      <h1 class="important-question">What is the significance of respiration in an organism?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Respiration is essential to living organisms because it provides oxygen needed for cellular respiration. In this process, glucose is broken down into energy, which is essential for all life processes in the organism.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">What is the role of the kidneys in excretion?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">The kidneys play an important role in the elimination process. The blood is filtered and wastes such as urea and excess salts removed. This waste is then transported to the bladder where it is excreted as liquid.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">How does photosynthesis contribute to the survival of all organisms?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Photosynthesis Green plants use sunlight, carbon dioxide and water to make their food. This provides food not only for the plant itself but also for other creatures that eat the plant. In addition, photosynthesis produces oxygen, which is an essential requirement for all living things to breathe.</p>
      
