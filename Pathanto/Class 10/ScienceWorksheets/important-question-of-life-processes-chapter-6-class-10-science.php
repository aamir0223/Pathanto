      <h1 class="important-question">
      Define life processes.
      </h1>
       <h4 class="answer">Answer </h4>
      <p class="important-answer">Life resources are the activities that an organism performs in order to survive and continue its species. These include nutrition, rest, travel and exclusions.</p>

      <h4 class="question">Question 2</h4>
      <h1 class="important-question">What is the role of the stomach in digestion?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The stomach plays an important role in digestion. It releases gastric juice containing pepsin and other enzymes that help break down proteins. The stomach is also a mechanical digestive system that breaks down food into a semi-solid substance known as chyme</p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question">How does transportation in plants occur?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Transport in plants occurs through a vascular system consisting of xylem and phloem. Specifically, the xylem carries water and metals to the rest of the plant, while the phloem transports nutrients from the leaves to other parts of the plant</p>

