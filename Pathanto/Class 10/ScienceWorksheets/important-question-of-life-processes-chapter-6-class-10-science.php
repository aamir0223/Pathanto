      <h4 class="question">Question 7</h4>
      <h1 class="important-question">Explain the process of ingestion in the human digestive system.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Food goes into the body. In humans, this process begins in the mouth where their teeth catch and chew food. The salivary glands cool food and the amylase enzyme in the saliva begins digestion by breaking down complex carbohydrates into simple sugars that then pass through the digestive tract to swallow the chewed food and to the abdominal cavity.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question"> What is the role of alveoli in the respiratory system?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Alveoli are tiny balloon-like structures in the lungs. They are the actual exchange point of oxygen and carbon dioxide. In the air we breathe, oxygen diffuses into the blood vessels around the veins and is carried to the cells of the body in the same way that carbon dioxide, a waste product of cellular respiration, diffuses out of the blood the root canal.</p>

      <h4 class="question">Question 9</h4>
      <h1 class="important-question">Explain the role of enzymes in the process of digestion and mention the factors affecting their activity.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Enzymes play an important role in digestion by breaking down food into simpler components that can be easily absorbed by the body. They work by reducing the kinetic energy required to make the reaction possible, thus speeding up the process. For example, the enzyme amylase breaks down starch into maltose, while protease breaks down protein into amino acids. Enzyme activity is affected by factors such as temperature, pH level, and substrate concentration. Too high or too low temperature and pH can damage enzymes, rendering them inactive, while increasing substrate concentration increases their reactivity to a certain extent</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">Describe the counter-current mechanism in the nephrons of the kidney. How does it help in the process of urine formation?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">In the counter current mechanism of the nephrons of the kidney, the two limbs of the loop of the Henle filter flow in opposite directions This creates a concentration gradient that contributes to the reabsorption of water and electrolytes, thus hydration the value is greater. This mechanism is necessary to conserve water in the body</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">Discuss the process of double circulation in humans and its significance.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">In humans, double circulation means that blood passes through the heart twice in one complete cycle of the body. It is divided into pulmonary and systemic circulation. Oxygenated blood in the pulmonary blood is pumped from the heart to the lungs for oxygenation. Oxygenated blood in the system is pumped from the heart to different parts of the body and back to the heart. This system ensures proper separation and circulation of oxygenated and deoxygenated blood, which is essential for maintaining a stable indoor environment</p>
      
