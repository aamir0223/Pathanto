        <h4 class="question">Question 12</h4>
      <h1 class="important-question">Explain the process of transpiration in plants. How does it help in the upward movement of water and minerals?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Condensation is water that travels through a plant and evaporates from leaves and other parts of the atmosphere. It provides transpiration which helps to move water and minerals up from the roots to different parts of the plant Warm water also helps with burning.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">Elaborate on the role of stomata in the process of photosynthesis.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Stomata are small openings on the leaf surface that facilitate gas exchange. In the process of photosynthesis, carbon dioxide is consumed which is used by the plant to make glucose. They also help release oxygen, which is a photosynthetic product.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">Discuss the differences between aerobic and anaerobic respiration and their energy production.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Aerobic respiration is a process that requires oxygen to break down glucose, producing carbon dioxide, water, and energy in the form of ATP. However, anaerobic respiration occurs in the absence of oxygen and produces lactic acid (in animals) or alcohol carbon dioxide (in yeast), where energy is relatively low Both are essential for the production of living organisms energy needed for various aspects of life.</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">Explain the concept of osmoregulation in animals.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">It is a process by which animals regulate the amount of water and salt in their bodies. It helps balance the intake and loss of water and salts, which are essential for healthy cell function.</p>
      
