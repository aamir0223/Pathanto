        <h4 class="question">Question 16</h4>
      <h1 class="important-question">Describe the process of protein synthesis in cells. How does it contribute to the growth and repair of tissues?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Protein synthesis is the production of proteins by cells. It consists of two main steps: transcription, where information in DNA is transcribed into another messenger RNA (mRNA) molecule, and translation, where mRNA is used as a template to assemble amino acids into a specific protein This process important for growth and repair Occurs because proteins are the building blocks of tissues.</p>
      
      
        <h4 class="question">Question 17</h4>
      <h1 class="important-question">Discuss the different types of nutrition and give examples of organisms that use each type.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Nutrition is the process of providing or obtaining nutrients necessary for health and growth. It can be autotrophic (autonomous), where organisms produce their own food through photosynthesis, as in plants, or it can be heterotrophic (alternative eating), where organisms rely on other organisms provide food like animals</p>
      
      
        <h4 class="question">Question 18</h4>
      <h1 class="important-question">Explain the concept of homeostasis and its importance in the survival of organisms.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Homeostasis is the ability of an organism to maintain a constant internal state despite changes in external conditions. It is essential for life because it allows organisms to function properly under different conditions. Examples include temperature control, pH balance, and fluid and electrolyte balance.</p>
      
      
        <h4 class="question">Question 19</h4>
      <h1 class="important-question">Explain the process of nerve impulse transmission in the human body.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Nerve impulses in the human body begin when a signal stimulates a nerve. This changes the electric field of the muscle, producing an action force. This action potential travels along the axon of the nerve, a process called propagation. When the action potential reaches the end of a nerve, it releases neurotransmitters. These neurotransmitters cross the synapse (the nerve between neurons) and interact with the control of subsequent neurons, creating a new action potential in that neuron This process repeats itself, creating a neuron the sensation travels through the whole body.

