      <h1 class="important-question">
     What is heredity?
      </h1>
       <h4 class="answer">Answer </h4>
      <p class="important-answer">Heredity refers to the transmission of traits from parents to their offspring either through asexual reproduction or sexual reproduction. The offspring cells and organisms acquire the genetic information of their parent cell or organisms</p>
      
