      <h4 class="question">Question 2</h4>
      <h1 class="important-question">Define evolution.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Evolution is the gradual development of organisms from a simple to a more complex form over several generations. It explains the diversity and unity of life on earth.</p>

      <h4 class="question">Question 3</h4>
      <h1 class="important-question">What are genes?</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Genes are units of heredity which are transferred from parents to offspring and determine some characteristics of the offspring. They are segments of DNA and are responsible for the physical and inheritable characteristics or traits of an individual.</p>

