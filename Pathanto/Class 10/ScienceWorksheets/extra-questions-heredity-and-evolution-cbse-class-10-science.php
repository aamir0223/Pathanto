        <h4 class="question">Question 16</h4>
      <h1 class="important-question">What are homologous and analogous organs? Give examples</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Homologous organs are those with similar structure and origin but different functions, like the forelimbs of humans and wings of birds. Analogous organs have different structures and origins but similar functions, like the wings of a bird and wings of a butterfly.
.</p>
      
      
        <h4 class="question">Question 17</h4>
      <h1 class="important-question">What is the evolutionary significance of the human appendix?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">The human appendix is considered a vestigial organ, with no apparent function in humans. Its presence suggests that our evolutionary ancestors may have had a diet that required a large cecum, which is used in the digestion of plant material. Over time, as human diets changed, the appendix became less useful.</p>
      
