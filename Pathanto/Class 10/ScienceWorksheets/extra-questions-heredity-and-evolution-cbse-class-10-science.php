        <h4 class="question">Question 20</h4>
      <h1 class="important-question">How can the age of fossils be determined?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer"> The age of fossils can be determined using several methods. One common method is relative dating, which involves looking at the layers of rock in which the fossil was found. Fossils in lower layers are older than those in upper layers. Another method is radiometric dating, which measures the decay of radioactive elements within the fossil or the rocks associated with it. This method can provide a more precise age.</p>
      
      
