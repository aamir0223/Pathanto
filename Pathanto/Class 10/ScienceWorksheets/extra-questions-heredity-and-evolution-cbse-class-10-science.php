      <h4 class="question">Question 7</h4>
      <h1 class="important-question">What is speciation?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Speciation is the formation of new and distinct species in the course of evolution. It occurs when populations of a species become isolated and evolve differently.</p>

      <h4 class="question">Question 8</h4>
      <h1 class="important-question">What is the role of DNA copying in inheritance?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">DNA copying plays a crucial role in inheritance as it helps in the transmission of traits from parents to offspring. The process of DNA copying brings about a variation each time cell division occurs, which forms the basis of evolution.</p>
      
      
