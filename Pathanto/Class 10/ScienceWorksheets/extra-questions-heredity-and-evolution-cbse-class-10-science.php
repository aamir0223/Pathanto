      <h4 class="question">Question 9</h4>
      <h1 class="important-question">Define fossils and explain their significance. </h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Fossils are preserved remains or traces of animals, plants, and other organisms from the remote past. They provide direct evidence about the life forms and their chronology on earth. They also provide evidence about how evolution has occurred.</p>

      <h4 class="question">Question 10</h4>
      <h1 class="important-question">Explain Mendel's experiment with peas.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Gregor Mendel conducted breeding experiments with pea plants. He took into consideration various contrasting traits like height, seed color, flower color, etc. He observed that some traits were dominant over others, and these findings led him to formulate the laws of inheritance.</p>
      
        <h4 class="question">Question 11</h4>
      <h1 class="important-question">What is genetic drift?.</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Genetic drift is a mechanism of evolution that involves random changes to the gene pool of a small population, which can lead to significant changes in the genetic makeup of the population over time..</p>
      
