      <h4 class="question">Question 4</h4>
      <h1 class="important-question">What is a trait?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A trait is a distinct characteristic or quality of an organism that can be inherited or acquired. Traits include physical characteristics such as height, eye color, and behavioral characteristics such as the ability to sing.</p>

      <h4 class="question">Question 5</h4>
      <h1 class="important-question">What is variation?
        </h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Variation refers to the differences in traits among the individuals of the same species. Variation can be caused by genetic differences, environment, or a combination of both.</p>

      <h4 class="question">Question 6</h4>
      <h1 class="important-question">Explain the term 'natural selection'.</h1>
        <h4 class="answer">Answer </h4>
      <p class="important-answer">Natural selection is a process by which species adapt to their environment. It involves survival of the fittest, where organisms best adapted to their environment are more likely to survive and pass on their traits to the next generation.

</p>
      
