        <h4 class="question">Question 12</h4>
      <h1 class="important-question">What is the significance of sex in reproduction?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Sexual reproduction leads to greater genetic diversity as genes from two parents combine in unique ways in each offspring. This genetic variation can lead to better survival of the species in changing environments.</p>
      
        <h4 class="question">Question 13</h4>
      <h1 class="important-question">What is a chromosome?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">A chromosome is a long DNA molecule with part or all of the genetic material of an organism. Humans have 23 pairs of chromosomes.</p>
      
        <h4 class="question">Question 14</h4>
      <h1 class="important-question">What is meant by acquired traits and inherited traits?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Acquired traits are the ones that an organism develops during its lifetime, like learning a skill. These traits cannot be passed on to the next generation. Inherited traits are those that are passed from parents to offspring through genes, like eye color or skin color</p>
      
      
        <h4 class="question">Question 15</h4>
      <h1 class="important-question">What is the difference between dominant and recessive traits?</h1>
      <h4 class="answer">Answer </h4>
      <p class="important-answer">Dominant traits are those that are expressed even if only one copy of the gene for that trait is present. Recessive traits are those that are expressed only when two copies of the gene for that trait are present.

</p>
      
