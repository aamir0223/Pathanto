<p><h5 class="question">Question 9</h5> What happens to the image distance in the eye when we increase the distance of an object from the eye?</p>
<h5 class="answer">Answer</h5>
<p>Due to accommodation our  eyes can increase or decrease the focal length  so that the image is  formed on the retina .So there is no effect on image  even on increasing the distance of an object from the eye.</p>


<p><h5 class="question">Question 10</h5> Why do stars twinkle?</p>
<h5 class="answer">Answer</h5>
<p>The twinkling of a star is due to atmospheric refraction of starlight. The starlight, on entering the earth’s atmosphere, and  reaching our eyes change continuously and stars appear to twinkle.</p>

<p><h5 class="question">Question 11</h5> Explain why the planets do not twinkle?</p>
<h5 class="answer">Answer</h5>
The planets are much closer to the  earth, and  a collection of a large number of point-sized sources of light, the total variation in the amount of light entering our eye from more than one point sources will average out to zero, thereby nullifying the twinkling effect

<p><h5 class="question">Question 12</h5> Why does the Sun appear reddish early in the morning?</p>
<h5 class="answer">Answer</h5>
<p>Light of sun has to travel more distance in the atmosphere because it is very far from earth. All colour of lights get scatter  except  red colour due to  longer wavelengths  Therefore, the sun appears reddish at sunrise and sunset.</p>

<p><h5 class="question">Question 13</h5> Why does the sky appear dark instead of blue to an astronaut?</p>
<h5 class="answer">Answer</h5>
<p> Due to absence of atmosphere in  space,scattering of light does not take place and sky appear dark instead of blue  to an astronaut.</p>




