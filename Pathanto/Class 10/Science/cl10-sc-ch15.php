<p> <h4 class="question">Question 4</h4> What will happen if we kill all the organisms in one trophic level?</p>
<h4 class="answer">Answer </h4>
<p> If we kill all the organisms in one trophic level, the food supply to the next level will strop resulting  the lower trophic level will grow more in an enormous way and the higher trophic level will die . So flow of energy from one trophic level to other will not take place.</p>


<p> <h4 class="question">Question 5</h4> Will the impact of removing all the organisms in the trophic level be different for different trophic levels? Can the organisms of any trophic level be removed without causing any damage to the ecosystem?</p>
<h4 class="answer">Answer </h4>

<p>Yes The impact for removing different organisms at different trophic levels would be different as the removal of organisms at any level would upset the whole ecosystem and the food chain is disturbed For e.g - If all snakes are killed, the peacocks belonging to the next level will also die. Also the population of rats in the preceding level will highly increase, if grasshoppers are removed frogs will starting reducing in number.Thus to maintain the balance of the ecosystem the presence of organisms is necessary at each trophic level.
</p>

