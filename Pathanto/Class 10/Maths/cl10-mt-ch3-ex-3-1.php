<h6 class="question">Question 1</h6>
Aftab tells his daughter, “Seven years ago, I was seven times as old as you were then.
Also, three years from now, I shall be three times as old as you will be.” (Isn’t this
interesting?) Represent this situation algebraically and graphically.<br>
<h6 class="answer">Answer</h6>
<br>
Let the present age of Aftab be x <br> And  his daughter be  y.<br>

 Seven years ago,<br>

Age of Aftab = x-7<br>

Age of his daughter = y-7<br>
According to question<br>


x−7 = 7(y−7)<br>

x−7 = 7y−49<br>

x−7y = −42  ......(1) <br>
 After three years<br>

Age of Aftab  = x+3.

Age of his daughter  = y+3

According to the question<br>

x+3 = 3(y+3)<br>

x+3 = 3y+9<br>

x−3y = 6 ....... (2) <br>

The algebraic equation is represented by<br>

x−7y = −42<br>

x−3y = 6<br>

For, x−7y = −42<br> or x = −42+7y<br><br>









