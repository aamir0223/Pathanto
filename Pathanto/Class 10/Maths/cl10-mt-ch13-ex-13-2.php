               <h6 class="question">Question 2</h6>.
               Rachel, an engineering student, was asked to make a model shaped like a cylinder with two cones attached
               at its two ends by using a thin aluminum sheet. The diameter of the model is 3 cm and its length is 12
               cm. If each cone has a height of 2 cm, find the volume of air contained in the model that Rachel made.
               (Assume the outer and inner dimensions of the model to be nearly the same.)</p>
               <h6 class="answer">Answer 2</h6>.
               <p>Given,</p>
               <p>Height of cylinder = 12–4 = 8 cm</p>
               <p>Radius = 1.5 cm</p>
               <p>Height of cone = 2 cm</p>
               <p>Now, the total volume of the air contained will be = Volume of cylinder+2×(Volume of cone)</p>
               <p>∴ Total volume = πr<sup>2</sup>h+[2×(⅓ πr<sup>2</sup>h )]
               <p>= 18 π+2(1.5 π)</p>
               <p>= 66 cm<sup>3</sup>.</p>

