               <h6 class="question">Question 1</h6>. A solid is in the shape of a cone standing on a hemisphere with
               both their radii being equal to 1 cm and the height of the cone is equal to its radius. Find the volume
               of the solid in terms of π.</p>
               <h6 class="answer">Answer 1</h6>.
               <img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.2-1.png"
                    alt="NCERT Ex-13.2 class 10"><br>
               <p>Here r = 1 cm and h = 1 cm.</p>

               <p>Now, Volume of solid = Volume of conical part + Volume of hemispherical part</p>
               <p>We know taht volume of cone = (1/3)πr<sup>2</sup>h</p>
               <p>And,</p>
               <p>The volume of hemisphere = (2/3)πr<sup>3</sup></p>
               <p>So, volume of solid will be</p>
               <p> (1/3)π(1)<sup>2</sup>[1 + 2(1)]cm<sup>3</sup> =(1/3)π x 1 x [3]cm<sup>3</sup></p>
               <p>= π cm<sup>3</sup></p>
