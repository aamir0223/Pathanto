               <h6 class="question">Question 6</h6> A solid iron pole consists of a cylinder of height 220 cm and base
               diameter 24 cm, which is surmounted by another cylinder of height 60 cm and radius 8 cm. Find the mass of
               the pole, given that 1 cm<sup>3</sup> of iron has approximately 8 g mass.</p>
               <h6 class="answer">Answer 6</h6>.

               <p>Given, the height of the big cylinder H = 220 cm</p>
               <p>Radius of the base R = 24/12 = 12 cm</p>
               <p>Now, the height of smaller cylinder (h) = 60 cm</p>
               <p>Radius of the base (r) = 8 cm</p>
               <p>So, the volume of the smaller cylinder = πr<sup>2</sup>h</p>
               <p>= π(8)2×60 cm<sup>3</sup></p>
               <p>= 12068.5 cm<sup>3</sup></p>
               <p>∴ Volume of iron pole = Volume of the big cylinder+ Volume of the small cylinder</p>
               <p>= 99565.8 + 12068.5</p>
               <p>=111634.5 cm<sup>3</sup></p>
               <p>We know,</p>
               <p>Mass = Density x volume</p>
               <p>So, mass of the pole = 8×111634.5</p>
               <p>= 893.26 Kg </p>

