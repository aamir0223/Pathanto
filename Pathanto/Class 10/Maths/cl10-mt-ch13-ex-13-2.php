               <h6 class="question">Question 3</h6> A Gulab jamun contains sugar syrup up to about 30% of its volume.
               Find approximately how much syrup would be found in 45 Gulab jamuns, each shaped like a cylinder with two
               hemispherical ends with length 5 cm and diameter 2.8 cm (see figure).</strong></p>
               <img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.2-4.png"
                    alt="NCERT Ex-13.2 class 10"><br>
               <h6 class="answer">Answer 3</h6>.

               <p>So, the total height of a gulab jamun = 5 cm.</p>
               <p>Diameter = 2.8 cm</p>
               <p>So, radius = 1.4 cm</p>
               <p>∴ The height of the cylindrical part = 5 cm–(1.4+1.4) cm</p>
               <p>=2.2 cm</p>
               <p>Now, volume of One Gulab Jamun = Volume of Cylinder + Volume of two hemispheres</p>
               <p>= πr<sup>2</sup>h+(4/3)πr<sup>3</sup></p>
               <p>= 4.312π+(10.976/3) π</p>
               <p>= 25.05 cm<sup>3</sup></p>
               <p>volume of sugar syrup = 30% of total volume</p>
               <p>So, volume of sugar syrup in 45 gulab jamuns = 45×30%(25.05 cm<sup>3</sup>)</p>
               <p>= 45×7.515 = 338.184 cm<sup>3</sup></p>
