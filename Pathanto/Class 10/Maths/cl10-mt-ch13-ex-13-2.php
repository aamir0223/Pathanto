               <h6 class="question">Question 5</h6> A vessel is in the form of an inverted cone. Its height is 8 cm and
               the radius of its top, which is open, is 5 cm. It is filled with water up to the brim. When lead shots,
               each of which is a sphere of radius 0.5 cm are dropped into the vessel, one-fourth of the water flows
               out. Find the number of lead shots dropped in the vessel.</p>
               <h6 class="answer">Answer 5</h6>
               <img class="Q2ch3-5n" src="https://pathanto.com/Pathanto/image/ex13.2-2.png" alt="NCERT Ex-13.2 class 10"
                    width="250" height="200"><br>
               <p>For the cone,</p>
               <p>Radius = 5 cm,</p>
               <p>Height = 8 cm</p>
               <p>Also,</p>
               <p>Radius of sphere = 0.5 cm</p>

               <p> volume of water in vessel</p>
               <p> =(1/3) x π x r<sup>2</sup> x h</p>
               <p> (1/3) x (22/7) x 5 x 5 x 8<br>=(4400/21)cm<sup>3</sup> </p>

               <p>Volume of lead shot</p>
               <p>= (4/3)πr<sup>3 </sup></p>
               <p>= (1/6) x (22 /7) = (11/21)cm<sup>3</sup> </p>
               <p> let n is the number of lead shots dropped in the vessel</p>

               <p> So, <br> n x volume of water in vessel = (1/4) x volume of water in vessel </p>
               <p> n x (11/21) = (4400/21 ) x (1/4) <br> = n x 11 =1100 <br> n=100</p>
               <p> So the number of lead shots =100</p>

