               <h6 class="question">Question 4</h6> A pen stand made of wood is in the shape of a cuboid with four
               conical depressions to hold pens. The dimensions of the cuboid are 15 cm by 10 cm by 3.5 cm. The radius
               of each of the depressions is 0.5 cm and the depth is 1.4 cm. Find the volume of wood in the entire stand
               (see Fig.).</strong></p>
               <img class="Q2ch3-5n" src="https://pathanto.com/Pathanto/image/ex13.2-6.png" alt="NCERT Ex-13.2 class 10"
                    width="250" height="200"><br>

               <h6 class="answer">Answer </h6>.
               <p>Volume of cuboid = length x width x height</p>
               <p> length = 15cm <br> width = 10cm <br>height =3.5cm </p>
               <p>So, the volume of the cuboid = 15x10x3.5 = 525 cm<sup>3</sup></p>
               <p>Given, radius r = 0.5 cm and height h = 1.4 cm</p>
               <p>Here, volume of conical depressions </p>
               <p> = (1/3)πr<sup>2</sup>h</p>

               <p>∴ Volume of 4 cones = 4x(1/3)πr<sup>2</sup>h</p>
               <p>= 1.46 cm<sup>2</sup></p>
               <p>Now, volume of wood = Volume of cuboid – 4 x volume of cone</p>
               <p>= 525-1.46 = 523.54 cm<sup>2</sup></p>

