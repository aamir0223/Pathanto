               <h6 class="question">Question 7</h6> A solid consisting of a right circular cone of height 120 cm and
               radius 60 cm standing on a hemisphere of radius 60 cm is placed upright in a right circular cylinder full
               of water such that it touches the bottom. Find the volume of water left in the cylinder, if the radius of
               the cylinder is 60 cm and its height is 180 cm.</p>
               <h6 class="answer">Answer 7</h6>.
               <img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.2-3.jpg"
                    alt="NCERT Ex-13.2 class 10"><br>
               <p>Given,</p>
               <p>Radius of cone r<sub> 1</sub> = 0.6 m,</p>
               <p>Height of cone h<sub> 1</sub>= 1.2 m</p>
               <p>Radius of cylinder r = 0.6 m</p>
               <p>Height of cylinder h= 1.8 m</p>
               <p>Radius of hemisphere r<sub> 2</sub>= 0.6 m</p>
               <p>Now,</p>
               <p> Volume of solid = volume of cone + volume of the hemisphere </p>
               <p> =(1/3)x π x r<sup>2</sup>h<sub> 1</sub> + (2/3) x π x r<sub> 2</sub><sup>3</sup> </p>
               <p> = (1/3) x (22/7) x (0.6)<sup>2</sup> x (1.2) + (2/3) x (22/7) x (0.6)<sup>3</sup></p>
               <p> = (22/21) x (0.6)<sup>2</sup>[ 1.2 + 2 x 0.6]</p>
               <p> = (22/21) x 0.36 (1.2 + 1.2)</p>
               <p> = 6.336/7 m<sup>3</sup></p>
               <p> volume of water left in cylinder = volume of water filled in circular cylinder - volume of solid </p>
               <p> (14.256/7 ) - (6.336/7) = 1.131 m<sup>3</sup></p>
