<h6 class="question">Question 1.</h6>

 Check whether the following are quadratic equations:<br>

(i) (x + 1)<sup>2</sup> = 2(x – 3)<br>

(ii) x<sup>2</sup> – 2x = (–2) (3 – x)<br>

(iii) (x – 2)(x + 1) = (x – 1)(x + 3)<br>

(iv) (x – 3)(2x +1) = x(x + 5)<br>

(v) (2x – 1)(x – 3) = (x + 5)(x – 1)<br>

(vi) x<sup>2</sup> + 3x + 1 = (x – 2)<sup>2</sup><br>

(vii) (x + 2)<sup>3</sup> = 2x (x2 – 1)<br>

(viii) x<sup>3</sup> – 4x<sup>2</sup> – x + 1 = (x – 2)<sup>3</sup><br>

<h6 class="answer">Answer </h6>

(i) Given <br>

(x + 1)<sup>2</sup> = 2(x – 3)<br>

 (a+b)<sup>2</sup> = a<sup>2</sup>+2ab+b<sup>2</sup><br>

⇒ x<sup>2</sup> + 2x + 1 = 2x – 6<br>

⇒ x<sup>2</sup> + 7 = 0<br>

   which is form of  ax<sup>2</sup> + bx + c = 0. where b=0<br>

It is a  quadratic equation.<br><br><br>

(ii) Given,<br> x<sup>2</sup> – 2x = (–2) (3 – x)<br>



⇒ x<sup>2</sup>  – 2x = -6 + 2x<br>

⇒ x<sup>2</sup>  – 4x + 6 = 0<br>

  which is form of  ax<sup>2</sup> + bx + c = 0.<br>

It is  quadratic equation.<br><br><br>

(iii) Given,<br> (x – 2)(x + 1) = (x – 1)(x + 3)<br>



⇒ x<sup>2</sup>  – x – 2 = x<sup>2</sup>  + 2x – 3<br.

⇒ 3x – 1 = 0 <br>

Which is  not in the form of ax<sup>2</sup> + bx + c = 0.<br>

It is  not a quadratic equation.<br><br><br>

(iv) Given,<br> (x – 3)(2x +1) = x(x + 5)<br>



⇒ 2x<sup>2</sup>  – 5x – 3 = x<sup>2</sup>  + 5x<br>

⇒  x<sup>2</sup>  – 10x – 3 = 0<br>

 which is form of  ax<sup>2</sup> + bx + c = 0.<br>

It is  quadratic equation.<br><br><br>

(v) Given,<br> (2x – 1)(x – 3) = (x + 5)(x – 1)<br>



⇒ 2x<sup>2</sup> – 7x + 3 = x<sup>2</sup> + 4x – 5<br>

⇒ x<sup>2</sup> – 11x + 8 = 0<br>

 It is   form of ax<sup>2</sup>+ bx + c = 0.<br>
 where a ≠ 0, and b,c are any real number<br>

It is  quadratic equation.<br><br><br>

(vi) Given, <br> x<sup>2</sup> + 3x + 1 = (x – 2)<sup>2</sup><br>



⇒ x<sup>2</sup> + 3x + 1 = x<sup>2</sup> + 4 – 4x<br>

⇒ 7x – 3 = 0<br>

It is form of ax<sup>2</sup> + bx + c = 0.<br>
where a=0 and b,c are any real number<br>

It  is not a quadratic equation.<br><br><br>

(vii) Given,<br> (x + 2)3 = 2x(x2 – 1)<br>



⇒ x<sup>3</sup>  + 8 + x<sup>2</sup>  + 12x = 2x<sup>3</sup>  – 2x<br>

⇒ x3 + 14x – 6x2 – 8 = 0<br>
It is not in the form of ax<sup>2</sup> + bx + c = 0.<br>
It is not a quadratic equation.<br><br><br>

(viii) Given, x<sup>3</sup>< – 4x<sup>2</sup>< – x + 1 = (x – 2)<sup>3</sup><

  x<sup>3</sup>< – 4x<sup>2</sup>< – x + 1 = x<sup>3</sup>< – 8 – 6x<sup>2</sup><  + 12x<br>
 2x<sup>2</sup><– 13x + 9 = 0<br>

It  is the form of ax<sup>2</sup>+ bx + c = 0.<br>
 where a ≠ 0, and b,c are any real number<br>

It is quadratic equation.<br>
