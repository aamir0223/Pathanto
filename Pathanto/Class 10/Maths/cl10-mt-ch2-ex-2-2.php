<p>Sum of zeroes = α+β = 1/4</p>
<p>Product of zeroes = α β = -1</p>
We know that <br>
<p>x<sup>2</sup>–(α+β)x +αβ = 0</p>
<p>x<sup>2</sup>–(1/4)x +(-1) = 0</p>
<p>4x<sup>2</sup>–x-4 = 0</strong></p>
