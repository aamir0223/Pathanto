            <h6 class="question">Question 2</h6>. The slant height of a frustum of a cone is 4 cm and the perimeters
            (circumference) of its circular ends are 18 cm and 6 cm. Find the surface area of the frustum.</p>
            <h6 class="answer">Answer 2</h6>

            <p>Given,</p>
            <p>Slant height l = 4 cm</p>
            <p>Circumference of upper end of the frustum = 18 cm</p>
            <p>∴ 2πr<sub>1</sub> = 18</p>
            <p>Or, r<sub>1</sub> = 9/π</p>
            <p> circumference of lower end of the frustum = 6 cm</p>
            <p>∴ 2πr<sub>2</sub> = 6</p>
            <p>Or, r<sub>2</sub> = 6/π</p>
            <p>Now, Coverd surface area of frustum = π(r<sub>1</sub>+r<sub>2</sub>) × l</p>
            <p>= π(9/π+6/π) × 4</p>
            <p>= 12×4 = 48 cm<sup>2</sup></p>

