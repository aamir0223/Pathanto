            <h6 class="question">Question 1</h6>.A drinking glass is in the shape of a frustum of a cone of height 14
            cm. The diameters of its two circular ends are 4 cm and 2 cm. Find the capacity of the glass.</p>
            <h6 class="answer">Answer 1</h6>

            <p>Radius r<sub>1</sub> of the upper end = 4/2 = 2 cm</p>
            <p>Radius (r<sub>2</sub>) of lower the end = 2/2 = 1 cm</p>
            <p>Height = 14 cm</p>
            <p> Capacity of glass = Volume of frustum of cone</p>
            <p> Capacity of glass =
                (1/3)×π×h(r<sub>1</sub><sup>2</sup>+r<sub>2</sub><sup>2</sup>+r<sub>1</sub>r<sub>2</sub>)</p>
            <p>= (1/3)× π × (14)(2<sup>2</sup>+1<sup>2</sup>+ (2)(1))</p>
            <p>∴ The capacity of the glass = 102×(2/3) cm<sup>3</sup></p>

