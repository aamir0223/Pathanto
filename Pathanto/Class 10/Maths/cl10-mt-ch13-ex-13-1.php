<p><h6 class="question">Question 5</h6> A hemispherical depression is cut out from one face of a cubical wooden block such that the diameter l of the hemisphere is equal to the edge of the cube. Determine the surface area of the remaining solid.</p>
<h6 class="answer">Answer 5</h6>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.1-5.png" alt="NCERT Ex-13.1 class 10"><br>

<p>Now, the diameter of hemisphere = Edge of the cube = l</p>
<p>So, the radius of hemisphere = l/2</p>
<p>∴ The total surface area of solid = surface area of cube + CSA of hemisphere – Area of base of hemisphere</p>
<p>TSA of remaining solid = 6 (edge)<sup>2</sup>+2πr<sup>2</sup> - πr<sup>2</sup></p>
<p>= 6l<sup>2</sup> + πr<sup>2</sup></p>
<p>= 6l<sup>2</sup> + π(l/2)<sup>2</sup></p>
