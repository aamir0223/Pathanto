<p><h6 class="question">Question 1</h6>
 2 cubes each of volume 64 cm<sup>3</sup> are joined end to end. Find the surface area of the resulting cuboid.</p>

<p><h6 class="answer">Answer </h6></p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.1-1.png" alt="NCERT Ex-13.1 class 10"><br>
<p>Given</p>
<p>The Volume  of each cube is = 64 cm<sup>3</sup></p>
<p>let's side of the cube =a</p>
<p>a<sup>3</sup> = 64 cm<sup>3</sup></p>
<p>∴ a = 4 cm</p>
<p> the side of the cube = a = 4 cm</p>

<p>Also, when we joined end to end of each cube then the length and breadth of the resulting cuboid will  8 cm and 4 cm.</p>
<p>So, the surface area of the cuboid = 2(lb+bh+lh)</p>
<p>= 2(8 × 4 + 4 × 4 + 4 × 8) cm<sup>2</sup></p>
<p>= 2(32+16+32) cm<sup>2</sup></p>
<p>= (2×80) cm<sup>2</sup> = 160 cm<sup>2</sup></p>

<p><h6 class="question">Question 2</h6>. A vessel is in the form of a hollow hemisphere mounted by a hollow cylinder. The diameter of the hemisphere is 14 cm and the total height of the vessel is 13 cm. Find the inner surface area of the vessel.</p>
<h6 class="answer">Answer 2</h6>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.1-2.png" alt="NCERT Ex-13.1 class 10"><br>


<p>The diameter of the hemisphere = D = 14 cm</p>
<p>The radius of the hemisphere = r = 7 cm</p>
<p> the height of the cylinder  h = 13 - 7 = 6 cm</p>
<p>  the radius of the hollow hemisphere = 7 cm</p>
<p>Now, the inner surface area of the vessel = Coverd Surface area of the cylindrical part + Coverd Surface area of hemispherical part</p>
<p>(2πrh+2πr<sup>2</sup>) cm<sup>2</sup> = 2πr(h+r) cm<sup>2</sup></p>
<p>2×(22/7)×7(6+7) cm<sup>2</sup> = 572 cm<sup>2</sup></p>

<p><h6 class="question">Question 3</h6> A toy is in the form of a cone of radius 3.5 cm mounted on a hemisphere of same radius. The total height of the toy is 15.5 cm. Find the total surface area of the toy.</p>
<h6 class="answer">Answer 3</h6>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.1-3.png" alt="NCERT Ex-13.1 class 10"><br>
<p>Given that  radius of the cone (r) = 3.5 cm <br> r= 7/2 cm</p>
<p>The total height of the toy = 15.5 cm.</p>
<p>So, the height of the cone = 15.5-3.5 = 12 cm</p>

<p> Total surface area of the toy =curved surface area of cone +curved surface area of the hemisphere = πrl + 2πr<sup>2</sup></p>
<p>=(22/7)×(7/2)×(25/2)  + 2×(22/7)×(7/2)<sup>2</sup></p>

<p>= (275/2)+77 cm<sup>2</sup></p>
<p>= (275+154)/2 cm<sup>2 </sup></p>
<p>= 429/2 cm<sup>2</sup> = 214.5cm<sup>2</sup></p>
<p>So, the total surface area of the toy is 214.5cm<sup>2</sup></p>
