<p><h6 class="question">Question 6</h6> A medicine capsule is in the shape of a cylinder with two hemispheres stuck to each of its ends. The length of the entire capsule is 14 mm and the diameter of the capsule is 5 mm. Find its surface area.</p>
<img class="Q2ch3-5n" src="https://pathanto.com/Pathanto/image/ex13.1-6.jpeg" alt="NCERT Ex-13.1 class 10"><br>
<h6 class="answer">Answer 6</h6>

<p>Two hemisphere and one cylinder are included</p>

<p>Here, the diameter of the capsule = 5 mm</p>
<p>∴ Radius = 5/2 = 2.5 mm</p>
<p>Now, the length of the capsule = 14 mm</p>
<p>So, the length of the cylinder = 14-(2.5+2.5) = 9 mm</p>
<p>∴ The surface area of a hemisphere = 2πr<sup>2</sup> = 2×(22/7)×2.5×2.5</p>
<p>= 275/7 mm<sup>2</sup></p>
<p>Now, the surface area of the cylinder = 2πrh</p>
<p>= 2×(22/7)×2.5×9</p>
<p>(22/7)×45 = 990/7 mm<sup>2</sup></p>
<p>Thus, the required surface area of medicine capsule </p>
<p>= 2 × surface area of hemisphere + surface area of the cylinder</p>
<p>= (2×275/7) × 990/7</p>
<p>(550/7) + (990/7) = 1540/7 = 220 mm<sup>2</sup></p>

<p><h6 class="question">Question 7</h6> A tent is in the shape of a cylinder surmounted by a conical top. If the height and diameter of the cylindrical part are 2.1 m and 4 m respectively, and the slant height of the top is 2.8 m, find the area of the canvas used for making the tent. Also, find the cost of the canvas of the tent at the rate of Rs 500 per m<sup>2</sup>. (Note that the base of the tent will not be covered with canvas.)</p>
<h6 class="answer">Answer 7</h6>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex13.1-7.jpeg" alt="NCERT Ex-13.1 class 10"><br>

<p> we know that</p>
<p>Diameter = 4 m</p>
<p>Slant height of the cone l = 2.8 m</p>
<p>Radius of the cone r = Radius of cylinder = 4/2 = 2 m</p>
<p>Height of the cylinder h = 2.1 m</p>
<p>So, the required surface area of tent = surface area of cone + surface area of cylinder</p>
<p>= πrl+2πrh</p>
<p>= πr(l+2h)</p>
<p>= (22/7)×2(2.8+2×2.1)</p>
<p>= (44/7)(2.8+4.2)</p>
<p>= (44/7)×7 = 44 m<sup>2</sup></p>
<p>∴ The cost of the canvas of the tent at the rate of ₹500 per m<sup>2 </sup>will be</p>
<p>= Surface area × cost per m<sup>2</sup></p>
<p>44×500 = ₹22000</p>
<p>So, Rs. 22000 will be the total cost of the canvas.</p>

