               <p>Area of gold region = π r<sub>1</sub><sup>2 </sup>= π(10.5)<sup>2 </sup>= 346.5 cm<sup>2</sup></p>

               <p>So, the radius of red region r<sub>2</sub> = 10.5cm+10.5cm = 21 cm</p>
               <p>Thus,</p>
               <p>∴ Area of red region = Area of 2<sup>nd</sup> circle − Area of gold region =
                    (πr<sub>2</sub><sup>2</sup>−346.5) cm<sup>2</sup></p>

               <p>= (π(21)<sup>2</sup> − 346.5) cm<sup>2</sup></p>
