               <h6 class="question">Question 1</h6> The radii of two circles are 19 cm and 9 cm respectively. Find the
               radius of the circle which has a circumference equal to the sum of the circumferences of the two circles.
               </p>
               <h6 class="answer">Answer</h6>
               <p>r<sub>1</sub>= 19 cm </p>
               <p> Circumference of the 1c<sup>st</sup> circle = 2π×19 = 38π cm</p>
               <p>r<sub>2</sub> = 9 cm </p>
               <p> Circumference of the 2<sup>nd</sup> circle = 2π×9 = 18π cm</p>
               <p>Now, let the radius of new circle circle = R</p>
               <p> According to question</p>

               <p> circumference of new circle =Circumference of the 1<sup>st</sup> circle + Circumference of the
                    2<sup>nd</sup> circle
               <p>
               <p> 2πR= 38π+18π = 56π cm</p>



               <p>2πR =56π </p>
               <p> R= 56π/2π</p>
               <p> R = 28 cm.</p>

               <p>
               <h6 class="question">Question 2</h6> The radii of two circles are 8 cm and 6 cm, respectively. Find the
               radius of the circle having area equal to the sum of the areas of the two circles.</p>
               <h6 class="answer">Answer</h6>
               <p> r<sub>1</sub> = 8 cm </p>
               <p> Area of 1<sup>st</sup> circle = π(8)<sup>2</sup> = 64π</p>
               <p>Radius of 2<sup>nd</sup> circle r<sub>2</sub> = 6 cm </p>
               <p>Area of 2<sup>nd</sup> circle = π(6)<sup>2</sup> = 36π</p>
               <p>So,</p>
               <p>The sum of 1<sup>st</sup> and 2<sup>nd</sup> circle will be = 64π+36π = 100π</p>
               <p>let the radius of new circle = R</p>
               <p> Area of the new circle = πR<sup>2</sup></p>
               <p>The area of the new circle = Area of 1<sup>st</sup> circle + Area of 2<sup>nd</sup> circle</p>
               <p>Or, πR<sup>2</sup> = 100πcm<sup>2</sup></p>
               <p>R<sup>2</sup> = 100cm<sup>2</sup></p>
               <p>So, R = 10cm</p>

