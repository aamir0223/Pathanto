               <h6 class="question">Question 4</h6> The wheels of a car are of diameter 80 cm each. How many complete
               revolutions does each wheel make in 10 minutes when the car is travelling at a speed of 66 km per hour?
               </p>
               <h6 class="answer">Answer</h6>
               <p> diameter of wheel d<sub> 1</sub> = 80</p>
               <p>The radius of car’s wheel = 80/2 = 40 cm</p>
               <p> the circumference of wheels = 2πr = 80 π cm</p>
               <p> Since speed of car =66kmh<sup>-1</sup> <br> 66 X 1000 /60 = 1100 m min<sup>-1</sup> </p>

               <p>Distance covered by the car in 1hr = (66×10<sup>5</sup>) cm</p>
               <p>In 10 minutes, the distance covered will be = (66×10<sup>5</sup>×10)/60 = 1100000 cm/s</p>
               <p>∴ Distance covered by car = 11×10<sup>5</sup> cm</p>
               <p>Now, the no. of revolutions of the wheels = (Distance covered by the car/Circumference of the wheels)
               </p>
               <p>=( 11×10<sup>5</sup>)/80 π = 4375.</p>

