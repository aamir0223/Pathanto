      So,<br>

      product of 26 and 91=Product of LCM(26,91) and HCF(26,91)<br><br>

      (ii) 510 and 92<br>

      510 = 2 × 3 × 17 × 5 × 1<br>

      92 = 2 × 2 × 23 × 1<br>

      So, LCM(510, 92) = 2 × 2 × 3 × 5 × 17 × 23 = 23460<br>

      And HCF (510, 92) = 2<br><br>

      Verification<br>
      LCM × HCF = product of both number<br>
      Now, product of 510 and 92 = 510 × 92 = 46920<br>

      And Product of LCM and HCF = 23460 × 2 = 46920<br>

      Hence, LCM × HCF = product of the 510 and 92.<br><br>

