<p> Write True or False: Give reasons for your Solutions.</p>

<p>(i) Line segment joining the centre to any point on the circle is a radius of the circle.</p>

<p>(ii) A circle has only finite number of equal chords.</p>

<p>(iii) If a circle is divided into three equal arcs, each is a major arc.</p>

<p>(iv) A chord of a circle, which is twice as long as its radius, is a diameter of the circle.</p>

<p>(v) Sector is the region between the chord and its corresponding arc.</p>

<p>(vi) A circle is a plane figure.</p>

<h4 class="answer"> Answer</h4>
<p>(i) True  <br>
 Line segment joining the centre to any point on the circle is a radius of the circle and  equidistant from the centre</p>
<p>(ii) False  <br>
 An infinite number of equal chords in circle</p>
<p>(iii) False <br>
 Equal arcs on a circle can't be  a major arc or a minor arc</p>
<p>(iv) True <br>
 Diameter = 2 x Radius</p>
<p>(v) False <br>
  Region of a circle between the arc and the two radii of the circle is sector.</p>
<p>(vi) True <br>
 A circle is a plane figure</p>






    
