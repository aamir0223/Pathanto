<p><h6 class="question">Question 7</h6>A chord of a circle of radius 12 cm subtends an angle of 120° at the centre. Find the area of the corresponding segment of the circle. (Use π = 3.14 and √3 = 1.73)</p>
<h6 class="answer">Answer</h6>
<img class="mode" src="https://pathanto.com/Pathanto/image/ex12.2-7.png" alt="NCERT Ex-12.2 class 10"><br>.
<p> cord AB substended an angle 120° to the center <br> </p>
<p> <span>&#8736;</span> AOB = 120° <br> draw OD perpendicular to AB <br> AB=BD </p>
<p> <span>&#8736;</span> AOD =<span>&#8736;</span> BOD= 60° </p>
<p>here radius r=12 cm<br> IN ΔAOD <br> sin60°=AD/AO= √3/2=AD/12 <br> AD =6√3 =10.38 <br>cos60°=OD/AO= 1/2=OD/12 <br> OD =6cm </p>
<p>length AB = 2AD =2 x 10.38 = 20.76 </p>
<p>So, the area of ΔAOB = 1/2 × base × height</p>
<p>So, area of ΔAOB = 1/2 × 20.76 × 6 = 36√3 cm = 62.28 cm<sup>2</sup></p>

<p>Now, the area of the minor sector = (θ/360°)×πr<sup>2</sup></p>
<p>= (120/360)×(22/7)×12<sup>2</sup></p>
<p>= 150.72 cm<sup>2</sup></p>
<p>∴ Area of the corresponding Minor segment = Area of the Minor sector – Area of ΔAOB</p>
<p>= 150.72 cm<sup>2</sup>– 62.28 cm<sup>2 </sup>= 88.44 cm<sup>2</sup></p>





