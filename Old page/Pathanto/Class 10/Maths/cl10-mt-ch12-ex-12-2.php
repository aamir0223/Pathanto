<p><h6 class="question">Question 6</h6>  A chord of a circle of radius 15 cm subtends an angle of 60° at the centre. Find the areas of the corresponding minor and major segments of the circle. (Use π = 3.14 and √3 = 1.73)</p>
<h6 class="answer">Answer</h6>
