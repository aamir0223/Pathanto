<p><h6 class="question">Question  4</h6> 
A chord of a circle of radius 10 cm subtends a right angle at the centre. Find the area of the corresponding:</p>
<p>(i) minor segment </p>
<p>(ii) major sector. (Use π = 3.14)</p>
<h6 class="answer">Answer</h6>
<img class="mode" src="https://pathanto.com/Pathanto/image/EX12.2-4.png" alt="NCERT Ex-12.2 class 10"><br>
<p>Given radius of a circle AO=10cm</p>
<p>A perpendicular is drawn from the center of circle to the chord of the circle which bisect the chord</p>
<p> AD=DC<br> <span>&#8736;</span>AOD=<span>&#8736;</span>COD =45°<br> <span>&#8736;</span>AOC =<span>&#8736;</span>AOD +<span>&#8736;</span>COD<br>=45° +45°=90</p>
<p>  In ΔAOD </p>
<p> sin45°= AD/AO= 1/<span>&#8730;</span>2 =AD/10</p>
<p> AD= 5<span>&#8730;</span>2 <br> and </p>
<p> cos45°= OD/AO= 1/<span>&#8730;</span>2 =OD/10</p>
<p> OD= 5<span>&#8730;</span>2 <br> and </p>
<p> AC = 2 AD =2 x  5<span>&#8730;</span>2 =10<span>&#8730;</span>2 </p>

<p>Also, area of ΔAOC = 1/2 × AC x OD</p>

<p>So, area of ΔAOB = 1/ × 10<span>&#8730;</span>2 × 5<span>&#8730;</span>2</p>
<p>= 50 cm<sup>2</sup></p>

<p>(i) Area of minor sector = (90/360°)×πr<sup>2</sup></p>
<p>=   90/360 x 3.14 x 10<sup>2</sup></p>
<p>= 78.5 cm<sup>2</sup></p>

<p>Now, area of minor segment = area of minor sector – area of ΔAOB</p>
<p>= 78.5 – 50</p>
<p>= 28.5 cm<sup>2</sup></p><br>
<p><strong>(ii)</strong> Area of major sector = Area of circle – Area of minor sector</p>
<p>= (3.14×10<sup>2</sup>)-78.5</p>
<p>= 235.5 cm<sup>2</sup></p>

