<p><h6 class="question">Question 1</h6> Find the area of a sector of a circle with radius 6 cm if angle of the sector is 60°.</p>
<h6 class="answer">Answer</h6>
<p> angle of the sector is 60°</p>
<p>the area of sector = (θ/360°)×πr<sup>2 </sup></p>
<p>∴ Area of the sector with angle 60° = (60°/360°)×πr<sup>2 </sup>cm<sup>2</sup></p>
<p>= (36/6)π cm<sup>2</sup></p>
<p>= 6×22/7 cm<sup>2 </sup>= 132/7 cm<sup>2</sup></p>
<p><h6 class="question">Question 2 </h6> Find the area of a quadrant of a circle whose circumference is 22 cm.</p>
<h6 class="answer">Answer</h6>
<p>Circumference of the circle = 22 cm (given)</p>
<p> quadrant of a circle is a sector which is making an angle of 90°.</p>
<p> radius of the circle = r</p>
<p>here <br> 2πr = 22,</p>
<p>r = 22/2π cm = 7/2 cm</p>
<p>∴ Area of the quadrant = (θ/360°) × πr<sup>2</sup></p>
<p>Here, θ = 90°</p>
<p>So, A = (90°/360°) × π r<sup>2 </sup>cm<sup>2</sup></p>
<p>= (49/16) π cm<sup>2</sup></p>
<p>= 77/8 cm<sup>2</sup> = 9.6 cm<sup>2</sup></p>

