<p>The angle swept by the minute hand in 1 minutes = 360°/60 = 6<sup>o</sup></p>
<p>The angle swept by the minute hand in 5 minutes =5 x  6<sup>o</sup> =30<sup>o<sup></p>
<p>Area of a sector = (θ/360°) × πr<sup>2</sup></p>
<p> = (30°/360°) × πr<sup>2 </sup>cm<sup>2</sup></p>
<p>= (1/12) × π14<sup>2</sup></p>
<p>= (49/3)×(22/7) cm<sup>2 </sup></p>
