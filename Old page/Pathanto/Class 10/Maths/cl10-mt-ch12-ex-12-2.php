<p><h6 class="question">Question 13 </h6>
 A round table cover has six equal designs as shown in Fig. 12.14. If the radius of the cover is 28 cm, find the cost of making the designs at the rate of ₹ 0.35 per cm<sup>2</sup> . (Use √3 = 1.7)</p>
  <img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/ex-12-2-13-class10.jpg" alt="NCERT Ex-12.2 class 10">
<h6 class="answer">Answer</h6>

<p>Total number of equal designs = 6</p>
<p>AOB= 360°/6 = 60°</p>
<p>Radius of the cover = 28 cm</p>
<p>Cost of making design = ₹ 0.35 per cm<sup>2</sup></p>

<p>Since the two arms of the triangle are the radii of the circle and thus are equal, and one angle is 60°, ΔAOB is an equilateral triangle. So, its area will be (√3/4)×a<sup>2</sup> sq. units</p>
<p>Here, a = OA</p>
<p>∴ Area of equilateral ΔAOB = (√3/4)×28<sup>2 </sup>= 333.2 cm<sup>2</sup></p>
<p>Area of sector ACB = (60°/360°)×πr<sup>2 </sup>cm<sup>2</sup></p>
<p>= 410.66 cm<sup>2</sup></p>
<p>So, area of a single design = area of sector ACB – area of ΔAOB</p>
<p>= 410.66 cm<sup>2</sup> – 333.2 cm<sup>2 </sup>= 77.46 cm<sup>2</sup></p>
<p>∴ Area of 6 designs = 6×77.46 cm<sup>2 </sup>= 464.76 cm<sup>2</sup></p>
<p>So, total cost of making design = 464.76 cm<sup>2 </sup>×Rs.0.35 per cm<sup>2</sup></p>
<p>= Rs. 162.66</p>
