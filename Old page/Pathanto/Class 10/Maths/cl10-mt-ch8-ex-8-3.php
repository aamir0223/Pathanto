<p><h6 class="question">Question 3</h6> If tan 2A = cot (A – 18°), where 2A is an acute angle, find the value of A</p>
<h6 class="answer">Answer</h6>
<p>tan 2A = cot (A- 18°)</p>
<p>We know that tan 2A = cot (90° – 2A)</p>

<p>⇒ cot (90° – 2A) = cot (A -18°)</p>
<p>On compare </p>
<p>⇒ 90° – 2A = A- 18° ⇒ 108° = 3A</p>
<p>A = 108° / 3</p>
<p> the value of A = 36°</p>
<p><h6 class="question">Question 4</h6>  If tan A = cot B, prove that A + B = 90°.</p>
<h6 class="answer">Answer</h6>
<p>tan A = cot B</p>
<p>We know that cot B = tan (90° – B)</p>

<p>tan A = tan (90° – B)</p>
<p>A = 90° – B</p>
<p>A + B = 90°</p>
<p>Hence Proved.</p>
<p><h6 class="question">Question 5</h6> If sec 4A = cosec (A – 20°), where 4A is an acute angle, find the value of A.</p>
<h6 class="answer">Answer</h6>
<p>sec 4A = cosec (A – 20°)</p>
<p>We know that sec 4A = cosec (90° – 4A)</p>

<p>cosec (90° – 4A) = cosec (A – 20°)</p>
<p>Now, On compare</p>
<p>90° – 4A= A- 20°</p>
<p>110° = 5A</p>
<p>A = 110°/ 5 = 22°</p>
<p>Therefore, the value of A = 22°</p>
<p><h6 class="question">Question 6</h6> If A, B and C are interior angles of a triangle ABC, then show that</p>
<p>   sin (B+C/2) = cos A/2</p>
<p>Solution:</p>
<p>We know that  sum of all the interior angles of a triangle is equal to 180°</p>
<p>A + B + C = 180° ….(1)</p>

<p>⇒ B + C = 180° – A</p>
<p>On dividing both side by 2</p>
<p>⇒ (B+C)/2 = (180°-A)/2</p>
<p>⇒ (B+C)/2 = (90°-A/2)</p>
<p>Now, multiply both sides by sin functions, we get</p>
<p>⇒ sin (B+C)/2 = sin (90°-A/2)</p>
<p>Since sin (90°-A/2) = = cos A/2</p>
<p>sin (B+C)/2 = cos A/2</p>
<p>Hence proved.</p>
<p><h6 class="question">Question 7</h6> Express sin 67° + cos 75° in terms of trigonometric ratios of angles between 0° and 45°.</p>
<h6 class="answer">Answer</h6>
<p>Given:</p>
<p>sin 67° + cos 75°</p>

<p>sin 67° = sin (90° – 23°)</p>
<p>cos 75° = cos (90° – 15°)</p>
<p>= sin (90° – 23°) + cos (90° – 15°)</p>

<p>= cos 23° + sin 15°</p>
<p>Therefore, sin 67° + cos 75° is also expressed as cos 23° + sin 15°</p>
