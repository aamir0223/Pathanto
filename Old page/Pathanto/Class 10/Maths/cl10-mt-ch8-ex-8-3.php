<p><h6 class="question">Question 1</h6> Evaluate :</p>
<p>(i) sin 18°/cos 72°      </p>
<p>(ii) tan 26°/cot 64°      </p>
<p>(iii)  cos 48° – sin 42°       </p>
<p>(iv)  cosec 31° – sec 59°</p>
<h6 class="answer">Answer</h6>
<p>(i) sin 18°/cos 72°</p>

<p>We know that,sin (90° – A)= cos A.</p>
<p>= sin (90° – 18°) /cos 72°</p>

<p>= cos 72° /cos 72° = 1</p>
<p>(ii) tan 26°/cot 64°</p>

<p>We know that tan (90° – A) = cot A</p>
<p>= tan (90° – 36°)/cot 64°</p>

<p>= cot 64°/cot 64° = 1</p>
<p>(iii) cos 48° – sin 42°</p>

<p>We know that cos (90° – A)= sin A.</p>
<p>= cos (90° – 42°) – sin 42°</p>

<p>= sin 42° – sin 42° = 0</p>
<p>(iv) cosec 31° – sec 59°</p>

<p>We know that,cosec (90° – 59°)= sec A</p>
<p>= cosec (90° – 59°) – sec 59°</p>

<p>= sec 59° – sec 59° = 0</p>
<p><h6 class="question">Question 2</h6>  Show that:</p>
<p>(i) tan 48° tan 23° tan 42° tan 67° = 1</p>
<p>(ii) cos 38° cos 52° – sin 38° sin 52° = 0</p>
<h6 class="answer">Answer</h6>
<p>(i) tan 48° tan 23° tan 42° tan 67°</p>

<p>We know that, tan 48° = tan (90° – 42°) = cot 42°  ,  tan 23° = tan (90° – 67°) = cot 67°</p>
<p>= tan (90° – 42°) tan (90° – 67°) tan 42° tan 67°</p>

<p>= cot 42° cot 67° tan 42° tan 67°</p>
<p>= (cot 42° tan 42°) (cot 67° tan 67°) = 1×1 = 1</p>

<p>(ii) cos 38° cos 52° – sin 38° sin 52°</p>

<p>We know that,</p>
<p>cos 38° = cos (90° – 52°) = sin 52°</p>
<p>cos 52°= cos (90°-38°) = sin 38°</p>
<p>= cos (90° – 52°) cos (90°-38°) – sin 38° sin 52°</p>

<p>= sin 52° sin 38° – sin 38° sin 52° = 0</p>
