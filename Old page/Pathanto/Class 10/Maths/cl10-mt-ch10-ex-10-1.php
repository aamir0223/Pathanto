<p><h6 class="question">Question 2 </h6>
Fill in the blanks:</p>
<p>(i) A tangent to a circle intersects it in …………… point(s).</p>
<p>(ii) A line intersecting a circle in two points is called a ………….</p>
<p>(iii) A circle can have …………… parallel tangents at the most.</strong></p>
<p>(iv) The common point of a tangent to a circle and the circle is called …………</p>
<h6 class="answer">Answer:</h6>
<p>(i) A tangent to a circle intersects it in one point(s).</p>
<p>(ii) A line intersecting a circle in two points is called a secant.</p>
<p>(iii) A circle can have two parallel tangents at the most.</p>
<p>(iv) The common point of a tangent to a circle and the circle is called the point of contact.</p>
