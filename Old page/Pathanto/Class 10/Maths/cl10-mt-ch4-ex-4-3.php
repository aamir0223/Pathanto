<h6 class="question">Question 4</h6>
 The sum of the reciprocals of Rehman’s ages, (in years) 3 years ago and 5 years from now is 1/3. Find his present age.

<h6 class="answer">Answer</h6>

<p>Let  present age of Rehman is <em>x</em> years.</p>
<p>Three years ago, Rehman’s age was (<em>x</em> – 3) years.</p>
<p>Five years after, his age will be (<em>x</em> + 5) years.</p>
<p> the sum of the reciprocals of Rehman’s ages 3 years ago and after 5 years is equal to 1/3.</p>
<p>∴ 1/<em>x</em>-3 + 1/<em>x</em>-5 = 1/3</p>
<p><em>(x</em>+5+<em>x</em>-3)/(<em>x</em>-3)(<em>x</em>+5) = 1/3</p>
<p>(2<em>x</em>+2)/(<em>x</em>-3)(<em>x</em>+5) = 1/3</p>
<p>⇒ 3(2<em>x</em> + 2) = (<em>x</em>-3)(<em>x</em>+5)</p>
<p>⇒ 6<em>x</em> + 6 = <em>x</em><sup>2</sup> + 2<em>x</em> – 15</p>
<p>⇒ <em>x</em><sup>2</sup> – 4<em>x</em> – 21 = 0</p>
<p>⇒ <em>x</em><sup>2</sup> – 7<em>x</em> + 3<em>x</em> – 21 = 0</p>
<p>⇒ <em>x</em>(<em>x</em> – 7) + 3(<em>x</em> – 7) = 0</p>
<p>⇒ (<em>x</em> – 7)(<em>x</em> + 3) = 0</p>
<p>⇒ <em>x</em> = 7, -3</p>
<p> age cannot be negative.</p>
<p>Therefore, Rehman’s present age is 7 years.</p>

