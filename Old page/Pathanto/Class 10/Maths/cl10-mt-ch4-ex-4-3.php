<p>suppose  sides of the two squares be <em>x</em> m and <em>y</em> m.</p>
<p>And area of the squares will be <em>x</em><sup>2</sup> and <em>y</em><sup>2</sup> respectively.</p>
<p>and  their perimeter will be 4<em>x</em> and 4<em>y</em> respectively</p>
 <p>According to question ,</p>
 <p><em>x</em><sup>2</sup> + <em>y</em><sup>2</sup> =468   </p>
<p>and difference of their perimeter =24</p>
<p>4<em>x</em> – 4<em>y</em> = 24</p>
<p><em>x</em> – <em>y</em> = 6</p>
<p><em>x</em> = <em>y</em> + 6</p>
<p> <em>x</em><sup>2 </sup>+<sup> </sup><em>y</em><sup>2</sup> = 468</p>
<p>⇒ (6 + <em>y</em><sup>2</sup>) + <em>y</em><sup>2</sup> = 468</p>
<p>⇒ 36 + <em>y</em><sup>2</sup> + 12<em>y</em> + <em>y</em><sup>2</sup> = 468</p>
<p>⇒ 2<em>y</em><sup>2</sup> + 12<em>y</em> + 432 = 0</p>
<p>⇒ <em>y</em><sup>2</sup> + 6y – 216 = 0</p>
<p>⇒ <em>y</em><sup>2</sup> + 18<em>y</em> – 12<em>y</em> – 216 = 0</p>
<p>⇒ <em>y</em>(<em>y </em>+18) -12(<em>y</em> + 18) = 0</p>
<p>⇒ (<em>y</em> + 18)(<em>y</em> – 12) = 0</p>
<p>⇒ <em>y</em> = -18, 12</p>
<p>As  the side of a square cannot be negative.</p>
<p>Hence, the sides of the squares are 12 m and (12 + 6) m = 18 m.</p>

