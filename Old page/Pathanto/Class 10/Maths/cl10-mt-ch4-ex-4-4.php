<p><h6 class="question">Question 1</h6>
 Find the nature of the roots of the following quadratic equations. If the real roots exist, find them;<br>
(i) 2<em>x</em><sup>2</sup> – 3<em>x</em> + 5 = 0<br>
(ii) 3<em>x</em><sup>2</sup> – 4√3<em>x</em> + 4 = 0<br>
(iii) 2<em>x</em><sup>2</sup> – 6<em>x</em> + 3 = 0</p>
<h6 class="answer">Answer (i)</h6>

<p><em>2x</em><sup>2</sup> – 3<em>x</em> + 5 = 0</p>
<p> On Comparing  equation with <em>ax</em><sup>2</sup> + <em>bx </em>+ <em>c</em> = 0,</p>
<p>Here <em>a</em> = 2, <em>b</em> = -3 and <em>c</em> = 5</p>
<p> Discriminant = <em>b</em><sup>2</sup> – 4<em>ac</em></p>
<p><em>= </em>( – 3)<sup>2</sup> – 4 (2) (5) = 9 – 40</p>
<p>= – 31< 0</p>
<p>As  b<sup>2</sup> – 4ac &lt; 0</p>
<p>Hece  no real root is possible for  <em>2x</em><sup>2</sup> – 3<em>x</em> + 5 = 0.</p>


<p><h6 class="answer">Answer (ii)</h6> 3<em>x</em><sup>2</sup> – 4√3<em>x</em> + 4 = 0</p>
<p>On Comparing  equation with <em>ax</em><sup>2</sup> + <em>bx </em>+ <em>c</em> = 0</p>
<p><em>a</em> = 3, <em>b</em> = -4√3 and <em>c</em> = 4</p>
<p> Discriminant = <em>b</em><sup>2</sup> – 4<em>ac</em></p>
<p>= (-4√3)<sup>2 </sup>– 4(3)(4)</p>
<p>= 48 – 48 = 0</p>
<p>As <em>b</em><sup>2</sup> – 4<em>ac</em> = 0,</p>
<p> given equation has two  equal root and real root .</p>
<p>So, the roots will be –<em>b</em>/2<em>a</em> and –<em>b</em>/2<em>a</em>.</p>
