<p><h6 class="question">Question 5</h6>
 Is it possible to design a rectangular park of perimeter 80 and area 400 m2? If so find its length and breadth.</p>
<h6 class="answer">Answer </h6>
<p>Let the length and breadth of the park be <em>l </em>and b.</p>
<p>Perimeter of the rectangular park = 2 (<em>l + b</em>) = 80</p>
<p><em>So, l + b </em>= 40</p>
<p>Or, <em>b</em> = 40 – <em>l</em></p>
<p>Area of the rectangular park = <em>l×b = l(40 – l) = </em>40<em>l </em>– <em>l</em><sup>2 </sup>= 400</p>
<p><em>l</em><sup>2</sup><em> </em>–<sup>  </sup>40<em>l</em> + 400<em> </em>= 0, A quadratic equation.</p>
<p>ON Comparing the equation with <em>ax</em><sup>2</sup> + <em>bx </em>+ <em>c</em> = 0</p>
<p><em>a</em> = 1, <em>b</em> = -40, <em>c</em> = 400</p>
<p> Discriminant = <em>b</em><sup>2</sup> – 4<em>ac</em></p>
<p><em>=(-</em>40<em>)</em><sup>2</sup> – 4 × 400</p>
<p>= 1600 – 1600 = 0</p>
<p>Thus,<em> b</em><sup>2</sup> – 4<em>ac </em>= 0</p>
<p>Therefore, this equation has equal real roots. So the situation is possible.</p>

<p><em>l</em> = –<em>b</em>/2<em>a</em></p>
<p><em>l</em> = (40)/2(1) = 40/2 = 20</p>
<p>Therefore, length of rectangular park, <em>l </em>= 20 m</p>
<p>And breadth of the park, <em>b </em>= 40 – <em>l </em>= 40 – 20 = 20 m.</p>
