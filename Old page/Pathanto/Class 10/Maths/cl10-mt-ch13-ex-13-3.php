<p><h6 class="question">Question 1</h6>. A metallic sphere of radius 4.2 cm is melted and recast into the shape of a cylinder of radius 6 cm. Find the height of the cylinder.</p>
<h6 class="answer">Answer 1</h6>
<p> radius of the sphere R = 4.2 cm</p>
<p> Radius of cylinder r = 6 cm</p>
<p> let height of cylinder = h</p>
<p>According to question </p>
<p>So, Volume of Sphere = Volume of Cylinder</p>
<p>∴ (4/3)×π×R<sup>3 </sup>= π×r<sup>2</sup>×h.</p>
<p>h = 2.74 cm</p>

<p><h6 class="question">Question 2</h6>. Metallic spheres of radii 6 cm, 8 cm and 10 cm, respectively, are melted to form a single solid sphere. Find the radius of the resulting sphere.</p>
<h6 class="answer">Answer 2</h6>
<p>For Sphere 1</p>
<p>Radius r<sub>1</sub> = 6 cm</p>
<p>∴ Volume V<sub>1</sub> = (4/3)×π×r<sub>1</sub><sup>3</sup></p>
<p>For Sphere 2:</p>
<p>Radius r<sub>2</sub> = 8 cm</p>
<p>∴ Volume V<sub>2</sub> = (4/3)×π×r<sub>2</sub><sup>3</sup></p>
<p>For Sphere 3:</p>
<p>Radius r<sub>3</sub> = 10 cm</p>
<p>∴ Volume V<sub>3</sub> = (4/3)× π× r<sub>3</sub><sup>3</sup></p>
<p> the radius of the resulting sphere be R</p>
<p>Now,</p>
<p>Volume of resulting sphere = V<sub>1</sub>+V<sub>2</sub>+V<sub>3</sub></p>
<p>(4/3)×π×R<sup>3</sup> = (4/3)×π×r<sub>1</sub><sup>3</sup>+(4/3)×π×r<sub>2</sub><sup>3</sup> +(4/3)×π×r<sub>3</sub><sup>3</sup></p>
<p>R<sup>3 </sup>= 6<sup>3</sup>+8<sup>3</sup>+10<sup>3</sup></p>
<p>R<sup>3 </sup>= 1728</p>
<p>R = 12 cm</p>

<p><h6 class="question">Question 3</h6>. A 20 m deep well with diameter 7 m is dug and the earth from digging is evenly spread out to form a platform 22 m by 14 m. Find the height of the platform.</p>
<h6 class="answer">Answer 3</h6>

<p>So, radius = 7/2 m</p>
<p>Also, height of deep well = 20 m</p>

<p>∴ Volume of Cylinder = π×r<sup>2</sup>×h</p>
<p>= 22×7×5 m<sup>3</sup></p>
<p>Let the height of the platform = H</p>
<p>Volume of deep well (cylinder) = Volume of soil used to make such platform</p>
<p>π×r<sup>2</sup>×h = Area of platform × Height of the platform</p>
<p>We know that the dimension of the platform is = 22×14</p>
<p>So, Area of platform = 22×14 m<sup>2</sup></p>
<p>∴ π×r<sup>2</sup>×h = 22×14×H</p>
<p>⇒ H = 2.5 m</p>
