<p><h6 class="question">Question 8</h6> Water in a canal, 6 m wide and 1.5 m deep, is flowing with a speed of 10 km/h. How much area will it irrigate in 30 minutes, if 8 cm of standing water is needed?</p>
<h6 class="answer">Answer 8</h6>

<p>Breadth (b) = 6 m and Height (h) = 1.5 m</p>

<p>The speed of canal = 10 km/hr</p>
<p>Length of canal covered in 1 hour = 10 km</p>
<p>Length of canal covered in 60 minutes = 10 km</p>
<p>Length of canal covered in 1 min = (1/60)x10 km</p>
<p>Length of canal covered in 30 min (l) = (30/60)x10 = 5km = 5000 m</p>
<p>We know that the canal is cuboidal in shape. So,</p>
<p>Volume of canal = lxbxh</p>
<p>= 5000x6x1.5 m<sup>3</sup></p>
<p>= 45000 m<sup>3</sup></p>
<p>Now,</p>
<p>Volume of water in canal = Volume of area irrigated</p>
<p>= Area irrigated x Height</p>
<p>So, Area irrigated = 56.25 hectares</p>
<p>∴ Volume of canal = lxbxh</p>
<p>45000 = Area irrigatedx8 cm</p>
<p>45000 = Area irrigated x (8/100)m</p>
<p>Or, Area irrigated = 562500 m<sup>2 </sup>= 56.25 hectares.</p>

<p><h6 class="question">Question 9</h6> A farmer connects a pipe of internal diameter 20 cm from a canal into a cylindrical tank in her field, which is 10 m in diameter and 2 m deep. If water flows through the pipe at the rate of 3 km/h, in how much time will the tank be filled?</strong></p>
<h6 class="answer">Answer 9</h6>
<p>Consider the following diagram-</p>
>
<p>Volume of water that flows in t minutes from pipe = t×0.5π m<sup>3</sup></p>

<p>Radius (r<sub>2</sub>) of the cylindrical tank =10/2 = 5 m</p>
<p>Depth (h<sub>2</sub>) of cylindrical tank = 2 m</p>
<p>Let the tank be filled completely in t minutes.</p>
<p>According to question</p>
<p>Volume of water that flows in t minutes from pipe = Volume of water in tank</p>
<p>t×0.5π = π×r<sub>2</sub><sup>2</sup>×h<sub>2</sub></p>
<p>Or, t = 100 minutes</p>
