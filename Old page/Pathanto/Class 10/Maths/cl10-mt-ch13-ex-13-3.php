<p><h6 class="question">Question 4</h6> A well of diameter 3 m is dug 14 m deep. The earth taken out of it has been spread evenly all around it in the shape of a circular ring of width 4 m to form an embankment. Find the height of the embankment.</strong></p>
<h6 class="answer">Answer 4</h6>
<img class="Q2ch3-5n" src="https://pathanto.com/Pathanto/image/ex13.3-1.png" alt="NCERT Ex-13.3 class 10"><br>
<p>The shape of the well will be cylindrical as given below.</p>

<p> Depth (h<sub>1</sub>) of well = 14 m</p>
<p>Diameter of the well =3 m</p>
<p>So, Radius r<sub>1</sub> = 3/2 m</p>
<p>Width of the embankment = 4 m</p>
<p> outer radius r<sub>2</sub> as 4 + (3/2) = 11/2 m and inner radius r<sub>1</sub> as 3/2 m</p>
<p>Now, let the height of embankment be h<sub>2</sub></p>
<p>∴ Volume of soil dug from well = Volume of earth used to form embankment</p>
<p>π×r<sub>1</sub><sup>2</sup>×h = π×(r<sub>2</sub><sup>2</sup>-r<sub>1</sub><sup>2</sup>) × h<sub>2</sub></p>
<p> (22/7) x (3/2)<sup>2</sup> x 14 = (22/7) x [(11/2)<sup>2</sup> - (3/2)<sup>2</sup>] x h<sub>2</sub></p>
<p> h<sub>2</sub> = (7 x 99 x 4 / 22 x 112) = 9/8 =1.125 m</p>
<p>The height of the embankment (h<sub>2</sub>) as 1.125 m.</p>


<p><h6 class="question">Question 5</h6>  A container shaped like a right circular cylinder having diameter 12 cm and height 15 cm is full of ice cream. The ice cream is to be filled into cones of height 12 cm and diameter 6 cm, having a hemispherical shape on the top. Find the number of such cones which can be filled with ice cream.</strong></p>
<h6 class="answer">Answer 5</h6>

<p>For the cylinder </p>
<p>Radius = 12/2 = 6 cm</p>
<p>Height = 15 cm</p>
<p>∴ Volume of cylinder = π×r<sup>2</sup>×h = 540π</p>



<p>For the ice cone</p>
<p>Radius r = 6/2 = 3 cm</p>
<p>Height  h = 12 cm</p>
<p>Radius of hemispherical = 6/2 = 3 cm</p>
<p>Now,</p>
<p>Volume of ice cream cone = Volume of conical  + Volume of hemispherical </p>
<p>= (1/3)× π × r<sup>2</sup> × h+(2/3)× π × r<sup>3</sup></p>
<p>= 36π +18π</p>
<p>= 54π</p>
<p>let n  be the number of cone filed with ic-cream<br> Volume of cylinder = n x Volume of ice cream cone</p>
<p>∴ n = (540π/54π)</p>
<p>n=10</p>

<p><h6 class="question">Question 6</h6> How many silver coins, 1.75 cm in diameter and of thickness 2 mm, must be melted to form a cuboid of dimensions 5.5 cm × 10 cm × 3.5 cm?</p>
<h6 class="answer"> Answer 6</h6>
<p>we  known that the coins are cylindrical in shape.</p>
<p>So, height h<sub>1</sub> of the cylinder = 2 mm = 0.2 cm</p>
<p>Radius  r of circular end of coins = 1.75/2 = 0.875 cm</p>
<p>Now,let n be  the number of coins to be melted to form the required cuboids/p>
<p>Volume of n coins = Volume of cuboids</p>
<p>n × π × r<sup>2 </sup>× h<sub>1</sub> = l × b × h</p>
<p>n×π×(0.875)<sup>2</sup>×0.2 = 5.5×10×3.5</p>
<p>Or, n = 400</p>


<p><h6 class="question">Question 7</h6> A cylindrical bucket, 32 cm high and with radius of base 18 cm, is filled with sand. This bucket is emptied on the ground and a conical heap of sand is formed. If the height of the conical heap is 24 cm, find the radius and slant height of the heap.</strong></p>
<h6 class="answer">Answer 7</h6>

<p>Given,</p>
<p>Height h<sub>1</sub> of  the bucket = 32 cm</p>
<p>Radius r<sub>1</sub> of  the bucket = 18 cm</p>
<p>Height of the conical heap h<sub>2</sub> = 24 cm</p>
<p>Now, let r be the radius  of heap of sand .</p>
<p>According to question </p>
<p>∴ Volume of sand in the cylindrical bucket = Volume of sand in conical heap</p>
<p>π×r<sub>1</sub><sup>2</sup>×h<sub>1</sub> = (1/3)×π×r<sup>2</sup>×h<sub>2</sub></p>
<p>π×18<sup>2</sup>×32 = (⅓)×π × r<sup>2</sup>×24</p>
<p>Or, r = 36 cm</p>
<p>And,</p>
<p>Slant height l = √(36<sup>2</sup>+24<sup>2</sup>) = 12√13 cm.</p>

