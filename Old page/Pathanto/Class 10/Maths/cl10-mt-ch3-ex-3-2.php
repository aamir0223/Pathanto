(a<sub>1</sub>/a<sub>2</sub>) = 3/2
<br>
(b<sub>1</sub>/b<sub>2</sub>) = 2/-3
<br>
(c<sub>1</sub>/c<sub>2</sub>) = -5/-7 = 5/7
<br>
Since, (a<sub>1</sub>/a<sub>2</sub>) ≠ (b<sub>1</sub>/b<sub>2</sub>)
<br>
So, the given equations intersect each other at one point and have only one possible solution. The equations are consistent.
<br><br>
(ii) Given 2x – 3y = 8 and 4x – 6y = 9
<br>
Therefore,
<br>
a<sub>1</sub> = 2, b<sub>1</sub> = -3, c<sub>1</sub> = -8
