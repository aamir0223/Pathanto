<br>
(i) 5x – 4y + 8 = 0
<br>
7x + 6y – 9 = 0
<br>
(ii) 9x + 3y + 12 = 0
<br>
18x + 6y + 24 = 0
<br>
(iii) 6x – 3y + 10 = 0
<br>
2x – y + 9 = 0
<br>
<h6 class="answer">Answer</h6>

(i) Given expressions;
<br>
5x−4y+8 = 0
<br>
7x+6y−9 = 0
<br>
Comparing these equations with a<sub>1</sub>x+b<sub>1</sub>y+c<sub>1</sub> = 0
<br>
And a<sub>2</sub>x+b<sub>2</sub>y+c<sub>2</sub> = 0
<br>
We get,
<br>
a<sub>1</sub> = 5, b<sub>1</sub> = -4, c<sub>1</sub> = 8
<br>
a<sub>2</sub> = 7, b<sub>2</sub> = 6, c<sub>2</sub> = -9
<br>
(a<sub>1</sub>/a<sub>2</sub>) = 5/7
<br>
(b<sub>1</sub>/b<sub>2</sub>) = -4/6 = -2/3
<br>
(c<sub>1</sub>/c<sub>2</sub>) = 8/-9
<br>
Since, (a<sub>1</sub>/a<sub>2</sub>) ≠ (b<sub>1</sub>/b<sub>2</sub>)
<br>
So, the pairs of equations have a unique solution<br> <br>
(ii) Given expressions;<br>

9x + 3y + 12 = 0
<br>
18x + 6y + 24 = 0
<br>
Comparing these equations with a<sub>1</sub>x+b<sub>1</sub>y+c<sub>1</sub> = 0
<br>
And a<sub>2</sub>x+b<sub>2</sub>y+c<sub>2</sub> = 0
<br>
We get,
<br>
a<sub>1</sub> = 9, b<sub>1</sub> = 3, c<sub>1</sub> = 12
<br>
a<sub>2</sub> = 18, b<sub>2</sub> = 6, c<sub>2</sub> = 24
<br>
(a<sub>1</sub>/a<sub>2</sub>) = 9/18 = 1/2
<br>
(b<sub>1</sub>/b<sub>2</sub>) = 3/6 = 1/2
<br>
(c<sub>1</sub>/c<sub>2</sub>) = 12/24 = 1/2
<br>
Since (a<sub>1</sub>/a<sub>2</sub>) = (b<sub>1</sub>/b<sub>2</sub>) = (c<sub>1</sub>/c<sub>2</sub>)
<br>
So, the pairs of equations have infinite possible solutions and the lines are coincident.
<br><br>
(iii) Given Expressions;
<br>
6x – 3y + 10 = 0
<br>
2x – y + 9 = 0
<br>
Comparing these equations with a<sub>1</sub>x+b<sub>1</sub>y+c<sub>1</sub> = 0
<br>
And a<sub>2</sub>x+b<sub>2</sub>y+c<sub>2</sub> = 0
<br>
<br>
