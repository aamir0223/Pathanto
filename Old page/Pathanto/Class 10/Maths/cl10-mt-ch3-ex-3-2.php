<br>
(b<sub>1</sub>/b<sub>2</sub>) = -3/-1 = 3/1
<br>
(c<sub>1</sub>/c<sub>2</sub>) = 10/9
<br>

Since (a<sub>1</sub>/a<sub>2</sub>) = (b<sub>1</sub>/b<sub>2</sub>) ≠ (c<sub>1</sub>/c<sub>2</sub>)
<br>
So, the pairs of equations are parallel to each other so  there is no possible solution for the given pair of equations.
