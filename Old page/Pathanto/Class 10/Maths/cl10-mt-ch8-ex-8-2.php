<p>4. State whether the following are true or false. Justify your answer.
</p>
<p>(i) sin (A + B) = sin A + sin B.
</p>
<p>(ii) The value of sin θ increases as θ increases.
</p>
<p>(iii) The value of cos θ increases as θ increases.
</p>
<p>(iv) sin θ = cos θ for all values of θ.
</p>
<p>(v) cot A is not defined for A = 0°.
</p>
<p>Solution:</p>
<p>(i) False.</p>
<p>Justify:<br><br>Let us take A = 30° and B = 60°, then</p>
<p>Substitute the values in the sin (A + B) formula, we get<br><br>sin (A + B) = sin (30° + 60°) = sin 90° = 1 and,<br><br>sin A + sin B = sin 30° + sin 60°</p>
<p>= 1/2 + √3/2 = 1+√3/2</p>
<p>Since the values obtained are not equal, the solution is false.</p>
<p>(ii) True.</p>
<p>Justify:</p>
<p>As we know </p>
<p>sin 0° = 0,
sin 30° = 1/2, 
sin 45° = 1/√2,
sin 60° = √3/2
<p>sin 90° = 1</p>
<p>As we saw on  θ increases  sin θ is also increases as. So the statement is true</p>
<p>
	<br>(iii) False.</p>
<p>As we know <br>
cos 0° = 1,
cos 30° = √3/2,
cos 45° = 1/√2,
cos 60° = 1/2,
cos 90° = 0</p>
<p>Thus, the value of cos θ decreases as θ increases. So, the statement is false.</p>

<p>(iv) False</p>
<p>sin θ = cos θ,let θ=30° <br> So sin 30° = 1/2,  cos 30° = √3/2, that are not equal   </p>

<p>(v) True.</p>

<p>cot A = cos A/sin A</p>
<p>here  A = 0°</p>
<p>cot 0° = cos 0°/sin 0° = 1/0 = undefined.</p>
<p>Hence, it is true</p>
