<h6 class="question">Question 4</h6> Find the area of the quadrilateral whose vertices, taken in order, are

(-4, -2), (-3, -5), (3, -2) and (2, 3).<br>

<h6 class="answer">Answer</h6>

<img class="Q2ch3-5n" src="https://pathanto.com/Pathanto/image/7-3-4.jpeg" alt="NCERT Solution Ex-7.3 class 10"><br>.

Let the vertices of the quadrilateral be A (- 4, – 2), B ( – 3, – 5), C (3, – 2), and D (2, 3). Join AC and divide quadrilateral into two triangles . ΔABC and ΔACD<br>





Area of a triangle =<div class="frac">
    <span> 1</span>
    <span class="symbol">/</span>
    <span class="bottom"> 2</span>
             
 </div>[x<sub>1</sub> (y<sub>2</sub> - y<sub>3</sub> ) + x<sub>2</sub>( y<sub>3</sub> - y<sub>1</sub>) + x<sub>3</sub>(y<sub>1</sub> - y<sub>2</sub>)]
<br>


Area of ΔABC = <div class="frac">
    <span> 1</span>
    <span class="symbol">/</span>
    <span class="bottom"> 2</span>
             
 </div> [(-4) {(-5) – (-2)} + (-3) {(-2) – (-2)} + 3 {(-2) – (-5)}] <br>

= <div class="frac">
    <span> 1</span>
    <span class="symbol">/</span>
    <span class="bottom"> 2</span>
             
 </div> (12 + 0 + 9) <br>

= 21/2 square units<br>

Area of ΔACD = <div class="frac">
    <span> 1</span>
    <span class="symbol">/</span>
    <span class="bottom"> 2</span>
             
 </div> [(-4) {(-2) – (3)} + 3{(3) – (-2)} + 2 {(-2) – (-2)}]<br>

=<div class="frac">
    <span> 1</span>
    <span class="symbol">/</span>
