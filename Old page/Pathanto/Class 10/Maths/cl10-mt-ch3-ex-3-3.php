<p><h6 class="question">Question 3</h6> Form the pair of linear equations for the following problems and find their solution by substitution method.</p>
<p>(i) The difference between two numbers is 26 and one number is three times the other. Find them.</p>
<p>(ii) The larger of two supplementary angles exceeds the smaller by 18 degrees. Find them.</p>
<p>(iii) The coach of a cricket team buys 7 bats and 6 balls for Rs.3800. Later, she buys 3 bats and 5 balls for Rs.1750. Find the cost of each bat and each ball.</p>
<p>(iv) The taxi charges in a city consist of a fixed charge together with the charge for the distance covered. For a distance of 10 km, the charge paid is Rs 105 and for a journey of 15 km, the charge paid is Rs 155. What are the fixed charges and the charge per km? How much does a person have to pay for travelling a distance of 25 km?</p>
<p>(v) A fraction becomes 9/11 , if 2 is added to both the numerator and the denominator. If, 3 is added to both the numerator and the denominator it becomes 5/6. Find the fraction.</p>
<p>(vi) Five years hence, the age of Jacob will be three times that of his son. Five years ago, Jacob’s age was seven times that of his son. What are their present ages?</p>


<h6 class="answer">Answer  (i)</h6>
<p>Let the two numbers be x and y respectively, such that y &gt; x.</p>
<p>According to the question,</p>
<p>y = 3x ……………… (1)</p>
<p>y – x = 26 …………..(2)</p>
<p>Substituting the value of (1) into (2), we get</p>
<p>3x – x = 26</p>
<p>x = 13 ……………. (3)</p>
<p>Substituting (3) in (1), we get y = 39</p>
<p> the numbers are 13 and 39.</p>

<h6 class="answer">Answer  (ii)</h6>
<p>Let the larger angle by x and smaller angle be y.</p>
<p> the sum of two supplementary pair of angles is  180<sup>o</sup>.</p>
<p>According to the question,</p>
<p>x + y = 180<sup>o</sup>……………. (1)</p>
<p>x – y = 18<sup>o </sup>……………..(2)</p>
<p>From (1), we get x = 180<sup>o</sup> – y …………. (3)</p>
<p>Substituting (3) in (2), we get</p>
<p>180<sup>o </sup>– y – y =18<sup>o</sup></p>
<p>162<sup>o</sup> = 2y</p>
<p>y = 81<sup>o</sup> ………….. (4)</p>
<p>Using the value of y in (3), we get</p>
<p>x = 180<sup>o</sup> – 81<sup>o</sup></p>
<p>= 99<sup>o</sup></p>
<p> the angles are 99<sup>o</sup> and 81<sup>o</sup>.</p>

<h6 class="answer">Answer  (iii)</h6>
<p>Let the cost a bat be x and cost of a ball be y.</p>
<p>According to the question,</p>
<p>7x + 6y = 3800 ………………. (I)</p>
<p>3x + 5y = 1750 ………………. (II)</p>
<p>From (I), we get</p>
<p>y = (3800-7x)/6………………..(III)</p>
<p>Substituting (III) in (II). we get,</p>
<p>3x+5(3800-7x)/6 =1750</p>
<p>3x+ 9500/3 – 35x/6 = 1750</p>
<p>3x- 35x/6 = 1750 – 9500/3</p>
<p>(18x-35x)/6 = (5250 – 9500)/3</p>
<p>-17x/6 = -4250/3</p>
<p>-17x = -8500</p>
<p>x = 500 ……………………….. (IV)</p>
<p>Substituting the value of x in (III), we get</p>
<p>y = (3800-7 ×500)/6 = 300/6 = 50</p>
<p> the cost of a bat is Rs 500 and cost of a ball is Rs 50.</p>

<h6 class="answer">Answer  (iv)</h6>
<p>Let the fixed charge be Rs x and per km charge be Rs y.</p>
<p>According to the question,</p>
<p>x + 10y = 105 …………….. (1)</p>
<p>x + 15y = 155 …………….. (2)</p>
<p>From (1), we get x = 105 – 10y ………………. (3)</p>
<p>Substituting the value of x in (2), we get</p>
<p>105 – 10y + 15y = 155</p>
<p>5y = 50</p>
<p>y = 10 …………….. (4)</p>
<p>Putting the value of y in (3), we get</p>
<p>x = 105 – 10 × 10 = 5</p>
<p>Hence, fixed charge is Rs 5 and per km charge = Rs 10</p>
<p>Charge for 25 km = x + 25y = 5 + 250 = Rs 255</p>

<h6 class="answer">Answer  (v)</h6>
<p>Let the fraction be x/y.</p>
<p>According to the question,</p>
<p>(x+2) /(y+2) = 9/11</p>
<p>11x + 22 = 9y + 18</p>
<p>11x – 9y = -4 …………….. (1)</p>
<p>(x+3) /(y+3) = 5/6</p>
<p>6x + 18 = 5y +15</p>
<p>6x – 5y = -3 ………………. (2)</p>
<p>From (1), we get x = (-4+9y)/11 …………….. (3)</p>
<p>Substituting the value of x in (2), we get</p>
<p>6(-4+9y)/11 -5y = -3</p>
<p>-24 + 54y – 55y = -33</p>
<p>-y = -9</p>
<p>y = 9 ………………… (4)</p>
<p>Substituting the value of y in (3), we get</p>
<p>x = (-4+9×9 )/11 = 7</p>
<p> the fraction is 7/9.</p>

<h6 class="answer">Answer  (vi)</h6>
<p>Let the age of Jacob and his son be x and y respectively.</p>
<p>According to the question,</p>
<p>(x + 5) = 3(y + 5)</p>
<p>x – 3y = 10 …………………………………….. (1)</p>
<p>(x – 5) = 7(y – 5)</p>
<p>x – 7y = -30 ………………………………………. (2)</p>
<p>From (1), we get x = 3y + 10 ……………………. (3)</p>
<p>Substituting the value of x in (2), we get</p>
<p>3y + 10 – 7y = -30</p>
<p>-4y = -40</p>
<p>y = 10 ………………… (4)</p>
<p>Substituting the value of y in (3), we get</p>
<p>x = 3 x 10 + 10 = 40</p>
<p> the present age of Jacob and his son is 40 years and 10 years, respectively.</p>
