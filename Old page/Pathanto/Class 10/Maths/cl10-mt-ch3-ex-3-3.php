<p><h6 class="question">Question 1</h6> Solve the following pair of linear equations by the substitution method</p>
<p>(i) x + y = 14</p>
<p> x – y = 4</p>
<p>(ii) s – t = 3</p>
<p> (s/3) + (t/2) = 6</p>
<p> (iii) 3x – y = 3</p>
<p> 9x – 3y = 9</p>
<p>(iv) 0.2x + 0.3y = 1.3 </p>
<p> 0.4x + 0.5y = 2.3</p>
<p>(v) √2 x+√3 y = 0 </p>
<p> √3 x-√8 y = 0</p>
<p> (vi) (3x/2) – (5y/3) = -2 </p>
<p> (x/3) + (y/2) = (13/6)</p>

<p><h6 class="answer">Answer  (i)</h6> </p>
<p>x + y = 14 and x – y = 4 are the two equations.</p>
<p>From 1<sup>st</sup> equation</p>
<p>x = 14 – y</p>
<p> substitute the value of x in second equation to get,</p>
<p>(14 – y) – y = 4</p>
<p>14 – 2y = 4</p>
<p>2y = 10</p>
<p>Or y = 5</p>
<p>Putting value of x</p>
<p>∵ x = 14 – y</p>
<p>∴ x = 14 – 5</p>
<p>Or x = 9</p>
<p>Hence, x = 9 and y = 5.</p>
<p><h6 class="answer">Answer  (ii)</h6> Given,</p>
<p>s – t = 3 and (s/3) + (t/2) = 6 are the two equations.</p>
<p>From 1<sup>st</sup> equation, </p>
<p>s = 3 + t ________________(1)</p>
<p> substitute the value of s in second equation to get,</p>
<p>(3+t)/3 + (t/2) = 6</p>
<p> (2(3+t) + 3t )/6 = 6</p>
<p> (6+2t+3t)/6 = 6</p>
<p> (6+5t) = 36</p>
<p>5t = 30</p>
<p>t = 6</p>
<p>Now, substitute the value of t in equation (1)</p>
<p>s = 3 + 6 = 9</p>
<p>Therefore, s = 9 and t = 6.</p>
<p><h6 class="answer">Answer  (iii)</h6> Given,</p>
<p>3x – y = 3 and 9x – 3y = 9 are the two equations.</p>
<p>From 1<sup>st</sup> equation,</p>
<p>x = (3+y)/3</p>
<p>Now, substitute the value of x in the given second equation to get,</p>
<p>9(3+y)/3 – 3y = 9</p>
<p>9 +3y -3y = 9</p>
<p> 9 = 9</p>
<p> y has infinite values and  x = (3+y) /3, so x also has infinite values.</p>
<p><h6 class="answer">Answer  (iv)</h6>Given,</p>
<p>0.2x + 0.3y = 1.3 and 0.4x + 0.5y = 2.3are the two equations.</p>
<p>From 1<sup>st</sup> equation, </p>
<p>x = (1.3- 0.3y)/0.2 _________________(1)</p>
<p>Now, substitute the value of x in the given second equation to get,</p>
<p>0.4(1.3-0.3y)/0.2 + 0.5y = 2.3</p>
<p>2(1.3 – 0.3y) + 0.5y = 2.3</p>
<p> 2.6 – 0.6y + 0.5y = 2.3</p>
<p> 2.6 – 0.1 y = 2.3</p>
<p> 0.1 y = 0.3</p>
<p> y = 3</p>
<p>Now, substitute the value of y in equation (1), </p>
<p>x = (1.3-0.3(3))/0.2 = (1.3-0.9)/0.2 = 0.4/0.2 = 2</p>
<p>Therefore, x = 2 and y = 3.</p>
<p><h6 class="answer">Answer  (v)</h6> Given,</p>
<p>√2 x + √3 y = 0 and √3 x – √8 y = 0</p>

<p>From 1<sup>st</sup> equation, we get,</p>
<p>x = – (√3/√2)y __________________(1)</p>
<p>Putting the value of x in the given second equation to get,</p>
<p>√3(-√3/√2)y – √8y = 0 ⇒ (-3/√2)y- √8 y = 0</p>
<p> y = 0</p>
<p>Now, substitute the value of y in equation (1), we get,</p>
<p>x = 0</p>
<p>Therefore, x = 0 and y = 0.</p>
