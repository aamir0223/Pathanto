<p><h6 class="question">Question 2</h6> Solve 2x + 3y = 11 and 2x – 4y = – 24 and hence find the value of ‘m’ for which y = mx + 3.</p>
<h6 class="answer">Answer  </h6>
<p>2x + 3y = 11…………………………..(I)</p>
<p>2x – 4y = -24………………………… (II)</p>
<p>From equation (II), we get</p>
<p>x = (11-3y)/2 ………………….(III)</p>
<p>Substituting the value of x in equation (II), we get</p>
<p>2(11-3y)/2 – 4y = 24</p>
<p>11 – 3y – 4y = -24</p>
<p>-7y = -35</p>
<p>y = 5……………………………………..(IV)</p>
<p>Putting the value of y in equation (III), we get</p>
<p>x = (11-3×5)/2 = -4/2 = -2</p>
<p>Hence, x = -2, y = 5</p>
<p>Also,</p>
<p>y = mx + 3</p>
<p>5 = -2m +3</p>
<p>-2m = 2</p>
<p>m = -1</p>
<p>Therefore the value of m is -1.</p>
