<h4 class="question">Question 5</h4>
<p> Give examples of polynomials p(x), g(x), q(x) and r(x), which satisfy the division algorithm and </p>
<p>(i) deg p(x) = deg q(x) </p>
<p>(ii) deg q(x) = deg r(x)</p>
<p>(iii) deg r(x) = 0</p>
<h4 class="answer">Answer</h4>

<p>(i) deg p(x) = deg q(x) </p>
<p> We know <br>Dividend = Divisor × Quotient + Remainder</p>
<p>∴ p(x) = g(x)×q(x)+r(x)</p>
<p>Where r(x) = 0 or degree of r(x)&lt; degree of g(x).</p>


<p>(i) deg p(x) = deg q(x)</p>
<h4 class="answer">Answer</h4>
<p>Degree of dividend is equal to degree of quotient, only when the divisor is a constant term.</p>
<p>Let us assume  an example, 6x<sup>2</sup>+6x+6 is a polynomial to be divided by 3.</p>
p(x)= 6x<sup>2</sup>+6x+6<br>
g(x)= 3<br>
<p>So,  (6x<sup>2</sup>+6x+6)/3 = 2x<sup>2</sup>+2x+2 = q(x)</p>

<p>so we can see, the degree  of p(x) and q(x) is equal.</p>
<p>Hence, division algorithm is satisfied here.</p>


<p>(ii) deg q(x) = deg r(x)</p>
<h4 class="answer">Answer</h4>
<p>Let us  p(x)=x<sup>4</sup>+x <br> g(x)=x<sup>3</sup>.</p>
<p>So, (x<sup>4</sup>+x)/x<sup>3</sup> = x = q(x)</p>
<p>Also, remainder, r(x) = x</p>
<p>we  can see, the degree of q(x) and g(x) is equal .</p>
<p>Hence, division algorithm is satisfied here.</p>

<p>(iii) deg r(x) = 0</p>
<h4 class="answer">Answer</h4>
<p>Let us take an example, p(x) = x<sup>3</sup>+1 is a polynomial to be divided by g(x)=x.</p>
<p>So,( x<sup>3</sup>+1)/x= x=q(x)</p>
<p>And r(x)=2</p>
<p>Clearly, the degree of remainder r(x) is 0.</p>
<p>Hence, division algorithm is satisfied here.</p>
