<h4 class="question">Question 2</h4>
 Construct a triangle of sides 4 cm, 5 cm and 6 cm and then a triangle similar to it whose sides are 2/3 of

the corresponding sides of the first triangle.
<h4 class="answer">Answer</h4>
<p>Step 1: Draw a line  AB of 4 cm </p>

<p>Step 2: Using a point A  draw an arc of radius 5 cm</p>

<p>Step 3: Similarly, Using a point   B as its centre, and draw an arc of radius 6 cm.</p>

<p>Step 4: Now join C to A and B and form  ΔABC  triangle.</p>

<p>Step 5:Draw a line AX with angle BAX  </p>

<p>Step 6: Make 3 points such as A<sub>1</sub>, A<sub>2</sub>, A<sub>3</sub>  on line AX such that AA1= A1A2 = A2A3.</p>

<p>Step 8: Join the  BA<sub>3</sub> through A<sub>2</sub> which is parallel to the line BA<sub>3</sub> meeting  AB at point B’.</p>

<p>Step 9: Using point B’, draw a line parallel to the line BC meeting AC at C’.</p>
<p> Justification:<br>

Here<br>
AB’ = (2/3)AB
<br>
B’C’ = (2/3)BC
<br>
AC’= (2/3)AC
<br>
By construction  we get B’C’ || BC
<br>
∴ ∠AB’C’ = ∠ABC (Corresponding angles)
<br>
In ΔAB’C’ and ΔABC,
<br>
∠ABC = ∠AB’C (Proved above)
<br>
∠BAC = ∠B’AC’ (Common)
<br>
∴ ΔAB’C’ ∼ ΔABC (by AA similarity )
<br>
So AB’/AB = B’C’/BC= AC’/AC …. (1)
<br>
In ΔAAB’ and ΔAAB,
<br>
∠A<sub>2</sub>AB’ =∠A<sub>3</sub>AB (Common)
<br>
∠AA<sub>2</sub>B’ =∠AA<sub>3</sub>B (Corresponding angles)
<br>
∴ ΔAAB’ ∼ ΔAAB (by AA similarity )
<br>
AA<sub>2</sub>B’ and AA<sub>3</sub>B
<br>
So, AB’/AB = AA<sub>2</sub>/AA<sub>3</sub>
<br>
 AB’/AB = 2/3 ……. (2)
<br>
From the eq (1) and (2)
<br>
AB’/AB=B’C’/BC = AC’/ AC = 2/3
<br>

AB’ = (2/3)AB
<br>
B’C’ = (2/3)BC
<br>
AC’= (2/3)AC
</p>
