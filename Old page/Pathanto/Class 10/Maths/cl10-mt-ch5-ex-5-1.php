<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = √8-√2  = 2√2-√2 = √2</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = √18-√8 = 3√2-2√2 = √2</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = 4√2-3√2 = √2</p>
