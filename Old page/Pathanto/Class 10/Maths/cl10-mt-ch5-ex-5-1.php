<p><h6 class="question ">Question 1</h6>
 In which of the following situations, does the list of numbers involved make as arithmetic progression and why?</p>
<p>(i) The taxi fare after each km when the fare is Rs 15 for the first km and Rs 8 for each additional km.</p>
<p><h6 class="answer">Answer 1</h6></p>
<p>According to question </p>
<p>the fare for journey of first  1 km = Rs 15</p>
<p>Taxi fare for next 2 kms = 15+8 = 23</p>
<p>Taxi fare for next 3 kms = 23+8 = 31</p>
<p>Taxi fare for next 4 kms = 31+8 = 39</p>
<p> 15, 23, 31, 39 ......<br> Here common difference is 8 , So, it is an A P</p>
<p>(ii) The amount of air present in a cylinder when a vacuum pump removes 1/4 of the air remaining in the cylinder at a time</p>
<p><h6 class="answer">Answer 1</h6></p>
<p>Let the volume of air in a cylinder,  is <em>V</em>  litres.</p>
<p>The vacuum pump removes 1/4th of air remaining in the cylinder at a time. <br>So<br> 1-1/4 = 3/4th part of air will remain.</p>
<p>So, volumes will be <em>V</em>, 3<em>V</em>/4 , (3<em>V</em>/4)<sup>2</sup> , (3<em>V</em>/4)<sup>3</sup>........</p>
<p>   d=   3y/4 - y = - y/4 <br> d= 9y/4 - 3y/4 = -3y/4 </p>
<p>Since common difference between them is not same . Therefore, this series is not an A.P.</p>
<p>(iii) The cost of digging a well after every metre of digging, when it costs Rs 150 for the first metre and rises by Rs 50 for each subsequent metre.</p>
<p><h6 class="answer">Answer 1</h6></p>
<p>According to question </p>
<p>The Cost of digging a well for first metre = Rs.150</p>
<p>Cost of digging a well for Second  metres = Rs.150+50 = Rs.200</p>
<p>Cost of digging a well for third metres = Rs.200+50 = Rs.250</p>
<p>Cost of digging a well for fourth metres =Rs.250+50 = Rs.300</p>
<p>And so on..</p>
<p>hence 150, 200, 250, 300 … forms an A.P. with a common difference of 50 between each term.</p>
<p>(iv) The amount of money in the account every year, when Rs 10000 is deposited at compound interest at 8% per annum.<br></p>
<p><h6 class="answer">Answer 1</h6></p>
<p>We know that if Rs. P is deposited at <em>r</em>% compound interest per annum for n years, the amount of money will be:</p>
<p>P(1+r/100)<sup>n</sup></p>
<p>So, the amount of money in the account in the first years ,second year third year and so on....;</p>
<p>10000(1+8/100), 10000(1+8/100)<sup>2</sup>, 10000(1+8/100)<sup>3</sup>……</p>
<p>So the terms of this series do not have the common difference between them. Therefore, this is not an A.P.</p>


<p><h6 class="question ">Question 2</h6>
 Write first four terms of the A.P. when the first term a and the common difference are given as follows:</p>
<p>(i) <em>a</em> = 10, <em>d</em> = 10<br>
(ii) <em>a</em> = -2, <em>d</em> = 0<br>
(iii) <em>a</em> = 4, <em>d</em> = – 3<br>
(iv) <em>a</em> = -1 <em>d</em> = 1/2<br>
(v) <em>a</em> = – 1.25, <em>d</em> = – 0.25</p>
<p><h6 class="answer">Answer 2</h6></p>
<p>(i) <em>a</em> = 10, <em>d</em> = 10</p>
<p>Let  the Arithmetic Progression series be <em>a<sub>1</sub>, a<sub>2</sub>, a<sub>3</sub>, a<sub>4</sub>, a<sub>5</sub> …</em></p>
<p><em>a</em><sub>1</sub> = <em>a</em> = 10</p>
<p><em>a</em><sub>2</sub> = <em>a</em><sub>1</sub>+<em>d</em> = 10+10 = 20</p>
<p><em>a</em><sub>3</sub> = <em>a</em><sub>2</sub>+<em>d</em> = 20+10 = 30</p>
<p><em>a</em><sub>4</sub> = <em>a</em><sub>3</sub>+<em>d</em> = 30+10 = 40</p>
<p><em>a</em><sub>5</sub> = <em>a</em><sub>4</sub>+<em>d</em> = 40+10 = 50</p>
<p>And so on…</p>
<p>So the A.P. series will be 10, 20, 30, 40, 50 …</p>
<p>And First four terms of an A.P. will be 10, 20, 30, and 40.</p>
<p>(ii) <em>a</em> = – 2, <em>d</em> = 0</p>
<p>Let  the Arithmetic Progression series be <em>a<sub>1</sub>, a<sub>2</sub>, a<sub>3</sub>, a<sub>4</sub>, a<sub>5</sub> …</em></p>
<p><em>a</em><sub>1</sub> = <em>a</em> = -2</p>
<p><em>a</em><sub>2</sub> = <em>a</em><sub>1</sub>+<em>d</em> = – 2+0 = – 2</p>
<p><em>a</em><sub>3</sub> = <em>a</em><sub>2</sub>+d = – 2+0 = – 2</p>
<p><em>a</em><sub>4</sub> = <em>a</em><sub>3</sub>+<em>d</em> = – 2+0 = – 2</p>
<p>Sothe A.P. series will be – 2, – 2, – 2, – 2 …</p>
<p>And, First four terms of an A.P. will be – 2, – 2, – 2 and – 2.</p>
<p>(iii) <em>a</em> = 4, <em>d</em> = – 3</p>
<p>Let the Arithmetic Progression series be <em>a<sub>1</sub>, a<sub>2</sub>, a<sub>3</sub>, a<sub>4</sub>, a<sub>5</sub> …</em></p>
<p><em>a</em><sub>1</sub> = <em>a</em> = 4</p>
<p><em>a</em><sub>2</sub> = <em>a</em><sub>1</sub>+<em>d</em> = 4-3 = 1</p>
<p><em>a</em><sub>3</sub> = <em>a</em><sub>2</sub>+<em>d</em> = 1-3 = – 2</p>
<p><em>a</em><sub>4</sub> = <em>a</em><sub>3</sub>+<em>d</em> = -2-3 = – 5</p>
<p>So the A.P. series will be 4, 1, – 2 – 5 …</p>
<p>And, first four terms of an A.P. will be 4, 1, – 2 and – 5.</p>
<p>(iv) <em>a</em> = – 1, <em>d</em> = 1/2</p>
<p>Let  the Arithmetic Progression series be <em>a<sub>1</sub>, a<sub>2</sub>, a<sub>3</sub>, a<sub>4</sub>, a<sub>5</sub> …</em></p>
<p><em>a</em><sub>2</sub> = <em>a</em><sub>1</sub>+<em>d</em> = -1+1/2 = -1/2</p>
<p><em>a</em><sub>3</sub> = <em>a</em><sub>2</sub>+<em>d</em> = -1/2+1/2 = 0</p>
<p><em>a</em><sub>4</sub> = <em>a</em><sub>3</sub>+<em>d</em> = 0+1/2 = 1/2</p>
<p>So the A.P. series will be-1, -1/2, 0, 1/2</p>
<p>And First four terms of an A.P. will be -1, -1/2, 0 and 1/2.</p>
<p>(v) <em>a</em> = – 1.25, <em>d</em> = – 0.25</p>
<p>Let , the Arithmetic Progression series be <em>a<sub>1</sub>, a<sub>2</sub>, a<sub>3</sub>, a<sub>4</sub>, a<sub>5</sub> …</em></p>
<p><em>a</em><sub>1</sub> = <em>a</em> = – 1.25</p>
<p><em>a</em><sub>2</sub> = <em>a</em><sub>1</sub> + <em>d</em> = – 1.25-0.25 = – 1.50</p>
<p><em>a</em><sub>3</sub> = <em>a</em><sub>2</sub> + <em>d</em> = – 1.50-0.25 = – 1.75</p>
<p><em>a</em><sub>4</sub> = <em>a</em><sub>3</sub> + <em>d</em> = – 1.75-0.25 = – 2.00</p>
<p>So, the A.P series will be 1.25, – 1.50, – 1.75, – 2.00 ……..</p>
<p>And first four terms of an A.P. will be – 1.25, – 1.50, – 1.75 and – 2.00.</p>
