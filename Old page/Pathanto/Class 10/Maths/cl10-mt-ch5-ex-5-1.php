<p><h6 class="question">Question 3</h6> For the following A.P.s, write the first term and the common difference.<br>
(i) 3, 1, – 1, – 3 …<br>
(ii) -5, – 1, 3, 7 …<br>
(iii) 1/3, 5/3, 9/3, 13/3 ….<br>
(iv) 0.6, 1.7, 2.8, 3.9 …</p>
<p><h6 class="answer">Answer 3</h6></p>

<p>3, 1, – 1, – 3 …</p>
<p>First term, <em>a</em> = 3</p>
<p>Common difference, <em>d</em> = 2nd  term – 1st term</p>
<p>⇒  1 – 3 = -2</p>
<p>⇒  d = -2</p>
<p>(ii)given – 5, – 1, 3, 7 …</p>
<p>First term, <em>a</em> = -5</p>
<p>Common difference, <em>d</em> = 2nd  term – 1st term</p>
<p>⇒ ( – 1)-( – 5) = – 1+5 = 4</p>
<p>(iii) Given  1/3, 5/3, 9/3, 13/3 ….</p>
<p>First term, <em>a</em> = 1/3</p>
<p>Common difference, <em>d</em> = 2nd  term – 1st term</p>
<p>⇒ 5/3 – 1/3 = 4/3</p>
<p>(iv) Given  0.6, 1.7, 2.8, 3.9 …</p>
<p>First term, <em>a</em> = 0.6</p>
<p>Common difference, <em>d</em> = 2nd  term – 1st term</p>
<p>⇒ 1.7 – 0.6</p>
<p>⇒ 1.1</p>
<p><h6 class="question">Question 4</h6> 
 Which of the following are APs? If they form an A.P. find the common difference <em>d</em> and write three more terms.</p>
<p>(i) 2, 4, 8, 16 …<br>
(ii) 2, 5/2, 3, 7/2 ….<br>
(iii) -1.2, -3.2, -5.2, -7.2 …<br>
(iv) -10, – 6, – 2, 2 …<br>
(v) 3, 3 + √2, 3 + 2√2, 3 + 3√2<br>
(vi) 0.2, 0.22, 0.222, 0.2222 ….<br>
(vii) 0, – 4, – 8, – 12 …<br>
(viii) -1/2, -1/2, -1/2, -1/2 ….<br>
(ix) 1, 3, 9, 27 …<br>
(x) <em>a</em>, 2<em>a</em>, 3<em>a</em>, 4<em>a</em> …<br>
(xi) <em>a</em>, <em>a</em><sup>2</sup>, <em>a</em><sup>3</sup>, <em>a</em><sup>4</sup> …<br>
(xii) √2, √8, √18, √32 …<br>
(xiii) √3, √6, √9, √12 …<br>
(xiv) 1<sup>2</sup>, 3<sup>2</sup>, 5<sup>2</sup>, 7<sup>2</sup> …<br>
(xv) 1<sup>2</sup>, 5<sup>2</sup>, 7<sup>2</sup>, 7<sup>3</sup> …<br></p>
<p><h6 class="answer">Answer 4</h6></p>
<p>(i) here ,</p>
<p>2, 4, 8, 16 …</p>
<p>Here, the common difference is;</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = 4 – 2 = 2</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = 8 – 4 = 4</p>
<p><em>a<sub>4</sub></em> – <em>a</em><sub>3</sub> = 16 – 8 = 8</p>
<p>Since, common difference is not the same every time.</p>
<p>hence the given series are not forming an A.P.</p>
<p>(ii) Given, 2, 5/2, 3, 7/2 ….</p>
<p>Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = 5/2-2 = 1/2</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = 3-5/2 = 1/2</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = 7/2-3 = 1/2</p>
<p>here the common difference is same every time.</p>
<p>Since  <em>d</em> = 1/2 and the given series are in A.P.</p>
<p>The next three terms </p>
<p><em>a</em><sub>5</sub> = 7/2+1/2 = 4</p>
<p><em>a</em><sub>6</sub> = 4 +1/2 = 9/2</p>
<p><em>a</em><sub>7</sub> = 9/2 +1/2 = 5</p>
<p>(iii) Given, -1.2, – 3.2, -5.2, -7.2 …<br></p>
<p>Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = (-3.2)-(-1.2) = -2</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = (-5.2)-(-3.2) = -2</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = (-7.2)-(-5.2) = -2</p>
<p>here common difference is same every time.</p>
<p>Since , <em>d</em> = -2 and the given series are in A.P.</p>
<p>Hence, next three terms ;</p>
<p><em>a</em><sub>5</sub> = – 7.2-2 = -9.2</p>
<p><em>a</em><sub>6</sub> = – 9.2-2 = – 11.2</p>
<p><em>a</em><sub>7</sub> = – 11.2-2 = – 13.2</p>
<p>(iv) Given, -10, – 6, – 2, 2 …<br><br>
Here, the common difference are;</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = (-6)-(-10) = 4</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = (-2)-(-6) = 4</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = (2 -(-2) = 4</p>
<p> the common difference is same every time.</p>
<p>Therefore, <em>d</em> = 4 and the given numbers are in A.P.</p>
<p>Hence, next three terms are;</p>
<p><em>a</em><sub>5</sub> = 2+4 = 6</p>
<p><em>a</em><sub>6</sub> = 6+4 = 10</p>
<p><em>a</em><sub>7</sub> = 10+4 = 14</p>
<p>(v) Given, 3, 3+√2, 3+2√2, 3+3√2<br><br>
Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = 3+√2-3 = √2</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = (3+2√2)-(3+√2) = √2</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = (3+3√2) – (3+2√2) = √2</p>
<p>the common difference is same every time.</p>
<p>Therefore, <em>d</em> = √2 and the given series forms a A.P.</p>
<p>Hence, next three terms</p>
<p><em>a</em><sub>5</sub> = (3+√2) +√2 = 3+4√2</p>
<p><em>a</em><sub>6</sub> = (3+4√2)+√2 = 3+5√2</p>
<p><em>a</em><sub>7</sub> = (3+5√2)+√2 = 3+6√2</p>
<p>(vi) 0.2, 0.22, 0.222, 0.2222 ….<br><br>
Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = 0.22-0.2 = 0.02</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = 0.222-0.22 = 0.002</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = 0.2222-0.222 = 0.0002</p>
<p> the common difference is not same every time.
 and the given series doesn’t forms a A.P.</p>
<p>(vii) 0, -4, -8, -12 …<br><br>
Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = (-4)-0 = -4</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = (-8)-(-4) = -4</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = (-12)-(-8) = -4</p>
<p> the common difference is same every time.</p>
<p>So, <em>d</em> = -4 and the given series forms a A.P.</p>
<p>Hence, next three terms</p>
<p><em>a</em><sub>5</sub> = -12-4 = -16</p>
<p><em>a</em><sub>6</sub> = -16-4 = -20</p>
<p><em>a</em><sub>7</sub> = -20-4 = -24</p>
<p>(viii) -1/2, -1/2, -1/2, -1/2 ….<br><br>
Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = (-1/2) – (-1/2) = 0</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = (-1/2) – (-1/2) = 0</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = (-1/2) – (-1/2) = 0</p>
<p>the common difference is same every time.</p>
<p>Therefore, <em>d</em> = 0 and the given series forms a A.P.</p>
<p>Hence, next three terms </p>
<p><em>a</em><sub>5</sub> = (-1/2)-0 = -1/2</p>
<p><em>a</em><sub>6</sub> = (-1/2)-0 = -1/2</p>
<p><em>a</em><sub>7</sub> = (-1/2)-0 = -1/2</p>
<p>(ix) 1, 3, 9, 27 …<br><br>
Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = 3-1 = 2</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = 9-3 = 6</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = 27-9 = 18</p>
<p> the common difference is not same every time.</p>
<p>Therefore, and the given series doesn’t form a A.P.</p>
<p>(x) <em>a</em>, 2<em>a</em>, 3<em>a</em>, 4<em>a</em> …</p>
<p>Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = 2<em>a</em>–<em>a </em>= <em>a</em></p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = 3<em>a</em>-2<em>a</em> = <em>a</em></p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = 4<em>a</em>-3<em>a</em> = <em>a</em></p>
<p>the common difference is same every time.</p>
<p>Therefore, <em>d</em> = <em>a</em> and the given series forms a A.P.</p>
<p>Hence, next three terms </p>
<p><em>a</em><sub>5</sub> = 4<em>a</em>+<em>a</em> = 5<em>a</em></p>
<p><em>a</em><sub>6</sub> = 5<em>a</em>+<em>a</em> = 6<em>a</em></p>
<p><em>a</em><sub>7</sub> = 6<em>a</em>+<em>a</em> = 7<em>a</em></p>
<p>(xi) <em>a</em>, <em>a</em><sup>2</sup>, <em>a</em><sup>3</sup>, <em>a</em><sup>4</sup> …</p>
<p>Here,</p>
<p><em>a</em><sub>2</sub> – <em>a</em><sub>1</sub> = <em>a</em><sup>2</sup>–<em>a</em> = a(<em>a</em>-1)</p>
<p><em>a</em><sub>3</sub> – <em>a</em><sub>2</sub> = <em>a</em><sup>3 </sup>–<sup> </sup><em>a</em><sup>2 </sup>= <em>a</em><sup>2</sup>(<em>a</em>-1)</p>
<p><em>a</em><sub>4</sub> – <em>a</em><sub>3</sub> = <em>a</em><sup>4</sup> – <em>a</em><sup>3 </sup>= <em>a</em><sup>3</sup>(<em>a</em>-1)</p>
<p> the common difference is not same every time.</p>
<p>Therefore, the given series doesn’t forms a A.P.</p>
