<p>Step 2:Make point outside of circle say 'A' such that AO is 10cm </p>
<p>Step 3:Make a perpendicular bisector of AB .P is mid point of AB</p>
<p>Step 4:P as center ,Draw a circle such that it intersect the previous circle at point B and C</p>
