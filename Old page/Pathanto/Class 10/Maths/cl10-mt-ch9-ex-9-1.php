<p><h6 class="question">Question 2</h6> A tree breaks due to storm and the broken part bends so that the top of the tree touches the ground making an angle 30° with it. The distance between the foot of the tree to the point where the top touches the ground is 8 m. Find the height of the tree.
</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-2.png" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>
<p> Let's the original height of tree be AC   due to storm  tree broke from point .the broken part of tree is BC  touch at ground at D making  a angle 30° </p>
<p>BC = 8 m</p>
	
<p>From figure: Total height of the tree is the sum of AB and AC i.e. AB+AC</p>
<p>In right ΔABC,</p>

<p>cos30° = BC/AC</p>
<p>Here, cos 30° = √3/2</p>
<p>√3/2 = 8/AC</p>
<p>AC = 16/√3 …(1)</p>
<p>Also,</p>
<p>tan 30° = AB/BC</p>
<p>1/√3 = AB/8</p>
<p>AB = 8/√3 ….(2)</p>
<p>Therefore, total height of the tree = AB + AC = 16/√3 + 8/√3 = 24/√3 = 8√3 m.</p>

