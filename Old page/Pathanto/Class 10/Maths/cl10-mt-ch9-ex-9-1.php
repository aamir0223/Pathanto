<p><h6 class="question">Question 14</h6> A 1.2 m tall girl spots a balloon moving with the wind in a horizontal line at a height of 88.2 m from the ground. The angle of elevation of the balloon from the eyes of the girl at any instant is 60°. After some time, the angle of elevation reduces to 30° (see Fig. 9.13). Find the distance travelled by the balloon during the interval.</p>
<p>
    <img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-14.jpeg" alt="NCERT Ex-9.1 class 10"><br>
	
<h6 class="answer">Answer</h6>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-15.jpg" alt="NCERT Ex-9.1 class 10"><br>
<p>Let the initial position of the balloon be A and final position be B.</p>
<p>Height of balloon above the girl height = 88.2 m – 1.2 m = 87 m.</p>
<p>To Find: Distance travelled by the balloon = DE = CE – CD</p>
<p>Let us redesign the given figure as per our convenient</p>

<p>tan 30° = BE/CE</p>
<p>1/√3= 87/CE</p>
<p>CE = 87√3</p>
<p>Step 2:</p>
<p>In right ΔADC,</p>
<p>tan 60° = AD/CD</p>
<p>√3= 87/CD</p>
<p>CD = 87/√3 = 29√3</p>
<p>Step 3:</p>
<p>DE = CE – CD = (87√3 – 29√3) = 29√3(3 – 1) = 58√3</p>
<p>Distance travelled by the balloon = 58√3 m.</p>
<p><h6 class="question">Question 15</h6>  A straight highway leads to the foot of a tower. A man standing at the top of the tower observes a car at an angle of depression of 30°, which is approaching the foot of the tower with a uniform speed. Six seconds later, the angle of depression of the car is found to be 60°. Find the time taken by the car to reach the foot of the tower from this point.
</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-16.png" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>

<p>Let AB be the tower.</p>
<p>D is the initial and C is the final position of the car respectively.</p>
<p>Since man is standing at the top of the tower so, Angles of depression are measured from A.</p>
<p>BC is the distance from the foot of the tower to the car.</p>

<p>Step 1: In right ΔABC,</p>
<p>tan 60° = AB/BC</p>
<p>√3 = AB/BC</p>
<p>BC = AB/√3</p>
<p>AB = √3 BC</p>
<p>Step 2:</p>
<p>In right ΔABD,</p>
<p>tan 30° = AB/BD</p>
<p>1/√3 = AB/BD</p>
<p>AB = BD/√3</p>
<p>Step 3: Form step 1 and Step 2, we have</p>
<p>√3 BC = BD/√3 (Since LHS are same, so RHS are also same)</p>
<p>3 BC = BD</p>
<p>3 BC = BC + CD</p>
<p>2BC = CD</p>
<p>or BC = CD/2</p>
<p>Here, distance of BC is half of CD. Thus, the time taken is also half.</p>
<p>Time taken by car to travel distance CD = 6 sec. Time taken by car to travel BC = 6/2 = 3 sec.</p>
<p><h6 class="question">Question 16</h6> The angles of elevation of the top of a tower from two points at a distance of 4 m and 9 m from the base of the tower and in the same straight line with it are complementary. Prove that the height of the tower is 6 m.>
</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-17.jpeg" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>
<p>Let AB be the tower. C and D be the two points with distance 4 m and 9 m from the base respectively. As per question,</p>

<p>In right ΔABC,</p>
<p>tan x = AB/BC</p>
<p>tan x = AB/4</p>
<p>AB = 4 tan x … (i)</p>
<p>Again, from right ΔABD,</p>
<p>tan (90°-x) = AB/BD</p>
<p>cot x = AB/9</p>
<p>AB = 9 cot x … (ii)</p>
<p>Multiplying equation (i) and (ii)</p>
<p>AB<sup>2</sup> = 9 cot x × 4 tan x</p>
<p>⇒ AB<sup>2</sup> = 36 (because cot x = 1/tan x</p>
<p>⇒ AB = ± 6</p>
<p>Since height cannot be negative. Therefore, the height of the tower is 6 m.</p>
<p>Hence Proved.</p>

