<p>IN the figure </p>
<p>angle made by the rope with the ground level is 30° and Length of the rope is 20 m and we have to determine the height of the pole AB=?<br> So,.</p>
<p> AC = 20 m and angle θ= 30°</p>


<p>In right ΔABC,</p>
<p>sin 30° = AB/AC</p>

