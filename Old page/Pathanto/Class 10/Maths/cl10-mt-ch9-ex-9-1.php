<p><h6 class="question">Question 12</h6> From the top of a 7 m high building, the angle of elevation of the top of a cable tower is 60° and the angle of depression of its foot is 45°. Determine the height of the tower.</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-12.png" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
<p>Let AB be the building of height 7 m and EC be the height of the tower.</p>
<p>A is the point from where elevation of tower is 60° and the angle of depression of its foot is 45°.</p>
<p>EC = DE + CD</p>
<p>Also, CD = AB = 7 m. and BC = AD</p>
<p>To Find: EC = Height of the tower</p>
<p>Design a figure based on given instructions:</p>

<p>In right ΔABC,</p>
<p>tan 45° = AB/BC</p>
<p>1= 7/BC</p>
<p>BC = 7</p>
<p>Since BC = AD</p>
<p>So AD = 7</p>
<p>Again, from right triangle ADE,</p>
<p>tan 60° = DE/AD</p>
<p>√3 = DE/7</p>
<p>⇒ DE = 7√3 m</p>
<p>Now: EC = DE + CD</p>
<p>= (7√3 + 7) = 7(√3+1)</p>
<p>Therefore, Height of the tower is 7(√3+1) m. Answer!</p>
<p><h6 class="question">Question 13</h6> As observed from the top of a 75 m high lighthouse from the sea-level, the angles of depression of two ships are 30° and 45°. If one ship is exactly behind the other on the same side of the lighthouse, find the distance between the two ships.
</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-13.png" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>
<p>Let AB be the lighthouse of height 75 m. Let C and D be the positions of the ships.</p>
<p>30° and 45° are the angles of depression from the lighthouse.</p>
<p>Draw a figure based on given instructions:</p>
<p>
	
<p>To Find: CD = distance between two ships</p>
<p>Step 1: From right triangle ABC,</p>
<p>tan 45° = AB/BC</p>
<p>1= 75/BC</p>
<p>BC = 75 m</p>
<p>Step 2: Form right triangle ABD,</p>
<p>tan 30° = AB/BD</p>
<p>1/√3 = 75/BD</p>
