<p><h6 class="question">Question 1</h6> A circus artist is climbing a 20 m long rope, which is tightly stretched and tied from the top of a vertical pole to the ground. Find the height of the pole, if the angle made by the rope with the ground level is 30°. (see fig. 9.11)
</p>
