<p><h6 class="question">Question 6</h6> A 1.5 m tall boy is standing at some distance from a 30 m tall building. The angle of elevation from his eyes to the top of the building increases from 30° to 60° as he walks towards the building. Find the distance he walked towards the building.</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-6.jpeg" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
<p>Let the boy initially stand at point Y with inclination 30° and then he approaches the building to the point X with inclination 60°.</p>

<p>From figure,</p>
<p>XY = CD.</p>
<p>Height of the building = AZ = 30 m.</p>
<p>AB = AZ – BZ = 30 – 1.5 = 28.5</p>
<p>Measure of AB is 28.5 m</p>
<p>In right ΔABD,</p>
<p>tan 30° = AB/BD</p>
<p>1/√3 = 28.5/BD</p>
<p>BD = 28.5√3 m</p>
<p>Again,</p>
<p>In right ΔABC,</p>
<p>tan 60° = AB/BC</p>
<p>√3 = 28.5/BC</p>
<p>BC = 28.5/√3 = 28.5√3/3</p>
<p>Therefore, the length of BC is 28.5√3/3 m.</p>
<p>XY = CD = BD – BC = (28.5√3-28.5√3/3) = 28.5√3(1-1/3) = 28.5√3 × 2/3 = 57/√3 m.</p>
<p>Thus, the distance boy walked towards the building is 57/√3 m.</p>

<p><h6 class="question">Question 7</h6> From a point on the ground, the angles of elevation of the bottom and the top of a</p>

<p>transmission tower fixed at the top of a 20 m high building are 45° and 60° respectively. Find the height of the tower. 
</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-7.jpeg" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>
<p>Let BC be the 20 m high building.</p>
<p>D is the point on the ground from where the elevation is taken.</p>
<p>Height of transmission tower = AB = AC – BC</p>

<p>From figure, In right ΔBCD,</p>
<p>tan 45° = BC/CD</p>
<p>1 = 20/CD</p>
<p>CD = 20</p>
<p>Again,</p>
<p>In right ΔACD,</p>
<p>tan 60° = AC/CD</p>
<p>√3 = AC/20</p>
<p>AC = 20√3</p>
<p>Now, AB = AC – BC = (20√3-20) = 20(√3-1)</p>

<p>Height of transmission tower = 20(√3 – 1) m.</p>
<p><h6 class="question">Question 8</h6>A statue, 1.6 m tall, stands on the top of a pedestal. From a point on the ground, the angle of elevation of the top of the statue is 60° and from the same point the angle of elevation of the top of the pedestal is 45°. Find the height of the pedestal.</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-8.jpg" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>
<p>Let AB be the height of statue.</p>
<p>D is the point on the ground from where the elevation is taken.</p>
<p>To Find: Height of pedestal = BC = AC-AB</p>

<p>From figure,</p>
<p>In right triangle BCD,</p>
<p>tan 45° = BC/CD</p>
<p> 1 = BC/CD</p>
<p>          BC = CD …..(1)</p>
<p>Again,</p>
<p>In right ΔACD,</p>
<p>tan 60° = AC/AD</p>
<p> √3 = ( AB+BC)/CD</p>
<p> √3CD = 1.6 + BC</p>
<p> √3BC = 1.6 + BC (using equation (1)</sup></p>
<p> √3BC – BC = 1.6</p>
<p> BC(√3-1) = 1.6 </p>
<p> BC = 1.6/(√3-1) m</p>
<p> BC = 0.8(√3+1) </p>
<p>Thus, the height of the pedestal is 0.8(√3+1) m.</p>
<p><h6 class="question">Question 9</h6>The angle of elevation of the top of a building from the foot of the tower is 30° and the angle of elevation of the top of the tower from the foot of the building is 60°. If the tower is 50 m high, find the height of the building.
