<p><h6 class="question">Question 10</h6> Two poles of equal heights are standing opposite each other on either side of the road, which is 80 m wide. From a point between them on the road, the angles of elevation of the top of the poles are 60° and 30°, respectively. Find the height of the poles and the distances of the point from the poles.</p>
<img class="Q2ch3-5n mode" src="https://pathanto.com/Pathanto/image/cl10-ex9.1-10.jpeg" alt="NCERT Ex-9.1 class 10"><br>
<h6 class="answer">Answer</h6>
</p>
<p>Let AB and CD be the poles of equal height.</p>
<p>O is the point between them from where the height of elevation taken. BD is the distance between the poles.</p>

<p>As per above figure, AB = CD,</p>
<p>OB + OD = 80 m</p>
<p>Now,</p>
<p>In right ΔCDO,</p>
<p>tan 30° = CD/OD</p>
<p>1/√3 = CD/OD</p>
<p>CD = OD/√3 … (1)</p>
<p>Again,</p>
<p>In right ΔABO,</p>
<p>tan 60° = AB/OB</p>
<p>√3 = AB/(80-OD)</p>
<p>AB = √3(80-OD)</p>
<p>AB = CD (Given)</p>
<p>√3(80-OD) = OD/√3 (Using equation (1))</p>
<p>3(80-OD) = OD</p>
<p>240 – 3 OD = OD</p>
<p>4 OD = 240</p>
<p>OD = 60</p>
<p>Putting the value of OD in equation (1)</p>
<p>CD = OD/√3</p>
<p>CD = 60/√3</p>
<p>CD = 20√3 m</p>
<p>Also,</p>
<p>OB + OD = 80 m</p>
<p>OB = (80-60) m = 20 m</p>
<p>Thus, the height of the poles are 20√3 m and distance from the point of elevation are 20 m and</p>
<p>60 m respectively.</p>
<p><h6 class="question">Question 11</h6>A TV tower stands vertically on a bank of a canal. From a point on the other bank directly opposite the tower, the angle of elevation of the top of the tower is 60°. From another point 20 m away from this point on the line joing this point to the foot of the tower, the angle of elevation of the top of the tower is 30° (see Fig. 9.12). Find the height of the tower and the width of the canal.
