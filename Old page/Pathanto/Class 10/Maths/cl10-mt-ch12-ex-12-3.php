<h6 class="answer">Answer </h6>

<p>we know that any triangle drawn from diameter RQ to the circle is 90°</p>
<p><span>&#8736;</span>P = 90°</p>

<p>Using Pythagorean theorem,</p>
<p>QR<sup>2 </sup>= PR<sup>2</sup>+PQ<sup>2</sup></p>
<p> QR<sup>2 </sup>= 7<sup>2</sup>+24<sup>2</sup></p>
<p>QR= 25 cm </p>
<p>Hence, the radius of the circle = 25/2 cm</p>
