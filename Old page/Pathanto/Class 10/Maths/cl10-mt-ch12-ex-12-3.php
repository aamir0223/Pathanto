<p>=1/2 × 7 × 24 cm<sup>2</sup></p>
<p>= 84 cm<sup>2</sup></p>

<p>Now, the area of the semicircle = (πR<sup>2</sup>)/2</p>
<p>= ((22/7)×(25/2)×(25/2))/2 cm<sup>2</sup></p>
<p>= 13750/56 cm<sup>2 </sup>= 245.54 cm<sup>2</sup></p>

<p> the area of the shaded region = 245.54 cm<sup>2</sup>-84 cm<sup>2 </sup></p>
<p>= 161.54 cm<sup>2</sup></p>


