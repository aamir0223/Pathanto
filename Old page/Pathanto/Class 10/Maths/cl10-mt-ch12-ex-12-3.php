<p><h6 class="question">Question 5</h6>  From each corner of a square of side 4 cm a quadrant of a circle of radius 1 cm is cut and also a circle of diameter 2 cm is cut as shown in Fig. 12.23. Find the area of the remaining portion of the square.</strong></p>
<p><strong>Solution:</strong></p>
<p>Side of the square = 4 cm</p>
<p>Radius of the quadrant = 1 cm</p>

<p>Area of the quadrant = (60°/360°) x πR<sup>2</sup> cm<sup>2</sup> = (22/7)×(1<sup>2</sup>)/4 = 11/14 cm<sup>2</sup></p>
<p>∴So Total area of the 4 quadrants = 4 ×(11/14) cm<sup>2</sup> = 22/7 cm<sup>2</sup></p>
<p>Area of square = (side)<sup>2</sup>= 4<sup>2 </sup>= 16 cm<sup>2</sup></p>
<p>Area of the circle = πR<sup>2 </sup>cm<sup>2</sup> = (22/7×1<sup>2</sup>) = 22/7 cm<sup>2</sup></p>



<p>Area of the shaded region = Area of square – (Area of the all quadrants + Area of the circle)</p>
<p>= 16 cm<sup>2</sup>-(22/7) cm<sup>2</sup>+(22/7) cm<sup>2</sup></p>
<p>= 68/7 cm<sup>2</sup></p>


