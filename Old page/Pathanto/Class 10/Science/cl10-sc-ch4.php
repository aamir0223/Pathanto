<p><h4 class="question">Question 15</h4> Explain the mechanism of the cleaning action of soaps.</p>
<p>
	<h4 class="answer">Answer </h4> A soap molecule have of two parts:
tail. It is hydrophobic in nature.<br>
Head: It is hydrophilic in nature.<br>
When soap  added to dirty water then the hydrophobic part of soap get attach to dirt while the hydrophilic part remain on the surface of water.This formation is called a micelle. Soap in the form of a micelle is able
to clean, since the oily dirt will be collected in the centre of the micelle. The micelles stay in solution as
a colloid and will not come together to precipitat because of ion-ion repulsion. Thus, the dirt
suspended in the micelles is also easily rinsed away

