<p><h4 class="question">Question 8</h4> Why does micelle formation take place when soap is added to water? Will a micelle be formed in other solvents such as ethanol also?</p>
<p><h4 class="answer">Answer </h4> Soap molecule has two ends. One end is hydrophilic and another end is hydrophobic. when soap is added to water Micelle formation takes place due  the hydrocarbon chains of soap molecules are hydrophobic  which are insoluble in water, but the ionic ends of soap molecules are hydrophilic that is, it interacts with water and hence soluble in water.Micelle is not formed in other solvent such as ethanol.

