<p><h4 class="question">Question 9</h4> How are the alveoli designed to maximize the exchange of gases?</p>
<h4 class="answer">Answer</h4> 

<p>	The balloon-like structures present inside lungs which are called alveoli (singular–alveolus). The alveoli provide a surface where the exchange of gases can take place. The walls of the alveoli contain an extensive network of blood-vessels.Walls of alveoli are extremely thin and made of single layer of cells. when we breathe air is sucked into the lungs and fills the expanded alveoli .
