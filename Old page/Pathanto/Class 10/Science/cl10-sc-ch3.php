<p><h4 class="question">Question 12</h4> Give reasons </p>
<p>(a) Platinum, gold and silver are used to make jewellery. </p>
<p>(b) Sodium, potassium and lithium are stored under oil. </p>
<p>(c) Aluminium is a highly reactive metal, yet it is used to make utensils for cooking. </p>
<p>(d) Carbonate and sulphide ores are usually converted into oxides during the process of extraction</p>
<p><h4 class="answer">Answer</h4> </p>
<p>(a) Platinum, gold and silver are used to make jewellery because they are very less reactive , luster   malleable and ductile in nature and also show high corrosion .</p>

<p>(b) Sodium, potassium and lithium are stored under oil to prevent their reaction with air as they are very reactive and catch fire when exposed to air.</p>

<p>(c) Aluminium forms a non-reactive layer of aluminium oxide all over its surface in presence of air . This layer prevents aluminium to react with other substances. That’s why aluminium is used to make cooking utensils.</p>

<p>(d) It is easy to extract metal form its oxide rather than from their carbonates or sulphides.</p>

