<p><h4 class="question">Question 16</h4> Explain the underlying principle and working of an electric generator by drawing a labelled diagram. What is the function of brushes?</p>
<h4 class="answer">Answer</h4>
 <img class="Q2ch3-5n" src="/Pathanto/image/electric-generator.png" alt="Chapter 13 science class 10 solution NCERT solution"><br>
<p>Principle:It is base on principle of electromagnetic induction such that when a straight conductor is moved in a magnetic field then current is induced in conductor</p>

<p> Working: The rotating the loop in a magnetic field, the side AB moves upward and the side CD moves downwards respectively in the produced magnetic field. Thus, current is induced in it whose direction can be determined by Flemings right hand rule. After half rotation, the ring P comes in contact with brush Y and the ring Q, comes in contact with brush X. Thus, the brush X is always in contact with the side moving upwards and brush Y is always in contact with side moving downwards as a result of which the current flows in one direction. This current is called direct current. This type of generator is called D.C. generator. Similarly, instead of a half ring, if a full ring is used then, A.C. current can be generated and such a generator is called an A.C. generator.</p>

<p>Function of  brushes help in transferring current from coil to the external circuit.</p>

