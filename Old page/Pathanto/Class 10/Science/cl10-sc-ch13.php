<p><h4 class="question">Question 11</h4> Draw a labelled diagram of an electric motor. Explain its principle and working. What is the function of a split ring in an electric motor?</p>

<h4 class="answer">Answer</h4>

<img class="Q2ch3-5n" src="/Pathanto/image/motor.png" alt="Chapter 13 science class 10 solution NCERT solution"><br>

<p> Principle: :when a rectangular coil is placed in a magnetic field and current is passed through it, a force acts on the coil which rotates it continuously.</p>
<p>Working: Current in the coil ABCD enters from the source battery through conducting brush X and flows back to the battery through brush Y. the current in arm AB of the coil flows from A to B. In arm CD it flows from C to D, that is, opposite to the direction of current through arm AB. On applying Fleming’s left hand rule for the direction of force on a current-carrying conductor in a magnetic field the force acting on arm AB pushes it downwards while the force acting on arm CD pushes it upwards and it rotate anti-clockwise. At half rotation, Q makes contact with the brush X and P with brush Y. the current in the coil gets reversed and flows along the path DCBA. The reversal of current also reverses the direction of force acting on the two arms AB and CD. so it pushed down. Therefore the coil and the axle rotate half a turn more in the same direction. The reversing of the current is repeated at each half rotation, giving rise to a continuous rotation of the coil and to the axle</p>
<p> Split ring is used  reverses the direction of the current flowing through the coil after each half rotation of the coil. Due to this reversal of current, the coil continues to rotate in the same direction.</p>

