<p><h4 class="question">Question 18</h4> What is the function of an earth wire? Why is it necessary to earth metallic appliances?</p>
<h4 class="answer">Answer</h4>
<p>The earth wire  used as a safety measure, especially for those appliances that have a metallic body, for example, electric press, toaster, table fan, refrigerator, etc. The metallic body is connected to the earth wire, which provides a low-resistance conducting path for the current. Thus, it ensures that any leakage of current to the metallic body of the appliance keeps its potential to that of the earth, and the user may not get a severe
electric shock.</p>

 


   


