<p><h4 class="question">Question 13</h4> A coil of insulated copper wire is connected to a galvanometer. What will happen if a bar magnet is (i) pushed into the coil, (ii) withdrawn from inside the coil, (iii) held stationary inside the coil?</p>
<h4 class="answer">Answer</h4>
<p>(i) A current is induced in the coil When a bar magnet is pushed into the coil  as a result deflects produce in  galvanometer .</p>
<p>(ii) A current is induced in the coil When a bar magnet is withdrawn from inside the coil but in the opposite direction and the galvanometer deflects in the opposite direction .</p>

<p>(iii) No current will be induced  When a bar magnet is held stationary inside the coil  as a result there will be no deflection in the galvanometer.</p>


<p><h4 class="question">Question 14</h4> Two circular coils A and B are placed closed to each other. If the current in the coil A is changed, will some current be induced in the coil B? Give reason.</p>
<h4 class="answer">Answer</h4>
<p>Yes, A induced  current is  produce in the coil B. When the current in coil A changes, the magnetic field of  coil A  also changes. As a result the magnetic field around coil B will change. The change in the magnetic field of coil B induces current in it.</p>
