<p><h4 class="answer">Answer</h4> HCl is more reactive than acetic acid. Fizzing occurs due to  the production of the hydrogen gas when  reaction of the acid with magnesium ribbon. Since HCl produce more hydrogen gas which causes fizzing more vigorously.hence  more fizzing take place in test tube A.</p>

