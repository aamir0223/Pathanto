<p><h4 class="question">Question 5</h4> Write word equations and then balanced equations for the reaction taking place when </p>
<p>(a) Dilute sulphuric acid reacts with zinc granules. </p>
<p>(b) Dilute hydrochloric acid reacts with magnesium ribbon. </p>
<p>(c) Dilute sulphuric acid reacts with aluminium powder.</p>
<p>(d) Dilute hydrochloric acid reacts with iron filings.</p>
<h4 class="answer">Answer</h4>
<p>(a) dilute sulphuric acid reacts with zinc granules:</p>
<p>dilute sulphuric acid + zinc → Zinc Sulphate + Hydrogen Gas</p>
<p> H<sub>2</sub>SO<sub>4</sub>(aq) + Zn → ZnSO<sub>4</sub>(aq) + H<sub>2</sub>(g)</p>
<p>(b) dilute hydrochloric acid reacts with magnesium ribbon.</p>
<p> dilute Hydrochloric + Magnesium → Magnesium Chloride + Hydrogen Gas</p>
<p> 2HCl(aq) + Mg → MgCl<sub>2</sub>(aq) + H<sub>2</sub>(g)</p>
<p>(c) dilute sulphuric acid reacts with aluminium powder.</p>
<p> dilute Sulphuric Acid + Aluminium → Aluminium Sulphate + Hydrogen Gas</p>
<p> 3H<sub>2</sub>SO<sub>4</sub>(aq) + 2Al(s) → Al<sub>2</sub>(SO<sub>4</sub>)<sub>3</sub>(aq) + 3H<sub>2</sub>(g)</p>
<p>(d) dilute hydrochloric acid reacts with iron filings.</p>
<p> dilute Hydrochloric Acid + Iron → Ferrous Chloride + Hydrogen Gas</p>
<p> 6HCl(aq) + 3Fe(s) → 3FeCl<sub>2</sub>(aq) + 3H<sub>2</sub>(g)</p>
