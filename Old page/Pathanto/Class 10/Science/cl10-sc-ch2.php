<p><h4 class="question">Question 2</h4> A solution reacts with crushed egg-shells to give a gas that turns lime-water milky. The solution contains </p>
<p>a) NaCl (b) HCl (c) LiCl (d) KCl</p>
<p><h4 class="answer">Answer</h4> Answer is HCl.</p>
<p>The egg-shells are made up of calcium carbonate. When it reacts with HCl it  give CO<sup>2</sup>gas which turns lime water milky.</p>
<p>CaCO<sub>3</sub> + 2HCl → CaCl<sub>2</sub> + H<sub>2</sub>O + CO<sub>2</sub></p>
<p><h4 class="question">Question 3</h4> 10 mL of a solution of NaOH is found to be completely neutralised by 8 mL of a given solution of HCl. If we take 20 mL of the same solution of NaOH, the amount HCl solution (the same solution as before) required to neutralise it will be</p>
<p> (a) 4 mL (b) 8 mL (c) 12 mL (d) 16 mL</p>
<p><h4 class="answer">Answer</h4> d) 16mL because  10 ml of NaOH requires 8 mL of HCL, 20 ml of NaOH require  16mL of HCl.  </p>
<p><h4 class="question">Question 4</h4> Which one of the following types of medicines is used for treating indigestion?</p>
<p> (a) Antibiotic (b) Analgesic (c) Antacid (d) Antiseptic</p>
