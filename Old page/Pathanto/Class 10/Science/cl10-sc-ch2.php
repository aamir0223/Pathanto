<p><h4 class="question">Question 7</h4> Why does distilled water not conduct electricity, whereas rain water does?</p>
<h4 class="answer">Answer</h4>
Distilled water not conduct electricity because it is pure form of water it does not contain any ionic compounds in it.While rainwater contain dissolved gases like CO<sub>2</sub> ,O<sub>2</sub> etc. and form acid like carbonic acid  which dissociate into ions hence rainwater conduct electricity.
<p><h4 class="question">Question 8</h4>Why do acids not show acidic behaviour in the absence of water?</p>
<p><h4 class="answer">Answer</h4>  Acidic nature of a substance due to formation of H<sup>+</sup> ions. Hydrogen ions can only be produced in the presence of water .So acids not show acidic behaviour in the absence of water</p>
