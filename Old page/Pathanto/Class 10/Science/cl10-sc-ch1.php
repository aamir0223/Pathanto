<p><h4 class="question">Question 13</h4>
What is the difference between displacement and double displacement reactions? Write equations for these reactions.<br />
<h4 class="answer">Answer</h4><br />
A displacement reactions in which a  more reactive metal displaces a less reactive metal from its compound. For example,<br />
Zn(s) + CuSO<sub>4</sub>(aq) → Cu(s) + ZnSO<sub>4</sub>(aq)<br />
here  Zinc is more reactive   displaces copper from its solution.<br />
A double displacement reactions, a mutual exchange of ions between two compound to form two new compound. For example,<br />
AgNO<sub>3</sub>(aq) + NaCl (aq) → AgCl(s) + NaNO<sub>3</sub> (aq)<br />
Here silver nitrate and sodium chloride exchange Cl<sup>&#8211;</sup> and NO<sub>3</sub><sup>&#8211;</sup>  and form two new compound.</p>
