<p><h4 class="question">Question 9</h4>
What does one mean by exothermic and endothermic reactions ? Give examples.<br />
<h4 class="answer">Answer</h4>
A reactions in which heat is evolved are known as exothermic reactions <br>
Example :<br />
(i) C (s) + O<sub>2</sub> (g) → CO<sub>2</sub> (g) + Heat<br />
(ii) N<sub>2</sub> (g) + 3H<sub>2</sub> (g) → 2NH<sub>3</sub> (g) + Heat</p>
<p>Endothermic reactions : Those reactions in which heat is absorbed are known as endothermic reactions. it is usually indicated by writing &#8220;Heat” on the left side of a chemical equation.<br />
Examples :<br />
(i) C (s) + 2S (s) → CS<sub>2</sub> (l) &#8211; Heat<br />
(ii) N<sub>2</sub> (g) + O<sub>2</sub> (g) → 2NO(g) &#8211; Heat</p>
