<p>Some important changes that can help make homes more environment-friendly include:</p>
<ul>
	<li>Switch off the electrical appliances  once not needed.</li>
	<li>Using 3R's (Reduce, Reuse, and Recycle) formula</li>
<li>Sepration of the garbage into biodegradable waste  and non-biodegradable waste .</li>
<li>Use  cloth shopping bags and paper cups instead of plastics.</li>


</ul>
