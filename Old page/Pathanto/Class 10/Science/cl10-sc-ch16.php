<p><h4 class="question">Question 3</h4> We saw in this chapter that there are four main stakeholders when it comes to forests and wildlife. Which among these should have the authority to decide the management of forest produce? Why do you think so?</p>
<h4 class="answer">Answer</h4>
The four main stakeholder  are :<br>

1. The local people who live in and around the forest<br>

2. Forest department of the government<br>

3. The industrialist who use various forest products for their factories<br>
4. The forest and wildlife activities who want to save forest<br>
<p>The local people who live in and around the forest area should have the authority to decide the management of forest produce. This is because the local people are well versed in the practice to use the forest produce in a Sustainable manner as they have been using the forest and wildlife resources since the ancient times without causing any damage to the environment.</p>



<p><h4 class="question">Question 4</h4>How can you as an individual contribute or make a difference to the management of (a) forests and wildlife, (b) water resources and (c) coal and petroleum?</p>
<p><h4 class="answer">Answer</h4>
(a). An individual can contribute to the management of forest and wildlife resources by:</p>
<ul>
	<li>Organising tree Plantation Campaign and Promoting afforestation.</li>
<li>Protest against deforestation and wildlife poaching.</li>
<li>Reusing the paper or writing on both sides of the paper</li>
<li>No use of animal skin product</li>
</ul>
<p>(b). An individual can contribute towards the management of water resources by:</p>
<ul>
<li>Avoiding the wastage of water .</li>
<li>Using Rainwater harvesting to save water.</li>
<li> Turn the taps off while brushing or bathing and repair leaking water taps.</li>
</ul>
<p>(c). An individual can contribute towards the management of non-renewable energy sources such as coal and petrol by:</p>
<ul>
<li>Using of public transport and  bicycles.</li>
<li>Using solar energy for cook  heaters instead of electric .</li>
</ul>

