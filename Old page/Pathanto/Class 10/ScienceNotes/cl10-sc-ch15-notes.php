          <h5 class="sc-chapter-name">Chapter 15:Our Environment</h5>

          <p> The  wast meterial produced by the various activityies of human and animal are poisonous to some extend and can be divided two main groups<br>i) Biodegradable Wastes <br>ii)Non-Biodegradable Wastes </p>
          <p><b style="color:#6f4a8e;"> Biodegradable Wastes:</b><br>Substances that are broken down by biological processes are said
to be biodegradable.</p>
          <p><b style="color:#6f4a8e;"> Non-Biodegradable Wastes:</b><br>Substances that are not broken down in this manner
are said to be non-biodegradable. These substances may be inert and
simply persist in the environment for a long time or may harm the various
members of the eco-system</p>
          <p><b style="color:#6f4a8e;"> ECO-SYSTEM:</b><br>All organisms such as plants, animals, microorganisms and human
beings interact with each other
and maintain a balance in nature. All the interacting organisms in an
area together with the non-living constituents of the environment form
an ecosystem.</p>
          <p><b style="color:#6f4a8e;"> Component of eco-systems  :</b><br>i)Biotic component<br>ii) Abiotic Component </p>
          <p><b style="color:#6f4a8e;"> i)Biotic component :</b><br>Living organisms of the environment like the  human ,plants, animals, microbes, and fungi<br>
          It includes three types of organisms <br>i) Producers<br>ii)  Consumers<br>iii)  Decomposers:</p>
          <p><b style="color:#6f4a8e;"> Producers  :</b><br>The autotrophs organism are also called producers because they  can produce their food  from inorganic substance using light energy  called Photosynthesis.for example plant blue green algae</p>

          <p><b style="color:#6f4a8e;">  Consumers  :</b><br>These organisms which consume the food produced,
either directly from producers or indirectly by feeding on other consumers
are the consumers.<br>The consumers can be classified into the following types :<br>i)Herbivores <br> ii)Carnivores <br>iii)Omnivores</p>
          <p><b style="color:#6f4a8e;"> Herbivores  :</b>hese are organisms  which get their food by eating the green plant only . ex- rabbit, goats</p>
          <p><b style="color:#6f4a8e;"> Omnivores  :</b>The organisms who eat both plants and animals are called omnivores ex-human</p>
          <p><b style="color:#6f4a8e;">  Carnivores:   :</b>hese are organisms  which consume other animals.ex-tiger, lion,</p>
          <p><b style="color:#6f4a8e;"> Decomposers:  :</b>Decomposers break down the organic matter or waste material and release nutrients into the soil.  For example, bacteria, worms, slugs, and snails.</p>
         


          <p>Planktons are very minute or microscopic organisms freely floating on the surface of water in a pond, lake, river or ocean. Planktons are of two types : Phytoplanktons and Zooplanktons.</p>

