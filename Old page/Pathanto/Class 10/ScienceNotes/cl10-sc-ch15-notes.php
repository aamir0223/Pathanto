          <p><b style="color:#6f4a8e;"> phytoplanktons.  :</b>The microscopic aquatic plants freely floating on the surface of water are called phytoplanktons.</p>
          <p><b style="color:#6f4a8e;"> zooplanktons:</b>The microscopic aquatic animals freely floating on water are called zooplanktons. The freely floating protozoa are an example of zooplankton</p>
          <p><b style="color:#6f4a8e;"> Abiotic Components:  :</b>These are non-living components of an ecosystem like water rock etc</p>

          <b style="color:#6f4a8e;"> Food web  :</b>Each organism is
generally eaten by two or more other
kinds of organisms which in turn this series
of branching lines called a food web<br><img class="Q2ch3-5n" src="/Pathanto/image/food-web.JPG"><br><br>





          <p><b style="color:#6f4a8e;"> Food Chain :</b>The sequence of living organisms in a community in which one organism consumes another organism to transfer food energy, is called a food chain.<br><img class="Q2ch3-5n" src="/Pathanto/image/food-chain.JPG"><br><br></p>
          <p><b style="color:#6f4a8e;"> Trophic level  :</b>Each step or level of the food chain forms a trophic level<br>i)The autotrophs or the producers are at the first trophic level.<br>ii)The herbivores or the primary
consumers come at the second<br>iii)small carnivores or the
secondary consumers at the third <br>iv)the tertiary consumers form the fourth trophic level</p>
a) Pyramid of numbers: gives the number of organisms present at each trophic level.
It can be upright or inverted.<br>
b) Pyramid of biomass: gives the biomass of each trophic levels and could be upright or inverted.<br>
c) Pyramid of energy: is always upright as it shows the flow of energy from one trophic level to the next trophic level<br><br><img class="Q2ch3-5n" src="/Pathanto/image/energy.png" width="300" height="200"><br><br>

              <b style="color:#6f4a8e;"> Energy flow:</b>
Transfer of energy from one trophic level to another depicting its direction and amount<br>
