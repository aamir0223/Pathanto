for example, produces 10 million times the energy produced by the
combustion of an atom of carbon from coal.</p>
<p> <b style="color:#6f4a8e;">Nuclear fusion:</b><br> i)Fusion means joining lighter nuclei to make a heavier nucleus, most commonly
