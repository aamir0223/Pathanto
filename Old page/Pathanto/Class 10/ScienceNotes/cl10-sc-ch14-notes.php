          <h5 class="sc-chapter-name">Chapter 14:Sources of Energy</h5>
          <p>
The sources of energy which have been in use from a long time are called conventional sources of energy</p>
           <p>There are two type of sources of energy:</p>
            <p>i) Renewable Energy</p>
         <p>ii) Non-Renewable Energy</p>
         <p><b style="color:#6f4a8e;">i) Renewable Energy:</b>  The resources
which can be renewed or reproduced by
physical, chemical or mechanical processes
are known as renewable For example, solar and wind
energy, water,</p>
           <p><b style="color:#6f4a8e;">ii) Non-Renewable Energy:</b> These occur over
a very long geological time. Minerals and fossil
fuels are examples of such resources. These
resources take millions of years in their
formation.  like fossil fuels </p>
           <p><b style="color:#6f4a8e;">WHAT IS A GOOD SOURCE OF ENERGY?:</b><br>A good source of energy would be one<br>
 which would do a large amount of work per unit volume or mass,<br>
be easily accessible,<br>
 be easy to store and transport<br>
 perhaps most importantly, be economical</p>
          <p><b style="color:#6f4a8e;">Fuels:</b> The substance which are burn to produced heat energy is called fuels like LPG ,CNG ,wood etc<br></p><p><b style="color:#6f4a8e;">Good  or Ideal fuel  :</b><br>i)A good source of should be one:<br>

ii)Easily available<br>
iii)Easy to store and transport<br>
iv)proper ignition temperature<br>v)It should have a high calorific value</p>
          <p><b style="color:#6f4a8e;">CONVENTIONAL SOURCES OF ENERGY:</b> <br> The traditional source of energy which are used most of people are known as conventional sources of energy like  wood, coal</p>
           <p><b style="color:#6f4a8e;">Fossil fuels:</b>Fossil fuels are formed due to compression of dead organic matter(plant and animals) over millions of years  deep under the earth example coal or natural gas.<br>ii)The fossil fuels are non-renewable
sources of energy, so we need to conserve
them and we need to find alternate sources of energy </p>
            <p><b style="color:#6f4a8e;">Disadvantages of fossil fuels:</b> i) The oxides of carbon, nitrogen and sulphur that are released on burning
fossil fuels are acidic oxides. These lead to acid rain which affects our
water and soil resources<br>ii) the air pollution caused by burning of coal or petroleum products.<br>iii)The burning of fossil fuel produced high amount od CO<sub>2</sub> and other gases Which caused  green-house effect</p>
             <p><b style="color:#6f4a8e;">Controlling pollution : </b>The pollution caused by burning fossil fuels can be reduced
by increasing the efficiency of the combustion process and using various
techniques like  catalytic converter</p>
          <p><b style="color:#6f4a8e;">Thermal power plants:</b>Thermal power plants use steam produced by burning fossil fuels ( coal, oil gas) to move the turbines to generate electricity. are known as thermal power plants<br>ii) The transmission of electricity is more efficient than
transporting coal or petroleum over the same distance so  many
thermal power plants are set up near coal or oil fields. <br>iii) The burning of coal heat up water to produce steam which  runs the turbine to
generate electricity.</p>
