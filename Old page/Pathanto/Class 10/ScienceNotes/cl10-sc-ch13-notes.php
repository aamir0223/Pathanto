      <p> <b style="color:#6f4a8e;">ELECTRIC MOTOR:</b><br>An electric motor is a rotating device that converts electrical energy to
mechanical energy.</p>
      <p><b style="color:#6f4a8e;">Principle:</b><br> when a rectangular coil is placed in a magnetic field and current is passed through it, a force acts on the coil which rotates it continuously.</p>
      <p><b style="color:#6f4a8e;">Construction:</b><br><img class="Q2ch3-5n" src="/Pathanto/image/motor.png" width="100%" height="400"><br><br> An electric motor,  consists of a rectangular
coil ABCD of insulated copper wire. The coil is placed between the two
poles of a magnetic field such that the arm AB and CD are perpendicular
to the direction of the magnetic field. The
ends of the coil are connected to the two
halves P and Q of a split ring. The inner sides
of these halves are insulated and attached
to an axle. The external conducting edges of
P and Q touch two conducting stationary
brushes X and Y, respectively,</p>
      <p><b style="color:#6f4a8e;">Working:</b> Current in the coil ABCD enters from the
source battery through conducting brush
X and flows back to the battery through
brush Y. the current in arm AB
of the coil flows from A to B. In arm CD it
flows from C to D, that is, opposite to the
direction of current through arm AB. On
applying Fleming’s left hand rule for the
direction of force on a current-carrying conductor in a magnetic field  the force
acting on arm AB pushes it downwards while the force acting on arm
CD pushes it upwards and it  rotate anti-clockwise. At half rotation, Q makes
contact with the brush X and P with brush Y. the current in
the coil gets reversed and flows along the path DCBA. The
reversal of current also reverses the direction of force acting on the two
arms AB and CD. so it pushed down. Therefore the coil and the axle rotate half a turn more in
the same direction. The reversing of the current is repeated at each half
rotation, giving rise to a continuous rotation of the coil and to the axle</p>
      <p><b style="color:#6f4a8e;">Power Source:</b> A simple motor usually has a DC power source.  It supplies power to the motor armature or field coils.<br>
<b style="color:#6f4a8e;">Commutator:</b> A device that
reverses the direction of flow of current through a circuit is called a
commutator.<br>
<b style="color:#6f4a8e;">Field Magnet</b> The magnetic field helps to produce a torque on the rotating armature coil by virtue of Fleming’s left-hand rule.<br>

<b style="color:#6f4a8e;">Brushes::</b> It is a device that conducts current between stationary wires and moving parts, most commonly the rotating shaft</p>
