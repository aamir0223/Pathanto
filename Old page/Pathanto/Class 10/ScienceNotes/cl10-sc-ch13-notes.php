      <p><b style="color:#6f4a8e;">ELECTRIC GENERATOR:</b><br>A generator converts mechanical energy into electrical energy</p>

     <br><img class="Q2ch3-5n" src="/Pathanto/image/electric-generator.png" width="100%" height="400"><br><br> 



      <p> <b style="color:#6f4a8e;">Principle:</b>It is base on principle of electromagnetic induction such that when a straight conductor is moved in a magnetic field then currentis induced in conductor</p>
     <b style="color:#6f4a8e;">Constuction:</b> An electric generator  consists of a rotating
rectangular coil ABCD placed between the two poles of a permanent
magnet. The two ends of this coil are connected to
the two rings R1 and R2. The inner side of these
rings are made insulated. The two conducting
stationary brushes B1 and B2 are kept pressed
separately on the rings R1 and R2, respectively. The
two rings R1 and R2 are internally attached to an
axle. The axle may be mechanically rotated from
outside to rotate the coil inside the magnetic field.
Outer ends of the two brushes are connected to
the galvanometer to show the flow of current in the
given external circuit.

      <p><b style="color:#6f4a8e;">Working:</b> The rotating the loop in a magnetic field, the side AB moves upward and the side CD moves downwards respectively in the produced magnetic field. Thus,  current is induced in it whose direction can be determined by Flemings right hand rule.
After half rotation, the ring P comes in contact with brush Y and the ring Q, comes in contact with brush X. Thus, the brush X is always in contact with the side moving upwards and brush Y is always in contact with side moving downwards as a result of which the current flows in one direction. This current is called direct current. This type of generator is called D.C. generator. Similarly, instead of a half ring, if a full ring is used then, A.C. current can be generated and such a generator is called an A.C. generator.</p>
      <p><b style="color:#6f4a8e;">Note: The difference between the direct and alternating currents is that the
direct current always flows in one direction, whereas the alternating
current reverses its direction periodically. </b></p>
