 <p><b style="color:#6f4a8e;">Valency :</b> the
number of valence electrons present in the outermost shell of its atom.On moving left to right in period the number of velence electron increase  while  velence in group  remain same</p>
 <p><b style="color:#6f4a8e;">Atomic size:</b> the distance between the centre of the nucleus and the outermost shell of an isolated atom <br>
  In period the atomic radius decreases in moving from left to
right along a period. This is due to an increase in nuclear charge which
tends to pull the electrons closer to the nucleus and reduces the size of
the atom <br>
In group, the atomic size increases down the group. This is
because new shells are being added as we go down the group. This
increases the distance between the outermost electrons and the nucleus
so that the atomic size increases in spite of the increase in nuclear charge.</p>
<p></p>
 <p><b style="color:#6f4a8e;">Metallic:</b> metals tend to lose electrons while
forming bonds, they are electropositive in nature <br>Down the group, the effective nuclear charge experienced by valence electrons is decreasing because the outermost electrons are farther away
from the nucleus. so they  lost electron easily.  metallic
character decreases across a period and increases down a group</p>
 <p><b style="color:#6f4a8e;">Non-metals:</b>Non-metals  are electronegative. They tend to form
bonds by gaining electrons. so  non-metals are found on
the right-hand side of the Periodic Table towards the top.<br> These trends also help us to predict the nature of oxides formed by
the elements because it is known to you that the oxides of metals are
basic and that of non-metals are acidic in general.</p>
 


 
 




