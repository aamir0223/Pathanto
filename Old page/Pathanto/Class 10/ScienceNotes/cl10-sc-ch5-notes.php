<p><b style="color:#6f4a8e;">Modern
Periodic Law :</b>‘Properties of elements are a periodic function of their atomic number.’<br>The Modern Periodic Table has 18 vertical columns known as ‘groups’
and 7 horizontal rows known as ‘periods’. </p>
 <p><b style="color:#6f4a8e;">Periods in Modern Periodic Table:</b><ul>
 <li>Elements present in the same period have the same number of shells and atomic number increases by one unit on moving from left to right
in a period.</li>
 <li> 1st period contains only 2 elements it is known as the shortest period</li>
 <li>The second  and third period contain 8 elements each it known as short periods.</li>
 <li>The fourth  and the fifth period   contain 18 elements each it are called long periods</li>
 <li>The 6th period and 7th period contains 32 elements  it are known as the longest period.</li>
 

 </ul></p>
 <p><b style="color:#6f4a8e;">Characteristics of Periods:</b><ul>
 <li>On moving left to right in period the number of velence electron increase from one to eight</li><li>On moving left to right in period in each short period velency of element first increase from 1 to 4 then decreses 4 to 0</li>
 <li>On moving left to right in period metellic character of element decrease</li>
 <li>On moving left to right chemical reactivity first increase then decreases</li>
 <li>On moving left to right in period the basic nature decreases and acidic nature of oxide increases </li></ul></p>
 <p><b style="color:#6f4a8e;">Groups in Modern Periodic Table:</b>

<ul> <li>1st Group of elements are known as alkali metals.</li>
<li>2nd Group of elements are known as alkaline earth metals.</li>
<li>15th Group of elements are known as pnicogens.</li>
<li>16th Group of elements are known as chalcogens.</li>
<li>17th Group of elements are known as halogens.</li>
<li>18th  Group of elements are known as noble gases.</li>
<li> Group  3 to group 12 of elements are known as transition element</li>

</ul>
</p>
