                                    <p> <b style="color:#6f4a8e;"> How Do Metals and Non-metals React:</b> <br> When metal react with non metal they form ionic compounds<br>

                                    Metals lose valence electron(s) and form cations.<br>
Non-metals gain those electrons in their valence shell and form anions.<br>
The cation and the anion are attracted to each other by strong electrostatic force, thus forming an ionic bond.<br>
For example: In Sodium Chloride the ionic bond is formed by opposite charged Sodium and chloride ions.
 sodium atom has one electron in its outermost shell. If it loses the electron from its M shell then its L shell
now becomes the outermost shell and that has a stable octet. The nucleus of this atom still has 11 protons but the number of electrons has become 10, so there is a net positive charge giving us a sodium cation  Na<sup>+</sup>. On the other hand chlorine has seven electrons in its outermost shell and it requires one more electron to complete its octet. If sodium and chlorine
were to react, the electron lost by sodium could be taken up by chlorine.
After gaining an electron, the chlorine atom gets a unit negative charge,
because its nucleus has 17 protons and there are 18 electrons in its K, L
and M shells. This gives us a chloride anion C1<sup>–</sup><br>
