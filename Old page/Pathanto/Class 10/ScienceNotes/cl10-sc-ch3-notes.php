                    <p> <b style="color:#6f4a8e;">PHYSICAL PROPERTIES METALS:</b> <br>

                            •	<b style="color:#6f4a8e;">Hardness:</b> Most of the metals are hard, except alkali metals, such as sodium, potassium, lithium, etc. are very soft metals. These can be cut by using a knife.<br>
