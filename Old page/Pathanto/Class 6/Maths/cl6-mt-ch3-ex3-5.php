<h6 class="question">Question 7</h6>
The product of three consecutive numbers is always divisible by 6. Verify this statement with the help of some examples.
<h6 class="answer">Answer 7</h6>
Example 1:<br>
Take three consecutive numbers 3, 4 and 5<br>
4 is divisible by 2<br>
3 is divisible by 3.<br>
So, 3 x 4 x 5  = 60 is divisible by 6.<br>

Example 2:<br>
Take three consecutive numbers 8 ,9 and 10.<br>
 8 is divisible by 2.<br>
 9 is divisible by 3<br>
 8 x 9 x 10 = 720 is divisible by 6.<br>


 


