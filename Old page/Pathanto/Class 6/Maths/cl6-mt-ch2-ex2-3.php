<h6 class="question">Question 2:</h6>
 Which of the following will not represent zero:<br>

(a) 1 + 0<br>

(b) 0 × 0<br>

(c) 0 / 2<br>

(d) (10 – 10) / 2<br>

<h6 class="answer">Answer</h6>

(a) 1 + 0 = 1<br>

it does not represent zero<br><br>



<h6 class="question">Question 2:</h6>
 If the product of two whole numbers is zero, can we say that one or both of them will be zero? Justify through examples.

<h6 class="answer">Answer 2:</h6>

 Yes,if product of two whole numbers is zero then  one of them is zero<br>

Ex: 0 × 3 = 0 and 15 × 0 = 0<br>

If  both number are zero then the result also be zero<br>

Ex: 0 × 0 = 0<br><br>



<h6 class="question">Question 3:</h6>
 If the product of two whole numbers is 1, can we say that one or both of them will be 1? Justify through examples.

<h6 class="answer">Answer 3:</h6>

If the product of two whole numbers is 1, both the numbers should be equal to 1<br>

Ex: 1 × 1 = 1<br>

But 1 × 5 = 5<br>

 So, the product of two whole numbers will be 1, then both numbers to be multiplied are 1<br><br>

