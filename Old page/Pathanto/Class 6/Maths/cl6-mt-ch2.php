<h6 class="question"> Question 6.</h6>
Write the predecessor of:<br>
(a) 94<br>
(b) 10000<br>
(c) 208090<br>
(d) 7654321<br>
<h6 class="answer">Answer 6:</h6>
(a) Predecessor of 94 = 94 – 1 = 93<br>
(b) Predecessor of 10000 = 10000 – 1 = 9999<br>
(c) Predecessor of 208090 = 208090 – 1 = 208089<br>
(d) Predecessor of 7654321 =7654321 – 1 = 7654320<br>

