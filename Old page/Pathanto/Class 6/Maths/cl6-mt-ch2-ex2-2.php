<h6 class="question"> Question 5</h6>.
A taxidriver filled his car petrol tank with 40 litres of petrol on Monday. The next day, he filled the tank with 50 litre of petrol. If the petrol cost ₹44 per litre, how much did he spend in all on petrol?<br>
<h6 class="answer">Answer 5:</h6>
Petrol filled on Monday = 40L<br>
Cost of 1 litre petrol = ₹44<br>
Petrol filled on next days = 50L<br>
∴ Total money spent in both days:<br>
= ₹(40 x 44 + 50 x 44)<br>
= ₹(40 + 50) x 44 = ₹90 x 44 = ₹3960<br><br>


 
<h6 class="question"> Question 6</h6>.
A vendor supplies 32 litres of milk to a hotel in the morning and 68 litres of milk in the evening. If the milk costs ₹15 per litre, how much money is due to the vendor per day?
<h6 class="answer">Answer 6:</h6>
The hotel  supplied  milk in the morning = 32 litres<br>
The hotel  supplied  milk in the evening = 68 litres<br>
Cost of  1 litre milk = ₹15 <br>
Now<br>
= ₹ (32 x 15 + 68 x 15)<br>
= ₹(32 + 68) x 15<br>
= ₹100 x 15
= ₹1500

So<br>
₹1500 is due to the vender per days<br><br>




