<h6 class="question">Ex 2.2 class 6 Question 1</h6>.
Find the sum by suitable arrangement:<br>
(a) 837 + 208 + 363<br>
(b) 1962 + 453,+ 1538 + 647<br>
<h6 class="answer">Answer 1:</h6>
(a) 837 + 208 + 363 = (837 + 363) + 208<br>
= 1200 + 208= 1408<br>

(b) 1962 + 453 + 1538 + 647<br>
= (1962 + 1538) + (453 + 647)<br>
= 3500 + 1100 = 4600<br><br>


 
<h6 class="question"> Question 2:</h6>.
Find the product by suitable arrangement:<br>
(а) 2 x 1768 x 50<br>
(b) 4 x 166 x 25<br>
(c) 8 x 291 x 125<br>
(d) 625 x 279 x 16<br>
(e) 285 x 5 x 60<br>
(f) 125 x 40 x 8 x 25<br>
<h6 class="answer">Answer 2:</h6>
(a) 2 x 1768 x 50 <br>
= (2 x 50) x 1768 = 176800<br>
(b) 4 x 166 x 25 <br>
= 166 x (25 x 4) = 166 x 100 = 16600<br>
(c) 8 x 291 x 125<br>
 = (8 x 125) x 291 = 1000 x 291 = 291000<br>
(d) 625 x 279 x 16<br>
 = (625 x 16) x 279 = 10000 x 279 = 2790000<br>
(e) 285 x 5 x 60 <br>
= 285 x (5 x 60) = 285 x 300 = 85500<br>
(f) 125 x 40 x 8 x 25 <br>
= (125 x 8) x (40 x 25) = 1000 X 1000 = 1000000<br><br>

 
<h6 class="question"> Question 3</h6>.
Find the value of the following:<br>
(а) 297 x 17 + 297 x 3<br>
(б) 54279 x 92 + 8 x 54279<br>
(c) 81265 x 169 – 81265 x 69<br>
(d) 3845 x 5 x 782 + 769 x 25 x 218<br>
<h6 class="answer">Answer 3:</h6>
(a) 297 x 17 x 297 x 3 = 297 x (17 + 3)<br>
= 297 x 20 = 5940<br>


 
(b) 54279 x 92 + 8 x 54279 = 54279 x (92 + 8)<br>
= 54279 x 100 = 5427900<br>

(c) 81265 x 169 – 81265 x 69<br>
= 81265 x (169 – 69)<br>
= 81265 x 100 = 8126500<br>

(d) 3845 x 5 x 782 + 769 x 25 x 218 <br>
= 3845 x 5 x 782 + (769 x 5) x 5 x 218<br>
= 3845 x 5 x 782 + 3845 x 5 x 218<br>
= 3845 x 5 x (782 + 218)<br>
= 3845 x 5 x 1000<br>
= 19225 x 1000
= 19225000<br><br>


 
