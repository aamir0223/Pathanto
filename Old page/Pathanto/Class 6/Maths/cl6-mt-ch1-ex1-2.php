<h6 class="question">Question 10:</h6>
Medicine is packed in boxes, each weighing 4 kg 500 g. How many such boxes can be loaded in a can which cannot carry beyond 800 kg?<br>
<h6 class="answer">Answer 10:</h6>:

The weight of one box = 4 kg 500 g = 4 x 1000 g + 500 g = 4500 g<br>

Maximum load of van = 800 kg = 800 x 1000 g = 800000 g<br>

Number of boxes = 800000 ÷ 4500<br>

So<br>

 177 boxes can be loaded.<br><br>


