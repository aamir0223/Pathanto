<h6 class="question">Question 6:</h6>
Explain the process of making yarn from fibre.

<h6 class="answer">Answer 6:</h6>
Yarns are made up of thin strands called fibers. The process of making yarn from fibers is called spinning. In this process, a mass of cotton wool are drawn out and twisted. This brings the fibers together to form yarn. Spindle and charkha are device are  used to done Spinning at home  <br><br> 

  
