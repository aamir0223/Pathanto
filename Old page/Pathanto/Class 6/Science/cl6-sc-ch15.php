<h6 class="question">Question 1</h6>
 What is the composition of air?
<h6 class="answer">Answer 1</h6>
 Air is mixture of nitrogen, oxygen, carbon dioxide, water vapour and a few other gases. Some dust particles may also be present in it. in fact nitrogen and oxygen togather make up 99% of the air and remaining 1% is carbon dioxide and other gases.<br>


<h6 class="question">Question 2</h6> 
Which gas in the atmosphere is essential for respiration?
<h6 class="answer">Answer 2</h6>
 Oxygen.<br><br>


 

<h6 class="question">Question 3</h6>
 How will you show that air supports burning?<br>
<h6 class="answer">Answer 3</h6>
Take a small burning candle. Cover the burning candle with a glass jar. After few minutes the candle is extinguished. As the supply of air is stopped due to glass jar the burning of candle is also stopped. This experiment proves that air supports burning.<br><br>

