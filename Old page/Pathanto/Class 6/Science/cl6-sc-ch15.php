<h6 class="question">Question 4</h6> 
How will you show that air is dissolved in water?
<h6 class="answer">Answer 4</h6>
 Take some water in a glass vessel or beaker. Heat it slowly on a tripod stand.  look carefully at the inner surface of the vessel before reachin boiling point tiny bubbles on the inside rising up . On heating, air dissolved in water escapes in the form of these bubbles.<br><br>


 

