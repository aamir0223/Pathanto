 <h6 class="question">Question 4</h6>
A drawing sheet changes when you draw a picture on it. Can you reverse this change?
<h6 class="answer">Answer 4</h6>
It may be reversible and irreversible depends on situations<br>
(a) if we draw with the help of pencil, we can erase the drawing by using an eraser. Then, it is a reversible change.
(b)If we draw using a pen, we cannot erase the drawing. Then it is irreversed.


 

 
