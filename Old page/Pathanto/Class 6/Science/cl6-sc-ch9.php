        <h6 class="question">Question 1</h6>
        . What is a habitat?

<h6 class="nswer">Answer 1</h6>
 A habitat is the type of natural environment in which a particular species of organism lives. The organisms depend on their habitat for their food, water, air, shelter , protection and other needs. Habitat means a dwelling place.it is characterized by both physical and biological features<br><br>

<h6 class="question">Question 2</h6>
 How are cactus adapted to survive in a desert?
<h6 class="answer">Answer 1</h6>
 Cactus are adapted to survive in a desert as they have<br>
(i) The leaves present in cactus in shape of spiny that help to prevent water loss through transpiration.<br>
(ii) Stem is modified in such a way that it performs photosynthesis and conserves water.<br>
(iii) It has long roots that go deep inside the soil for absorbing water<br>

