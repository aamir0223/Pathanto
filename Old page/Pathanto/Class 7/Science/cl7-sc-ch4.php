<p><h5  class="question" >Question 5</h5> Discuss why wearing more layers of clothing during winter keeps us warmer than wearing just one thick piece of clothing.</p>
<h5  class="answer" >Answer </h5>

<p>More layers of clothing during winter keeps us warmer  because as they have a lot of space between them and  air gets trapped between layer. As air is a bad conductor of heat it does not allow the escape of the heat  out of the  body.</p>

