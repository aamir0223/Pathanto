<p><h5  class="question" >Question 1</h5> State similarities and differences between the laboratory thermometer and the clinical thermometer.</p>
<h5  class="answer" >Answer </h5>
<p>Similarities</p>
<ul>
<li>Both consists of a long, narrow, uniform glass tube</li>
<li> both  have a bulb at one end.</li>
<li>Bulbs of both the thermometers consist of mercury</li>
<li>The scale of both the thermometerwe use  celsius scale, indicated by °C.</li>
</ul>

<p>Differences</p>
<ul>
<li>Temperature range of clinical thermometer is  35<sup>0</sup>C to 42<sup>0</sup>C While  Temperature range of laboratory thermometer is -10<sup>0</sup>C to 110 <sup>0</sup>C</li>
<li>The clinical thermometer is used to measure body temperature, however, laboratory thermometer is Used to measure temperature in the laboratory</li>

</ul>







