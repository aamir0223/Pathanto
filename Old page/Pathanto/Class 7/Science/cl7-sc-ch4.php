<p><h5  class="question" >Question 7</h5> In places of hot climate it is advised that the outer walls of houses be painted white. Explain.</p>
<h5  class="answer" >Answer </h5>
<p>In places of hot climate, it is advised that the outer walls of houses be painted white because white colour is best reflected colour and  poor absorber of heat. So  the houses do not heat up too much</p>

