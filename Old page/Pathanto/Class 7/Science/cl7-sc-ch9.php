<p><h5 class="question">Question 4</h5>Explain how soil is formed.</strong></p>
<h6 class="answer">Answer </h6>
<p>Soil is formed by the breaking down of rocks by the action of wind, water and climate.
This process is called weathering.In this  process  rocks are converted to small pieces, which eventually turn to soil .</p>
<p><h5 class="question">Question 5</h5> How is clayey soil useful for crops?</strong></p>
<h6 class="answer">Answer </h6>
<p>Clayey soil is useful for crops for the following reasons</p>
<ul>
<li>It contain rich amount of minerals or  organic matter</li>
<li>It has good ability  to hold water </li>

<li>It is also rich in humus and very fertile .So it is suitable for growing crop like wheat and gram. </li>
</ul>
