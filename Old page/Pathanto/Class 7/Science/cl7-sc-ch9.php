<p>Prevention of soil pollution :</p>
<ul>
<li>industrial waste should be treated before disposed  in soil .</li>
<li>By using organic manure  insted of chemical fertilisers and pesticides</li>
<li>we should  not disposed plastic in soil it reduce the fertilty of soil</li>
</ul>

<p>Prevention of Soil erosion </p>
<ul>
<li>By using different method of croping like Crop Rotation,Strip Farming  </li>
<li>By afforestation  </li>
<li>By avoiding overgrazing of animals.</li>
