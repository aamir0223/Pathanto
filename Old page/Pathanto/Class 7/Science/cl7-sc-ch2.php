 <h6 class="question"> Question 13.</h6>
  Can we survive only on raw, leafy vegetables/grass? Discuss.

<h6 class="answer">Answer 13</h6>

No, It is because the grass is rich in cellulose, which is a type of carbohydrate that humans are not able to digest due to the absence of cellulose-digesting enzymes  We need all required nutrients, such as carbohydrates, proteins, lipids, vitamins and minerals, from them .So,We cannot survive only on raw, leafy vegetables/grass, because we need  balance diet to live long and healthy life.<br><br>




 



    
