 <h6 class="question"> Question 5.</h6>
  What are villi? What is their location and function?

<h6 class="answer">Answer 5</h6>
The finger-like outgrowths  on the inner surface of the small intestine which help in absorbing the digested food are called vill

The villi increase the surface area for absorption of the digested food. Each villus has a network of thin and small blood vessels close to its surface. The surface of the villi absorbs the digested food materials.<br><br>

