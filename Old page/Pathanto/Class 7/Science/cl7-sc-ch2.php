 <h6 class="question"> Question 10.</h6>
  Write one similarity and one difference between the nutrition in amoeba and human beings.

<h6 class="answer">Answer 10</h6>
Similarity: Both Amoeba and human beings are heterotrophs and derive nutrition from other organisms andboth need digestive juices for the digestion of food.<br>
Dissimilarity: Human beings have a complex digestive system and different nutrients are digested in separate regions. Amoeba does not have a digestive system and all the nutrients are digested in the food vacuole and  also Humans need to chew the food on the other hand amoeba does not need to chew the food.<br><br>

