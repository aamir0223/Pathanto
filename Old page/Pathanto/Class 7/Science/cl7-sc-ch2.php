 <h6 class="question"> Question 7.</h6>
  Name the type of carbohydrate that can be digested by ruminants but not by humans. Give the reason also.
<h6 class="answer">Answer 7</h6>

The name of the carbohydrate is cellulose. It is digested by ruminants but not by humans because  ruminants have a large sac-like structure called rumen which is located between the small intestine and large intestine. Certain enzymes present   which  digest cellulose and that is   not present in humans.<br><br>
