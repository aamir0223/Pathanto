<h6 class="question">Question 3.</h6>
 How would you test the presence of starch in leaves?

<h6 class="answer">Answer 3</h6>
Iodine solution is used to test leaves for the presence of starch. You need to:<br>

Step 1:Boilinga plant leaf in  water for 30 seconds (this stops its chemical reactions)<br>
Step 2:boiling it in  ethanol for a few minutes (this removes most of its colour)<br>
Step 3:wash with water and spread onto a white tile<br>
Step 4:add iodine solution from a dropping pipette<br>
After a few minutes, the parts of the leaf that contain starch turn blue-black<br><br>


<h6 class="question">Question 4.</h6>
 Give a brief description of the process of synthesis of food in green plants

<h6 class="answer">Answer 4</h6>

Green plants use a process called as photosynthesis to prepare their food. As we know leaves are the food factory of green plant and  photosynthesis occurs in leaves so all raw material must reach there.So

Water is taken from the roots of the plant, and it is transported to leaves of the plant.And
Carbon dioxide from air enter the leaves through pores know as stomata. The leaves contain green pigment know as chlorophyll which helps to hold sunlight.This energy is used to prepare food from carbon  dioxide and water.And in the process hydrogen combines with Oxygen and Hydrogen to form carbohydrates.<br>
