<p><h6 class="question">Question 9</h5> How do elephants living in the tropical rainforest adapt itself?</strong></p>
<h6 class="answer">Answer </h6>
<p>The elephant has adapted to the conditions of rainforest in many remarkable ways.

They have a strong sense of smell for which they use their trunk.
Its trunk is also used for picking up and holding food.
They have long ears that help them to keep cool in the hot and humid climate and to hear very soft sounds.
Its tusk are used to tear bark of trees for consumption.</p>
