<p><h5 class="question">Question 1</h5> Explain how animals dwelling in the forest help it grow and regenerate.</strong></p>
<h6 class="answer">Answer </h6>
<p>Animals help in growing and regenerating forests in many ways like they help in dispersing plant seeds which helps in growing of plants. Microorganisms provides nutriention and humus by  decaying dead plants and animals.

<p><h5 class="question">Question 2</h5> Explain how forests prevent floods.</strong></p>
<h6 class="answer">Answer </h6>
<p>Forests can soak up excess rainwater. Palnt root absorb running water and allow water to be drained into the ground so we can get clean water.</p>
<p><h5 class="question">Question 3</h5> What are decomposers? Name any two of them. What do they do in the forest?</strong></p>
<h6 class="answer">Answer </h6>
<p>The micro-organisms like  bacteria and fungi which decay the dead plants and animals and convert to humus are known as decomposers.  They clean the forest by decay plants and animals and  help in recycle the nutrients of soil .</p>

<p><h5 class="question">Question 4</h5> Explain the role of forest in maintaining the balance between oxygen and carbon dioxide in the atmosphere.</strong></p>
<h6 class="answer">Answer </h6>
<p>Plants prepare food by useing carbon dioxide by the process of  photosynthesis and  releases oxygen . In this way the oxygen and carbon dioxide  goes back in the atmosphere.</p>

<p><h5 class="question">Question 5</h5> Explain why there is no waste in a forest.</strong></p>
<h6 class="answer">Answer </h6>
<p>There is no waste in a forest because  micro-organisms like  bacteria and fungi convert waste to humus known as decomposers and these humus goes back to soil that plant take from soil .</p>
